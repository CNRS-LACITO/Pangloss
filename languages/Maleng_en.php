            <h1 align="center"><a name="Vietnamese"></a>Ngữ
liệu tiếng Maleng (Maleng, M&atilde;liềng v&agrave; Kha Phong)</h1>
            <strong>C&aacute;c
t&agrave;i liệu được lưu trữ: ngữ li&ecirc;̣u v&ecirc;̀ từ
vựng</strong>
            <p>Phần ghi
&acirc;m ở đ&acirc;y bao gồm c&aacute;c dữ liệu về từ
vựng. T&agrave;i liệu tiếng Rục được sưu tầm trong sự hợp t&aacute;c
v&oacute;i GS. TS. Trần Tr&iacute; D&otilde;i (VNU).</p>
            <br>
            <p style="font-weight: bold;">T&agrave;i
liệu tham khảo:&nbsp;</p>
Bảng từ
EFEO-CNRS-SOAS d&ugrave;ng trong c&ocirc;ng t&aacute;c điền
d&atilde; ng&ocirc;n ngữ học ở Đ&ocirc;ng Nam &Aacute;
(tải bảng từ <a
 href="https://halshs.archives-ouvertes.fr/halshs-01068533/"
 target="_blank">ở đ&acirc;y</a>). <br>
            <p> <span style="font-style: italic;">C&aacute;c
tư liệu về tiếng Chứt đ&atilde; được số h&oacute;a
v&agrave; bi&ecirc;n tập lại trong khu&ocirc;n khổ của một
đề t&agrave;i k&eacute;o d&agrave;i từ
th&aacute;ng 9/2014 đến th&aacute;ng 2/2016, với ng&acirc;n
s&aacute;ch từ chương tr&igrave;nh Thư
viện Khoa học Kỹ thuật
số <a href="http://www.bibliothequescientifiquenumerique.fr/"
 target="_blank">(Digital Scientific Library)</a>,
một chương
