demeurent tr&egrave;s peu connues.&nbsp;</p>
            <h3>Une des nombreuses pistes de recherche &agrave; explorer</h3>
            <p style="text-align: justify;">Parmi les multiples
questions de recherche &agrave; explorer, les langues austroasiatiques
parl&eacute;es dans le nord du Vietnam d&eacute;tiennent les clefs de
la compr&eacute;hension de ph&eacute;nom&egrave;nes d'embo&icirc;tement
de dialectes dont on trouve la trace dans les langues vietiques du nord
(vietnamien et mường). La langue ksingmul fait partie d'un ensemble de
langues austroasiatiques dont l'implantation au nord-ouest de l'actuel
Vietnam para&icirc;t tr&egrave;s ancien; les peuples avoisinants
consid&egrave;rent les Ksingmul comme autochtones. La langue ne
pr&eacute;sente pas d'occlusives sonores. Or le vietnamien et le&nbsp;mường
pr&eacute;servent pr&eacute;cis&eacute;ment des traces d'un substrat
sans occlusives sonores (Ferlus 1999).&nbsp;</p>
            <p style="text-align: justify;">En outre la langue ksingmul
est non tonale; une vaste &eacute;tude dialectale qui couvrirait tous
les villages o&ugrave; est encore parl&eacute; le ksingmul permettrait
&agrave; n'en pas douter des observations passionnantes au sujet des
contacts entre langues poss&eacute;dant des tons ; langues
poss&eacute;dant des registres de qualit&eacute; de voix ; et langues
ne poss&eacute;dant ni l'un ni l'autre.&nbsp;</p>
