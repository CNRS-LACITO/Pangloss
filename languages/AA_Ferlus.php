            <h2><a name="languages"></a>1.
Au sujet
des langues mon-khmer et de la famille austroasiatique</h2>
            <h3> Une
classification en dix groupes</h3>
            <p>Le mon-khmer fait
partie d'une famille plus &eacute;tendue : l'austroasiatique.
Ferlus (1996) indique utiliser "une classification en dix groupes
essentiellement bas&eacute;e sur celle de D. Thomas et R. Headley
(1970), &agrave; laquelle nous avons rajout&eacute; un groupe
constitu&eacute; des langues asliennes."</p>
            <p>1. <span style="font-style: italic;">Khasi</span>:
khynriam (nonglum), lyngngam, pnar (synteng), war, bhoi.</p>
            <p>2. <span style="font-style: italic;">Palaungique</span>
(palaung-wa): palaung/benglong (ta-ang, ra-ang, rumai), ryang, danaw,
wa/khawa (praok), lawa, khalok, bulang, samtao, angku, hu, u, lamet
/&nbsp;khamet, phsin (kha bit), khang.</p>
            <p>3. <span style="font-style: italic;">Khamouique</span>:
khamou (theng, rok), kesing mul (puoc), then, tay hat (&ouml;du),
mal (thin, phay, pray).</p>
            <p>4. <span style="font-style: italic;">Viet-muong</span>
(vietique)</p>
            <p>5. <span style="font-style: italic;">Katouique</span>
(s&ocirc;-souei): bru (s&ocirc;, mangkong, chali/tri), souei
(kuy/kuoy), katang, pakoh, katu/kantou, ngeh (nkriang), yir/in (ong).</p>
            <p>6. <span style="font-style: italic;">Bahnarique</span>
:<br>
- <span style="font-style: italic;">Nord</span>
: sedang, rengao, cua, hr&ecirc;, lamam, tampouan, alak, kaseng.<br>
- <span style="font-style: italic;">Ouest
            </span>: laven/boloven,
nyaheun, brao (lav&egrave;, cheng, sapouan).<br>
- <span style="font-style: italic;">Sud
            </span>: bahnar, mnong /
phnong, biat, stieng, maa', sr&ecirc;, lac, chrau.</p>
            <p>7. <span style="font-style: italic;">Khmer</span></p>
            <p>8.
P&eacute;arique: p&eacute;ar/por, chong,
samr&ecirc;/somray, saoch.</p>
            <p>9. M&ocirc;n:
m&ocirc;n, nyah kur (chao bon).</p>
            <p>10. Aslien:<br>
- <span style="font-style: italic;">Nord</span>
: tonga (mos), semang, kensiw, jahai, bateg, cheq wong.<br>
- <span style="font-style: italic;">Centre
            </span>: senoi, semai,
temiar, jah hut.<br>
- <span style="font-style: italic;">Sud
            </span>: semelai, mah
meri, semoq brio</p>
            <p>Langues non
class&eacute;s: mla bri (yumbri), mang, lai (bolyu).</p>
            <p style="text-align: justify;">"Cette
classification en dix groupes, qui ne pr&eacute;tend &ecirc;tre
ni la meilleure ni d&eacute;finitive, a l'avantage d'&ecirc;tre
claire, pratique et de satisfaire largement aux besoins courants. Les
groupes sont in&eacute;gaux en nombre de langues, le plus petit est
celui du khmer qui se r&eacute;duit &agrave; une seule langue
et ses dialectes, &agrave; l'oppos&eacute; le groupe bahnarique
est riche d'une vingtaine de langues. D'autre part, les distances entre
ces groupes ne sont pas &eacute;quivalentes et certains d'entre eux
pr&eacute;sentent des affinit&eacute;s qui permettent des
regroupements." (Ferlus 1996 :
9).</p>
            <h3>Situation
sociolinguistique actuelle : un remplacement qui va
s'acc&eacute;l&eacute;rant</h3>
            <p style="text-align: justify;">La
famille austroasiatique compte de nombreux repr&eacute;sentants
rest&eacute;s remarquablement conservateurs, en comparaison de la
famille sino-tib&eacute;taine, par exemple. Mais la situation
actuelle est celle d'un remplacement rapide par des langues dominantes
au plan local, ou par des langues nationales.&nbsp;A
titre d'exemple, une
monographie au sujet de la langue mảng (<span
 style="font-style: italic;">La langue mảng</span>,
Nguyễn Văn Lợi, Nguyễn Hữu
Ho&agrave;nh&nbsp;&amp;&nbsp;Tạ&nbsp;Văn
Th&ocirc;ng 2009) souligne le risque d'interruption de la
transmission de la langue et de la tradition orale, suite au
d&eacute;placement de la population, install&eacute;e dans une
r&eacute;gion qui s'est trouv&eacute;e recouverte par les eaux
de la retenue hydro-&eacute;lectrique de Sơn La.&nbsp;</p>
            <p style="text-align: justify;">"Kết
quả điều tra
d&acirc;n số năm 1999 cho biết, d&acirc;n tộc Mảng hiện
c&oacute; 2663
người, cư tr&uacute; tập trung tại c&aacute;c huyện
S&igrave;n Hồ, Mường Lay (thuộc tỉnh Lai
Ch&acirc;u), Mường T&egrave; (thuộc tỉnh Điện Bi&ecirc;n).
(...) Theo c&aacute;c t&aacute;c giả của c&ocirc;ng
tr&igrave;nh: "Những nh&oacute;m d&acirc;n tộc
thu&ocirc;c ngữ hệ Nam &Aacute; ở T&acirc;y Bắc Việt Nam",
xuất
bản năm 1972, ở nước ta người Mảng c&oacute; 241 gia
đ&igrave;nh, 1398 người (...).
So s&aacute;nh số lượng người Mảng theo t&agrave;i liệu
n&agrave;y với số liệu vừa tr&igrave;nh
b&agrave;y, đồng thời đối chiếu danh s&aacute;ch c&aacute;c
bản người Mảng đang sinh sống
hiện nay với danh s&aacute;ch 19 bản người Mảng cư tr&uacute;
trước kia, ch&uacute;ng ta
nhận thấy rằng: Sau 30 năm, số lượng người Mảng tăng gần gấp
đ&ocirc;i v&agrave; c&oacute;
sự thay đổi khu vực cư tr&uacute;: c&oacute; những bản trước
kia kh&ocirc;ng c&oacute; người Mảng,
nay c&oacute; tộc người n&agrave;y sinh sống, đồng thời,
c&oacute; những bản trước kia c&oacute;
người Mảng, nay kh&ocirc;ng c&ograve;n nữa." (pp. 15-17)</p>
            <p style="text-align: justify;">"Do
điều kiện lịch
sử, địa l&iacute;, d&acirc;n số hiện nay, văn h&oacute;a
v&agrave; ng&ocirc;n ngữ Mảng c&oacute; nguy cơ mai một,
dẫn đến ti&ecirc;u vong. Điều lưu &yacute; l&agrave;, địa
b&agrave;n cư tr&uacute; của người Mảng hiện nay đều nằm trong
v&ugrave;ng l&ograve;ng hồ thủy điện Sơn La, việc di dời
c&aacute;c l&agrave;ng bản của họ đi nơi kh&aacute;c
l&agrave; kh&ocirc;ng tr&aacute;nh khỏi. Do đ&oacute;,
việc thu thập t&agrave;i liệu về ng&ocirc;n ngữ, văn
h&oacute;a Mảng trở n&ecirc;n v&ocirc; c&ugrave;ng cấp
thiết." (p. 19)<br>
            </p>
            <p>"Tiếng Mảng hiện
nay
ph&acirc;n bố ở một vị tr&iacute; tương đối biệt lập,
t&aacute;ch khỏi c&aacute;c ng&ocirc;n ngữ Mon - Khmer
kh&aacute;c. Trong v&agrave;i ba thế kỉ gần đ&acirc;y,
ng&ocirc;n ngữ n&agrave;y tiếp x&uacute;c nhiều với
c&aacute;c ng&ocirc;n ngữ Th&aacute;i t&acirc;y - nam
(Th&aacute;i Đen, Th&aacute;i Trắng ở Việt Nam, Đai ở
V&acirc;n Nam) v&agrave; c&aacute;c ng&ocirc;n ngữ Tạng
- Miến (c&aacute;c ng&ocirc;n ngữ nh&oacute;m Miến Điện -
L&ocirc; L&ocirc; như Di, H&agrave; Nh&igrave;,
Ph&ugrave; L&aacute;, C&ocirc;ống..., ở bi&ecirc;n giới
Việt Trung).<br>
Tiếng Mảng đ&atilde; trải qua một qu&aacute; tr&igrave;nh
biến đổi l&acirc;u d&agrave;i v&agrave; s&acirc;u sắc
trong sự tiếp x&uacute;c với c&aacute;c ng&ocirc;n ngữ
Th&aacute;i - Kađai v&agrave; Tạng - Miến -- c&aacute;c
ng&ocirc;n ngữ kh&aacute;c về nguồn gốc v&agrave; một số
đặc điểm loại h&igrave;nh học. Tiếng Mảng c&oacute; nhiều từ
vay mượn từ tiếng Th&aacute;i. Một số đặc điểm ngữ &acirc;m,
ngữ ph&aacute;p của ng&ocirc;n ngữ n&agrave;y
kh&ocirc;ng giống với c&aacute;c ng&ocirc;n ngữ Mon -
Khmer. T&iacute;nh chất cận &acirc;m tiết t&iacute;nh
(sesquisyllable) ở tiếng Mảng kh&ocirc;ng r&otilde; rệt như ở
c&aacute;c ng&ocirc;n ngữ Mon - Khmer kh&aacute;c, kể cả
c&aacute;c ng&ocirc;n ngữ Việt - Mường (Vietic) như Arem, Rục.
Tiếng Mảng c&oacute; hệ thống thanh điệu ph&aacute;t triển;
thanh điệu biến đổi trong ngữ lưu." (pp. 36-37)</p>
            <p>(Nguyễn Văn Lợi,
Nguyễn Hữu Ho&agrave;nh&nbsp;&amp;&nbsp;Tạ&nbsp;Văn
Th&ocirc;ng. 2009. <span style="font-style: italic;">Tiếng Mảng</span>.
H&agrave; Nội: NXB Khoa học
X&atilde; hội.)</p>
