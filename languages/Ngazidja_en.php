      <p>The language of Grande Comore, the most northerly island in the Comoro Archipelago, is a Bantu language, as is also Swahili, but differs greatly nonetheless. Guthrie classified the two languages in the same G.40 group, but there is no intercomprehension between Swahili and the Comoro languages, no more than between French and Italian.</p>
Ngazidja (Grand-comorian) is quite close to Mwali, the language spoken on Moheli island, but is quite distinct from Maore, another Bantu language spoken on Mayotte, which is closer to Nzuani, spoken on Anjouan.
