            <h1 align="center">Mường corpus</h1>
            <p class="intertitre">&nbsp;</p>
            <p><b>Muong
(Mường) </b>is the closest relative
of Vietnamese, the national language of Vietnam. Within the Vietic
group of the Austroasiatic language family, the Northern Vietic
subgroup (also known as "Viet-Muong") consists of Vietnamese and Muong.
Among the language varieties which evolved historically out of
Proto-Viet-Muong, Vietnamese gradually became the most widespread and
influential; in Michel Ferlus's classification, all the other language
varieties of this subgroup are referred to as "Muong".
Linguistically
speaking, the Nguồn language is also part of Muong.</p>
            <h4 align="left">Resources
