            <h2><a name="language"></a>1.
About Phunoi (Phou Noy)</h2>
            <p style="text-align: justify;">Phunoi is a member
of the Loloish subgroup of the
Sino-Tibetan
family. The Phunoi people&nbsp;live in Northern Thailand
and in&nbsp;areas straddling&nbsp;the Burma-Laos border. Michel
Ferlus (1977b) notes that Phunoi is
close to&nbsp;Pyen (spoken in Kengtung,
Burma); Bisu (spoken
in China and Thailand); Mpi&nbsp;and Lawa (both spoken in Northern
Thailand). Most Phunoi speakers live in Laos in Phongsali Province,
Louang Namtha
Province, Houaphanh Province but also around the cities of Luang
Phrabang and Vientiane (Ferlus 1971; Bradley 2007; Bout&eacute;
2010). Edmondson (2005) distinguishes five varieties of
Phunoi:&nbsp; Black Khoany, Hwethom, Khaskhong, Mung,
White Khoany. The variety spoken by Phunoi people in Vietnam (Mường
T&egrave; District, Lai Ch&acirc;u Province, Northern Vietnam) <b>
            </b>is referred to as
Cố&ocirc;ng (Bradley 1977; Phạm 1998).</p>
            <p style="text-align: justify;">According
to the available information,
Phunoi remains a vigorous language. The Loloish
branch of languages&nbsp;was&nbsp;the focus
of intensive documentation and research in the 1970s: see in
particular Matisoff 1969, 1972; Bradley 1978; and, concerning Phunoi,
Ferlus's publications
from 1971 and 1980, which contain information about phonology
(Ferlus 1975), morphology and syntax (Ferlus 1975,
1977a, 1979, 1980), semantics (Ferlus 1976), and ethnography
(Ferlus 1977b).
Since then, linguistic research into Loloish languages of Southeast
Asia has been somewhat less intense, in comparison with research about
varieties spoken in China (see e.g. the fine-grained of tone change in
progress conducted by Yang 2015). By present-day standards, Phunoi
remains an
underdocumented language. Fresh fieldwork on
Phunoi would seem well worth conducting, with strong potential
contributions to the field of&nbsp;Loloish linguistics, and to
general linguistics.&nbsp;Morpho-phonologically, Phunoi shows sandhi
phenomena
which have not been the object of in-depth exploration. From a
sociolinguistic point of view, the current geographical distribution of
Phunoi in Laos means that it is not currently under massive Chinese
influence (unlike
Loloish languages in China, where language shift to Mandarin is under
way); its current linguistic dynamics in
Southeast Asian settings appears well worth exploring, in conjunction
with an analysis of social dynamics (Bout&eacute; 2011).</p>
