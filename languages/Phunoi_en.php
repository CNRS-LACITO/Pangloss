            <h2><strong><a name="arcived_doc"></a>2.
The
archived documents</strong></h2>
            <p style="text-align: justify;">Ferlus's
data
on Phunoi mainly consist of vocabulary elicitation and narratives. In
addition, this collection comprises songs and rituals. The fieldwork
took place in Laos (Vientiane and Luang Phrabang) mainly in the 60's
and the 70's, and then in 1972 in France when a Phunoi speaker stayed
in
this country on a training course. "At that time, Laos was
in an unstable political-military situation and
it became impossible to gain access to remote areas, precisely those
which had the most minority languages. Many of them had never been
described or even identified." (Ferlus 2011)</p>
            <p style="text-align: justify;">The
majority of the&nbsp;texts recorded by
Michel Ferlus have been transcribed,
annotated and translated (into French). Ferlus currently plans to edit
his&nbsp;unpublished typed manuscripts during the year 2016 and
publish them. It is hoped that, thanks to the availability of these
data, the research community will be more easily able to build upon
Michel Ferlus's research, and to extend it, achieving cumulative
progress in documentation and research on Phunoi. Colleagues
interested to contribute to&nbsp;documentary efforts are encouraged to
get in touch.</p>
