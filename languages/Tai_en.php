            <p class="intertitre">&nbsp;</p>

            <p>About Tai Yo,
a.k.a. Tay Yo, see pp. 304-309 of:&nbsp;</p>

            <div style="line-height: 1.35; padding-left: 2em; text-indent: -2em;" class="csl-bib-body">
            <div class="csl-entry">Ferlus,
Michel. 2008. The Tai
dialects of Nghệ An, Vietnam (Tay Daeng, Tay Yo, Tay Muong). In Anthony
Diller, Jerold A. Edmondson &amp; Luo Yongxian (eds.), <i>The
Tai-Kadai languages</i>,
298&ndash;316. London &amp; New York:
Routledge.</div>

            <span class="Z3988" title="url_ver=Z39.88-2004&amp;ctx_ver=Z39.88-2004&amp;rfr_id=info%3Asid%2Fzotero.org%3A2&amp;rft_val_fmt=info%3Aofi%2Ffmt%3Akev%3Amtx%3Abook&amp;rft.genre=bookitem&amp;rft.atitle=The%20Tai%20dialects%20of%20Ngh%E1%BB%87%20An%2C%20Vietnam%20(Tay%20Daeng%2C%20Tay%20Yo%2C%20Tay%20Muong)&amp;rft.place=London%20%26%20New%20York&amp;rft.publisher=Routledge&amp;rft.aufirst=Michel&amp;rft.aulast=Ferlus&amp;rft.au=Michel%20Ferlus&amp;rft.au=Anthony%20Diller&amp;rft.au=Jerold%20A.%20Edmondson&amp;rft.au=Luo%20Yongxian&amp;rft.date=2008&amp;rft.pages=298-316&amp;rft.spage=298&amp;rft.epage=316"></span></div>

            <p> This language
variety is closely related to Tai Paw:&nbsp;diachronically, Tai Pao
and Tai Yo are considered as originating in
the same language (referred to by Michel Ferlus and other linguists as
Tai Muong). Their great proximity is reflected in the peculiar
treatment of some Proto-Southwestern Tai phonemes in both Tai Pao and
Tai Yo.</p>

            <p></p>

            <strong>The
archived documents: two lexical surveys, and&nbsp;read manuscripts
(c. 1997-2004); and folklore and vocabulary recorded in 2014</strong>
            <p>The recordings made
available here were recorded from 1997 to 2004; an extra set of
recordings was conducted in 2014. For information on the
speakers, see the metadata accompanying each document.&nbsp; </p>

