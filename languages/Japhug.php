            <h1 align="center"><a name="up"></a>Corpus
japhug</h1>
            <p class="intertitre">&nbsp;</p>
            <h3 align="left"><a name="resources"></a>Ressources</h3>
            <p class="intertitre" align="left"> <img
 src="../../images/icones/h_parleur.gif" alt="a" height="20" width="20">
            <a href="../tools/list_rsc.php?lg=Japhug&amp;aff=Japhug"
 target="_blank">Acc&egrave;s
&agrave; toutes
les ressources</a> </p>
            <p class="intertitre" align="left">Pr&eacute;sentation de
la langue
japhug, et du groupe des langues rGyalrong, sur
le <a href="http://panchr.hypotheses.org/264" target="_blank">carnet de recherche</a>
de <a href="https://cnrs.academia.edu/GuillaumeJacques" target="_blank">Guillaume
Jacques</a>
:
&nbsp;</p>
            <p class="intertitre" align="left">"La langue japhug est
parl&eacute;e dans la partie tib&eacute;taine du Sichuan (par
moins de dix mille personnes); j'&eacute;tudie
la vari&eacute;t&eacute; du
village
de Kamnyu, situ&eacute; ici:</p>
            <p><iframe
 src="https://www.google.com/maps/embed?pb=%211m10%211m8%211m3%211d374726.30953588494%212d101.956828%213d32.206583%213m2%211i1024%212i768%214f13.1%215e1%213m2%211sfr%212sfr%214v1431252874555"
 frameborder="0" height="450" width="600"></iframe></p>
            <p>Je travaille sur le
japhug depuis 2002. Mon travail consiste &agrave; collecter des
histoires traditionnelles et des conversations, &agrave; compiler
un dictionnaire multim&eacute;dia (qui devrait para&icirc;tre
tr&egrave;s bient&ocirc;t sur le site Pangloss) et &agrave;
analyser la grammaire de cette langue &agrave; travers des livres
et des articles. Cette approche <a
 href="https://fr.wikipedia.org/wiki/Franz_Boas">boasienne</a>
me semble la seule appropri&eacute;e pour obtenir des
donn&eacute;es fiables en linguistique et en ethnologie sur les
langues et les cultures de tradition orale menac&eacute;es.</p>
            <p>Voici une
photographie de mes informateurs principaux, Tshendzin 陈珍 et Dpalcan
柏尔青, prise chez eux en 2012 (...). En plus de s&eacute;jours de
terrain presque tous les ans, je suis en contact
t&eacute;l&eacute;phonique r&eacute;gulier avec eux et
d&rsquo;autres amis de la r&eacute;gion.</p>
            <p><a
 href="https://panchr.hypotheses.org/files/2015/05/IMG_0185.jpg"><img
 src="https://panchr.hypotheses.org/files/2015/05/IMG_0185-300x225.jpg"
 alt="IMG_0185" class="alignnone size-medium wp-image-267" height="225"
 width="300"></a></p>
            <p>Quel est
l&rsquo;int&eacute;r&ecirc;t plus
g&eacute;n&eacute;ral de travailler sur une langue obscure,
inconnue du grand public en Chine et m&ecirc;me dans les
r&eacute;gions tib&eacute;taines, o&ugrave; on la
consid&egrave;re comme un dialecte bizarre du tib&eacute;tain?</p>
            <p>Tout
d&rsquo;abord, c&rsquo;est le v&eacute;hicule de la culture
locale. Le corpus d&rsquo;histoires traditionnelles et de films que
j&rsquo;ai recueilli (transcrit mais pas encore traduit) contient
des descriptions originales de la faune et de la flore locale, ainsi
que des informations d&rsquo;un grand int&eacute;r&ecirc;t
sur les techniques agricoles, de construction de maisons et de tissage.
            </p>
            <p>Ensuite,
c&rsquo;est une langue dont la structure grammaticale est
tr&egrave;s particuli&egrave;re, voire unique au monde. Le
japhug et quelques langues voisines sont les seules langues en dehors
des Am&eacute;riques &agrave; avoir un syst&egrave;me
d&rsquo;accord de type <a
 href="https://www.academia.edu/968767/The_inverse_in_Japhug_Rgyalrong">direct-inverse</a>,
font partie des rares langues <a
 href="https://www.academia.edu/2008163/Harmonization_and_disharmonization_of_affix_ordering_and_basic_word_order">&agrave;
la fois verbe-finales et majoritairement pr&eacute;fixantes</a>,
les seules langues de Chine &agrave; <a
 href="https://www.academia.edu/1627216/From_denominal_derivation_to_Incorporation">incorporation
nominale</a> et quasiment les seules
langues d&rsquo;Eurasie avec un <a
 href="https://www.academia.edu/4483958/Denominal_affixes_as_sources_of_antipassive_markers_in_Japhug_Rgyalrong">antipassif</a>
qui ne soit pas en m&ecirc;me temps moyen,
autob&eacute;n&eacute;factif ou r&eacute;fl&eacute;chi.
Le japhug est la seule langue au monde dont <a
 href="https://www.academia.edu/8418290/Generic_person_marking_in_Japhug_and_other_Rgyalrong_languages">l&rsquo;inverse
marque aussi le g&eacute;n&eacute;rique du sujet transitif</a>.
Il a aussi un syst&egrave;me tr&egrave;s riche d&rsquo;<a
 href="https://www.academia.edu/5951569/Ideophones_in_Japhug_Rgyalrong">id&eacute;ophones</a>,
qui donnent &agrave; cette langue une forme
d&rsquo;expressivit&eacute; peu commune.</p>
            <p>Enfin,
c&rsquo;est une langue qui pr&eacute;sente des traits
conservateurs parmi les langues sino-tib&eacute;taines du point de
vue de la <a
 href="https://www.academia.edu/1526166/Agreement_morphology_the_case_of_Rgyalrongic_and_Kiranti">morphologie
verbale</a> et de la <a
 href="https://www.academia.edu/968769/Approaching_the_historical_phonology_of_three_highly_eroded_Sino-Tibetan_languages_Naxi_Na_and_Laze">phonologie</a>.
Cette langue permet de mieux comprendre l&rsquo;histoire de langues
litt&eacute;raires plus &ldquo;importantes&rdquo; comme le <a
 href="https://www.academia.edu/11754167/On_the_cluster_sr_in_Sino-Tibetan">chinois</a>
et le <a
 href="https://www.academia.edu/1142880/An_internal_reconstruction_of_Tibetan_stem_alternations">tib&eacute;tain</a>.
Son importance pour la reconstruction de l&rsquo;histoire des
langues sino-tib&eacute;taines est comparable &agrave; celle du
sanskrit pour l&rsquo;indo-europ&eacute;en. Mais si personne
n&rsquo;oserait se pr&eacute;tendre
indo-europ&eacute;aniste sans conna&icirc;tre le sanskrit, le
japhug et les autres langues gyalrongs sont malheureusement
n&eacute;glig&eacute;es par les linguistes. </p>
            <p>C&rsquo;est
pour cette raison que j&rsquo;essaie de mettre en place
&agrave; Paris une &eacute;quipe de sp&eacute;cialistes de
langues gyalronguiques, comprenant pour le moment <a
 href="http://khroskyabs.info/fr.html">Lai Yunfan</a>,
sp&eacute;cialiste du <a
 href="https://www.academia.edu/3990414/La_morphologie_affixale_du_lavrung_wobzi">Khroskyabs</a>
et <a href="https://ens.academia.edu/XunGong">Gong Xun</a>
du <a
 href="https://www.academia.edu/3255902/The_Personal_agreement_system_of_Zbu_rGyalrong_Ngyaltsu_variety_">zbu</a>.
Esp&eacute;rons que nous parviendrons &agrave; terme
&agrave; d&eacute;crire de fa&ccedil;on satisfaisantes ces
langues vuln&eacute;rables, et &agrave; leur donner la place
qu&rsquo;elle m&eacute;ritent en linguistique historique et
typologique. Peut-&ecirc;tre qu&rsquo;un jour la connaissance
d&rsquo;une langue rgyalrong, en plus du chinois et du
tib&eacute;tain, sera un pr&eacute;-requis pour faire un
doctorat en linguistique compar&eacute;e du
sino-tib&eacute;tain, comme c&rsquo;est le cas avec le sanskrit
en indo-europ&eacute;en."&nbsp;<span style="font-style: italic;"><br>
            </span></p>
            <div style="text-align: right;"><span
 style="font-style: italic;">(Extrait
du <a href="https://panchr.hypotheses.org/" target="_blank">Carnet
de recherche</a> de Guillaume
Jacques.)</span></div>
