            <h2><a name="language"></a>1.
A propos du phou noy</h2>
            <p style="text-align: justify;">Le phou noy est une
langue du groupe loloish
de la famille des langues sino-tib&eacute;taines. Les Phou Noy
(Phunoi)&nbsp;vivent au nord du Laos et dans des zones &agrave; cheval sur la
fronti&egrave;re
entre Birmanie et Laos. Michel Ferlus (1977b) rel&egrave;ve une
forte proximit&eacute; entre le phou
noy
et d'autres langues loloish telles que le pyen/phen (parl&eacute;
aux
alentours de Kengtung, &eacute;tat Shan, Birmanie); le bisu
(parl&eacute; en
Chine et en Tha&iuml;lande); le mpi et le lawa (parl&eacute;s
dans le nord de la Tha&iuml;lande). La plupart des locuteurs du
phoy
noy
vivent au Laos dans les provinces de Phongsaly, de Luang Namtha,
d'Houaphan mais aussi autour des villes de&nbsp;Luang
Prabang et de Vientiane (Ferlus 1971; Bradley 2007; Bout&eacute;
2010). Edmondson (2005) distingue cinq vari&eacute;t&eacute;s
de phou
noy&nbsp;: <span style="font-style: italic;">Black Khoany, Hwethom,
Khaskhong, Mung</span>, et <span style="font-style: italic;">White
Khoany</span>.
Le parler des Phou Noy du Vietnam&nbsp;(district de Mường
T&egrave;,
province de Lai Ch&acirc;u, Nord Vietnam) est
d&eacute;sign&eacute; par le terme <span style="font-style: italic;">cố&ocirc;ng</span>
(Bradley 1977; Phạm 1998).</p>
            <p style="text-align: justify;"><br>
            </p>
            <p style="text-align: justify;">Le
groupe loloish a fait l'objet de recherches intenses jusqu'&agrave;
la fin des
ann&eacute;es
70 (voir en particulier: Matisoff 1969, 1972; Bradley 1978). Les
publications de
Michel Ferlus
au sujet du phou noy apportent&nbsp;un important
&eacute;clairage sur
cette langue dans des champs disciplinaires compl&eacute;mentaires
tels
que la phonologie
(Ferlus 1975), la morphologie et la syntaxe (Ferlus 1975,
1977a, 1979, 1980), la s&eacute;mantique (Ferlus 1976) mais aussi
l'ethnographie
(Ferlus 1977b). Mais ce domaine de recherche semble avoir
&eacute;t&eacute; quelque peu d&eacute;laiss&eacute; au cours des trois
derni&egrave;res d&eacute;cennies. Aujourd'hui,&nbsp;il semblerait que
les
nouveaux travaux de documentation et recherche concernent plus souvent
les langues loloish parl&eacute;es en Chine que celles d'Asie du
Sud-Est : voir par exemple une
&eacute;tude d&eacute;taill&eacute;e d'un changement tonal
en cours dans la langue lalo (Yang 2015).&nbsp;Or les langues loloish
d'Asie du Sud-Est fourniraient
mati&egrave;re &agrave; des &eacute;tudes
linguistiques&nbsp;approfondies, qui enrichiraient la base
empirique des recherches en linguistique sino-tib&eacute;taine et
en linguistique g&eacute;n&eacute;rale. Selon les
informations disponibles, le phou noy
demeure usit&eacute; et vivace; cette langue
repr&eacute;sente un riche champ
d'investigation pour celles et ceux qui sont
int&eacute;ress&eacute;s par les
langues du groupe loloish et par les langues lolo-birmanes. Du point de
vue morpho-phonologique, le phou noy
pr&eacute;sente des ph&eacute;nom&egrave;nes de sandhis qui
ont
&eacute;t&eacute; tr&egrave;s peu
&eacute;tudi&eacute;s. Au plan socio-linguistique, la
situation sp&eacute;cifique du phou noy dans le paysage tant
linguistique que g&eacute;ographique de l'Asie du
Sud-Est le tient actuellement &agrave; l'&eacute;cart d'une influence
directe du chinois (tandis que les langues loloish de Chine connaissent
un remplacement progressif par le mandarin),&nbsp;tout
en
exposant &agrave; d'autres dynamiques, qui font l'objet d'&eacute;tudes
sociologiques et anthropologiques (voir en particulier Bout&eacute;
2010, 2011) et fourniraient mati&egrave;re &agrave; des &eacute;tudes
linguistiques.&nbsp;</p>
