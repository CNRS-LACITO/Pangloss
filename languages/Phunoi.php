            <h2><strong><a name="arcived_doc"></a>2.
Les documents archiv&eacute;s</strong></h2>
            <p style="text-align: justify;"><span>Les
donn&eacute;es phou noy de Michel Ferlus consiste en des
enqu&ecirc;tes
linguistiques et des r&eacute;cits. Des chants et des formules
rituelles ont &eacute;galement &eacute;t&eacute;
enregistr&eacute;s.
La collecte des donn&eacute;es s'est d&eacute;roul&eacute;
au Laos
(Vientiane and Luang Prabang) essentiellement dans les
ann&eacute;es 60
et 70, et en 1972 en France, lorsqu'une personne qui avait
&eacute;t&eacute; consultant linguistique de Michel
Ferlus
au Laos s'est rendue en France pour y effectuer un stage.&nbsp;</span><span
 style="color: rgb(0, 0, 0);">"&Agrave; cette
&eacute;poque</span><span style="color: rgb(0, 0, 0);">-</span><span
 style="color: rgb(0, 0, 0);">l&agrave;, le Laos
&eacute;tait dans une situation politico-militaire instable et il
&eacute;tait devenu impossible d&rsquo;acc&eacute;der aux
zones recul&eacute;es du pays, justement celles qui abritaient le
plus de langues minoritaires dont plusieurs n&rsquo;avaient jamais
&eacute;t&eacute; d&eacute;crites ni m&ecirc;me
recens&eacute;es</span><span>"
(Ferlus 2011).</span></p>
            <p style="text-align: justify;"><span>La
majorit&eacute; des textes enregistr&eacute;s par Ferlus et disponibles
dans la Collection Pangloss ont
&eacute;t&eacute; transcrits, annot&eacute;s et traduits
(en
fran&ccedil;ais). Michel Ferlus a le projet de mettre au propre ces
tapuscrits in&eacute;dits de langue phou noy, qui viendront
compl&eacute;ter les ressources actuellement en ligne. Il est &agrave;
esp&eacute;rer qu'une nouvelle
g&eacute;n&eacute;ration de chercheurs s'int&eacute;resse
&agrave; ces
donn&eacute;es et les utilise pour entreprendre de nouvelles
&eacute;tudes sur le phou noy, contribuant aux progr&egrave;s conjoints
de la documentation linguistique et de la recherche.</span></p>
