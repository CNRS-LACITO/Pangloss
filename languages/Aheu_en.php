            <p>Phần ghi
&acirc;m ở đ&acirc;y bao gồm c&aacute;c dữ liệu về từ vựng.
Ch&uacute;ng được ghi &acirc;m trong sự hợp t&aacute;c với
GS. TS. Trần
Tr&iacute; D&otilde;i (VNU).</p>
            <br>
            <p style="font-weight: bold;">T&agrave;i
liệu tham khảo:&nbsp;</p>
- Những b&agrave;i viết đ&atilde; được c&ocirc;ng bố về
tiếng Cuối Chăm:&nbsp;
            <p>Ferlus, Michel.
1994. &ldquo;<a
