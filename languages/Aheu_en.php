            <h1 align="center">Aheu
(Thavung) data&nbsp;&nbsp;</h1>
            
            <p>&nbsp;</p>
            <h4 align="left">Resources
:</h4>
            <p class="intertitre"> <img
 src="../../images/icones/h_parleur.gif" alt="a" height="20" width="20"><a
 href="../tools/list_rsc_en.php?lg=Thavung&amp;aff=Thavung" target="_blank">
Click
here for a list of Thavung resources</a></p>
            <p class="intertitre"><img
 src="../../images/icones/h_parleur.gif" alt="a" height="20" width="20"><a
 href="../tools/list_rsc_en.php?lg=Aheu&amp;aff=Aheu" target="_blank">
Click
here for a list of
Aheu (Ph&ocirc;n Soung) resources </a></p>
            <p class="intertitre"><br>
            </p>
            <p>This language is
variously known as Aheu, (Kha) Thavung, S&ocirc;, (Kha)
Ph&ocirc;n Soung and Kha Tong Luang.&nbsp;See pp. 14-15 of:
Ferlus, Michel. 1996. <a
 href="http://sealang.net/sala/archives/pdf8/ferlus1996langues.pdf"
 target="_blank">Langues et peuples viet-muong</a>. <span
 style="font-style: italic;">Mon-Khmer
Studies</span> 26. 7&ndash;28.</p>
            <p>Two dialects are
presented here:&nbsp;</p>
            <p>(i) that of a
dialect whose speakers refer to themselves as "Thavung".</p>
            <p>(ii) that of a
dialect whose speakers refer to themselves as "Aheu" or "Ph&ocirc;n
Soung".
"Ph&ocirc;n Soung" means "high hills" and refers to the environment
where they used to live. "Aheu", phonetically /<span
 style="font-weight: bold;">ahəː&sup1;</span>/,
is interpreted as the
interrogative 'What?', which would have been chosen
by&nbsp;neighbouring peoples to refer to them. </p>
            <p>Linguistic
fieldwork was conducted&nbsp;in 1993 in the Aheu community; at the
time
they were living in the forest in a rudimentary settlement. The closest
available landmark to locate this Aheu village was a Lao village
located down in the Nam Theun river valley: Sop Hia village, Lak Sao
district, Bolikhamxay province, Laos. The Aheu village was located
higher up on the hill slopes. This landmark (Ban Sop Hia) has since
disappeared from the maps, as the village's population was resettled in
the first decade of the 21st century: the area was flooded by the
construction of the&nbsp;<a
 href="https://en.wikipedia.org/wiki/Nam_Theun_2_Dam" target="_blank">Nam
Theun 2</a> hydroelectric dam. That
village,&nbsp;"Ban Sop Hia", is
mentioned in the resettlement plan available <a
 href="http://www.poweringprogress.org//download//V1AppendixM,%20Khamkerd%20Resettlement%20Village%20Site%20Planning.pdf"
 target="_blank">here</a>
(document dated 2003, consulted in September
2015), and appears on the map (page 7).</p>
            <p><br>
            </p>
            <p class="intertitre"><strong>The
archived documents: vocabulary</strong></p>
            <p>The recordings made
available here were conducted by Michel Ferlus, in collaboration with
Pr. Trần Tr&iacute; D&otilde;i (Vietnam National University).
They&nbsp;contain lexical data. As Ferlus's field notes have not
been
verified item by item to finalize the word list, it was decided to put
the data online without a transcription, rather than run the risk of
publishing data that contain mistaken
transcriptions. Michel Ferlus rated the technical quality of the
Aheu recordings as "very poor"; this is
due to the use of low-grade tapes (counterfeit tapes sold on the local
market).</p>
            <br>
            <p style="font-weight: bold;">References
and links to
related resources:&nbsp;</p>
Ferlus, Michel. 1996. <a
 href="http://sealang.net/sala/archives/pdf8/ferlus1996langues.pdf"
 target="_blank">Langues et peuples viet-muong</a>. <span
 style="font-style: italic;">Mon-Khmer
Studies</span> 26, pp.
7&ndash;28.<span style="font-style: italic;">&nbsp;</span>
