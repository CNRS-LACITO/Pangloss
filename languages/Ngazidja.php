      <p align="left">Le parler de la Grande Comore, l'&icirc;le la plus au nord de l'archipel des Comores, est un parler bantu au m&ecirc;me titre que le swahili, mais cependant bien diff&eacute;rent. Guthrie classe ces deux langues dans le m&ecirc;me groupe G40, mais il n'y a pas d'intercompr&eacute;hension entre le swahili et les parlers comoriens, pas plus qu'entre le fran&ccedil;ais et l'italien.</p>
      <p>Le ngazidja (grand-comorien) est assez proche du parler de l'&icirc;le de Moh&eacute;li, le mwali, mais pr&eacute;sente des diff&eacute;rences sensibles avec le maore, parler bantu de Mayotte, qui se rapproche davantage du nzuani d'Anjouan.</p>
