            <h2><strong><a name="archived_doc"></a>2.
The
archived documents</strong></h2>
            <p style="text-align: justify;">Michel
Ferlus collected Khmu data from 1963 to the late 70's. In
the course of his field trips, he became a fluent speaker of
this
language. Ferlus's Khmu data represents his most extensive collection:
he
collected enough material to produce a dictionary and a grammar, and to
edit a rich collection of fully transcribed and annoted texts. These
constitute valuable resources to
explore the language as well as the customs and oral tradition of the
Khmu people.&nbsp;Their contents are also of interest to historians,
anthropologists, and sociologists.&nbsp;&nbsp; </p>
            <p>These
recordings mainly comprise narratives and&nbsp;songs. To take only one
example illustrating their richness,&nbsp;the <span
 style="font-style: italic;">Chuang/Cheuang story</span> is of key
importance to several cultures of the area.
(About this epic, see Ferlus, Michel. 1979. "Le r&eacute;cit
khamou
de Chuang et ses implications historiques pour le Nord-Laos", in Asie
du sud-est et Monde Insulindien, vol. 10, no. 2-4.) In his discussion
of the Tai version of this narrative, Chamberlain (1990) explains that
"the epic of Thao Hung or Cheuang is perhaps the most important piece
of
literature from the Southeast Asian mainland, as monumental to the
study of Southeast Asia civilization as the Ramayana is to India or the
Iliad is to the West. [...] For students of history, the work
represents a detailed account of a place and period in Southeast Asian
history that is a virtual vacuum in our knowledge, but a place and
period that was undoubedly the site of great upheavals as the
pre-Buddhist Tais migrated westward from northern Vietnam through
northern Laos, to northern Tailand, Burma, Yunnan, and Assam,
conquering the older Austroasiatic populations as they went. [...]
In essence, Cheuang is the most comprehensive account avaible of
ancient Tai civilization, and as such it is wealth of information [...]
for the entire Tai-speaking world [...]." (Chamberlain,
James. 1990. "Thao Hung or Cheuang: A Tai Epic Poem" in Mon-Khmer
Studies, vol. 18-19, pp. 14-34.)</p>
            <p style="text-align: justify;">The resources made
available here are intended for a broad audience of
linguists interested by the morphological and phonological aspects of
Mon-Khmer languages. At present, Michel Ferlus's typed transcriptions
of texts and lexicographic materials have not yet been digitized;
persons interested to contribute in the task are welcome to get in
touch.&nbsp;</p>
            <p style="text-align: justify;">A technical note:
in cases where the latitude and longitude of the place of recording
could not be determined with precision, the values indicated, by
default, are those of the Luang Prabang area (lat. 20, long. 102; these
cases can be identified automatically in the database, as only an
integer figure is provided). We would be most grateful if visitors of
this site could help us determine the present-day coordinates of these
locations.</p>
            <p style="text-align: justify;">Ferlus worked with several
collaborators, who acted as respondent during the
collection of
tales to help rekindle the storyteller's eloquence and asked questions
during interviews. Not all participants in the recordings have been
identified so far; we hope to be able to&nbsp;add more and more
pieces&nbsp;of this type of information to the metadata as these legacy
data continue to be&nbsp;processed.&nbsp; </p>
