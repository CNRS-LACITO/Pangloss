            <h2><a name="language"></a>1.
About Khmu</h2>
            <p style="text-align: justify;">The Khmu (other spellings:
Khamu, Khammu, Khamou or
Kemu) mainly live in northern Laos: Nam-Tha,
Ph&ocirc;ngsali, Sam-Neua and Luang Phrabang; they are considered
autochthonous to their current living area. There are some groups
located in southwestern China (Yunnan Province), Vietnam (Son-Tay and
Cua-Rao), Burma and Thailand (Chiang-Ray and Nan Province).</p>
            <p style="text-align: justify;">They
speak Khmu, a language which is a member of the Khmuic branch
of the Mon-Khmer family (Austroasiatic). Khmu is closely&nbsp;related
to languages such as&nbsp;Ksingmul (Xinh Mun) in Vietnam and
Laos, and Phong (Kniang) in Laos
(see Ferlus 1977; Sidwell 2014). Khmu is a wonderful introduction
to&nbsp;typical Mon-Khmer features such as infixal morphology (Ferlus
1977a) and sesquisyllabic structure (Matisoff 1973).</p>
            <p style="text-align: justify;">There are several
Khmu dialects which present some
differences in their consonant inventories and in their prosodic
systems (Suwilai 2001, 2004). On the basis of these
differences,
Khmu can be subdivided into two main dialects: <span
 style="font-style: italic;">Western Khmu</span>
has fewer
consonant phonemes and presents a two-way contrast in register
(lax/breathy vs. tense/modal), further evolving towards a tonal
contrast, whereas <span style="font-style: italic;">Eastern Khmu</span>
has neither
register distinction nor tone distinction. Eastern Khmu preserves
voicing oppositions among initial stops (voiced,
unvoiced, and aspirated series) and initial sonorants (voiced,
unvoiced,
and glottalized series) (Suwilai <span style="font-style: italic;">et
al.</span>
2002). These two dialects have been the object of in-depth
investigations (see Svantesson
1983; Suwilai 2004; Lindell <span style="font-style: italic;">et al.</span>
1979, 1981).</p>
            <p style="text-align: justify;">Khmu remains
a&nbsp;vigorous language and there remains considerable room for
research.&nbsp; </p>
