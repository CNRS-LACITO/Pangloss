      <p>Tamang is spoken in the Center of Nepal, by approximately 1 million people. A century ago it was known as Murmi, and was classified by Konow (<em>Linguistic Survey of India</em>) in the &quot;non-pronominalized&quot; group of Himalayan languages. Tamang is closely related to Tibetan, and Schafers classifies it in the &quot;Gurung&quot; branch of the Bodish section of the Bodic division of Sino-Tibetan. This branch is currently being renamed the &quot;Tamang branch&quot; because many of those who speak other languages from the same branch mostly use, to refer to themselves as well as to the Tamang proper, a form which can be derived from an earlier form *tamang. The other main languages of this branch are Gurung, Thakali and the Manang varieties. Although these languages are all closely related, they are not mutually comprehensible.</p>
      <p> Tamang can be further divided into two dialect groups, western and eastern Tamang. Phonologically, the eastern dialects are more conservative, and have retained canonical syllable structure, with word final consonants and word initial consonant clusters, a system that becomes progressively more simplified as one moves from East to West, the final point being Gurung, with canonical CV syllable structure. The dialect presented here is spoken in the town of Risiangku, it is an eastern, phonologically conservative variety. </p>
      <p>The Tamang are mostly farmers. Most are Buddhists, especially in Risiangku, where shamanist methods are commonly practiced, namely in medicine.</p>
