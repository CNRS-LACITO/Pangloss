<h1>Corpus prinmi</h1>
</div>
<div class="text-left">
<p class="intertitre">&nbsp;</p>
<p style="text-align: justify;" class="intertitre">Le
pumi, aussi appel&eacute; prinmi, est une langue
sino-tib&eacute;taine parl&eacute;e dans le sud-ouest de la
Chine.&nbsp;</p>
<p style="text-align: justify;" class="intertitre">Trois
des textes&nbsp;pr&eacute;sent&eacute;s ici ont
&eacute;t&eacute; recueillis, transcrits et annot&eacute;s
par <a href="https://fr.wikipedia.org/wiki/Guillaume_Jacques"
 target="_blank">Guillaume Jacques</a>
dans la pr&eacute;fecture de Muli (Liangshan, province du Sichuan),
et mis en ligne en 2012.
Le dialecte concern&eacute; est celui du comt&eacute; de
Shuiluo (水洛乡) ; il se rattache &agrave; l'ensemble dialectal nord.</p>
<p style="text-align: justify;" class="intertitre">En
2017, Henri&euml;tte Daudey et Gerong Pincu
ont confi&eacute; &agrave; la Collection Pangloss un premier
ensemble de documents recueillis dans la plaine de Yongning,
comt&eacute; de Ninglang (province du
Yunnan). Le dialecte concern&eacute; est celui qui a fait l'objet
d'une monographie par Henri&euml;tte Daudey (2014). Les documents ont
&eacute;t&eacute; cr&eacute;&eacute;s dans l'environnement logiciel
FLEx et convertis au format de la Collection Pangloss au moyen de
scripts Python 3 r&eacute;alis&eacute;s par S&eacute;verine
Guillaume.&nbsp;</p>
