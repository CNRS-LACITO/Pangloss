            <h1 align="left"><a name="PHONGNHA"></a>Phong
Nha: a heterodox dialect of Quảng B&igrave;nh Province</h1>
<p>&nbsp;</p>
            <p>"The Vietnamese
dialect of the hamlet of
Phong Nha (commune of Sơn Trạch,
county of Bố Trạch, Quảng B&igrave;nh) is one of the
&ldquo;heterodox&rdquo; dialects of
Vietnamese, which are known to present considerable interest for the
historical
study of Vietnamese and of the Vietic group at large. These dialects
are the
product of the southerly expansion of Vietnamese over related (Vietic)
languages, a process which involved various interferences. Comparative
evidence
reveals strata of standardization: some words are phonologically
identical to
Standard Vietnamese; others are of Southern Vietic stock, as
demonstrated by
the absence of telltale historical changes that took place in
Vietnamese, such
as the spirantization of medial stops; still others appear to be the
result of
hybridization."</p>
            <div style="text-align: right;"><small>(Michaud,
Alexis, Michel Ferlus &amp; Minh-Ch&acirc;u
Nguyễn. 2015. <a
 href="https://halshs.archives-ouvertes.fr/halshs-01141389"><br>
"Strata
of standardization: the Phong Nha dialect of Vietnamese (Quảng
B&igrave;nh Province) in historical perspective."</a> <span
 style="font-style: italic;"><br>
Linguistics
of the Tibeto-Burman Area</span>
38(1). 124&ndash;162.)</small><br>
            </div>
