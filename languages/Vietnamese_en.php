            <h1 align="left"><a id="background"></a>Background
to this data set: the AuCo collection</h1>
            <p>&nbsp;</p>
            <p class="intertitre">These
Vietnamese
documents are part of the AuCo collection, a project of the MICA
International Research Institute in Hanoi, Vietnam. The aim of this
project is to put together an open-access collection of recordings of
languages of Vietnam and neighbouring countries. AuCo stands for
AudioCorpora; it is also a reference to &Acirc;u Cơ, a fairy who
bore an egg sac that hatched a hundred children: the Hundred Peoples
(B&aacute;ch Việt), ancestor to the Vietnamese and to the multitude
of other ethnic groups of the area. The round dots in the logo of the
AuCo/&Acirc;uCơ collection are an allusion to these hundred eggs -
a symbol of the cultural and linguistic diversity reflected in the
collection.&nbsp;</p>
            <p class="intertitre"><img
 src="../../images/logos/AuCo_logo.jpg"
 alt="Logo of the AuCo project, International Research Institute MICA, Hanoi Univ. of Science and Technology"
 style="width: 230px; height: 98px;" align="middle" height="77"
 width="304"></p>
            <p class="intertitre">The
aim of the AuCo
collection to collect the documents recorded by researchers in the
course of their research activity. The AuCo collection thereby fulfills
an important function: it allows for cumulative progress in speech data
collection. Preparing, recording and annotating audio data sets is
highly time-consuming; with a little extra investment of time and
effort, the data can be prepared in such a way as to be re-usable by
others, for various purposes (including phonetic / phonological
analysis, and automatic speech processing, but also language teaching /
language revitalization). The AuCo collection aims to contribute to the
documentation of a precious human heritage: the languages of the world.
It also aims to facilitate interdisciplinary research involving
engineers and linguists, through the sharing of data, tools and methods.<br>
The AuCo collection is open to documents of various types: from unique
heritage recordings dating back several decades, to everyday recordings
of national languages collected for one-off research purposes. Because
there is no telling when and how documents will be re-used, the AuCo
collection chooses not to exclude any type of data.<br>
The documents AuCo collection were recorded and transcribed / annotated
by researchers from very different backgrounds, including the members
of the "Speech Communication" department of the <a
 href="http://en.wikipedia.org/wiki/MICA_Institute">International
Research Institute MICA</a> (HUST -
CNRS/UMI-2954 - Grenoble INP,
Hanoi University of Science and Technology). The tasks involved in the
preparation of the documents for archiving and online distribution are
realized by members of the "Speech Communication" department of the
MICA Institute. Long-term preservation (perennial archiving) and online
distribution are taken charge of by the <a target="_blank"
 href="http://www.huma-num.fr/">Tr&egrave;s
Grand
Equipement Huma-Num</a>, in
partnership with <a href="http://www.cines.fr/?lang=en">CINES</a>
and <a href="http://cc.in2p3.fr/">IN2P3</a>.
Tasks of data
filing are realized with the help of the two centres that serve as
archive entry points: the <a
 href="http://lacito.vjf.cnrs.fr/pangloss/index_en.htm">Pangloss
Collection</a> / Cocoon data
repository (CNRS-<a href="http://lacito.vjf.cnrs.fr/">LACITO</a>),
and the
Speech and Language Data Repository: <a
 href="http://crdo.up.univ-aix.fr/">SLDR</a>
(CNRS-LPL).</p>
