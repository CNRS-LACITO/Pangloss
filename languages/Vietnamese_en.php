            <h1 align="left"><a name="CAOLAOHA"></a>Cao
Lao Hạ: a heterodox dialect of Quảng B&igrave;nh Province</h1>
			<p>&nbsp;</p>
            <p>The Cao Lao Hạ
dialect was investigated
by Michel Ferlus in December 1993 together with Pr. Trần
Tr&iacute; D&otilde;i. A six-page conference handout about this
dialect was circulated in 1995: </p>
            <p>Ferlus, Michel.
1995. <a href="http://halshs.archives-ouvertes.fr/halshs-00922735">Particularit&eacute;s
du dialecte vietnamien de Cao Lao Hạ (Quảng B&igrave;nh, Vietnam)</a>.
Dixi&egrave;mes Journ&eacute;es de Linguistique
d&rsquo;Asie Orientale. Paris.&nbsp;</p>
            <p>The&nbsp;data
set
on which the 1995 report is based is made available here: click on the
link
below to access the full list.</p>
            <p class="intertitre"><img src="../../images/images_pangloss/haut_parleur_s.png" alt="a"
 height="20" width="20"> <a
 href="../corpus/list_rsc_en.php?lg=Cao Lao Hạ"
 target="_blank"> All available resources for the Cao
Lao Hạ dialect</a></p>
            <p>About Cao Lao Hạ, a
substantial resource in Vietnamese is available: L&ecirc; Văn Sơn, <span
 style="font-style: italic;">Địa ch&iacute;
l&agrave;ng Cao Lao Hạ</span>,
Nh&agrave; xuất bản Thuận H&oacute;a, 2007 (313 pages). (The
full text
of this book, along with other information, was available, as of 2015,
from the official website <a href="http://caolaoha.com/"
 target="_blank">http://caolaoha.com/</a>)
Here
are some excerpts.&nbsp;</p>
            <p><span style="font-weight: bold;">(i) historical
background</span>: the conquest and
peopling of the Cao Lao Hạ&nbsp;area</p>
            <p style="text-align: justify;">"Cao
Lao Hạ, xưa
gọi l&agrave; Kẻ Hạ thuộc ch&acirc;u Bố ch&iacute;nh nay
l&agrave; x&atilde; Hạ Trạch, huyện Bố Trạch, tỉnh Quảng
B&igrave;nh. (...) Đối với l&agrave;ng Cao Lao Hạ, theo truyền
tụng cũng bắt đầu từ những người di cư lập ấp từ thời L&yacute;,
Trần, nầy. Nhưng theo gia phả c&aacute;c họ tộc ở l&acirc;u đời
nhất, c&oacute; văn bản b&uacute;t t&iacute;ch ghi
ch&eacute;p, th&igrave; từ thời Hậu L&ecirc;, m&agrave;
r&otilde; n&eacute;t nhất l&agrave; từ thời L&ecirc;
Th&aacute;nh T&ocirc;ng (1470), xuống chiếu mộ d&acirc;n
lập ấp v&agrave;o ch&acirc;u Bố Ch&iacute;nh: &ldquo;Bố
Ch&iacute;nh đất rộng người thưa, liền với ch&acirc;u Hoan, vậy
qu&acirc;n v&agrave; d&acirc;n đến đ&oacute; khẩn hoang
(l&agrave;m ăn) sẽ c&oacute; lợi lớn&rdquo;
(tr&iacute;ch chiếu Nam Hạ). Đ&acirc;y l&agrave; đợt di
d&acirc;n lớn của triều L&ecirc;. Người bốn trấn
ph&iacute;a bắc hưởng ứng chiếu, di d&acirc;n rất nhiều. Phần
đất v&ugrave;ng ph&iacute;a nam s&ocirc;ng Gianh thuộc sơn
hệ Lệ Đệ được danh dự đ&oacute;n tiếp người Thanh &ndash; Nghệ
đến lập l&agrave;ng x&oacute;m, khai h&oacute;a ba
l&agrave;ng Cao Lao liền, xưa gọi l&agrave; Cao Lao Thượng
(c&ograve;n gọi l&agrave; Kẻ Thạng), l&agrave;ng Cao Lao
Trung (c&ograve;n gọi l&agrave; Kẻ Chu&ocirc;ng )
v&agrave; Cao Lao Hạ (gọi l&agrave; Kẻ Hạ).<br>
Đến thời Nguyễn Ho&agrave;ng v&agrave;o trấn thủ đất Thuận
H&oacute;a (1558) mới chia ch&acirc;u Bố Ch&iacute;nh
th&agrave;nh hia ch&acirc;u l&agrave; Nam Bố
Ch&iacute;nh v&agrave; Bắc Bố Ch&iacute;nh. Ph&iacute;a
nam S&ocirc;ng Giang l&agrave; nam Bố Ch&iacute;nh,
ph&iacute;a bắc s&ocirc;ng Giang l&agrave; Bắc Bố
Ch&iacute;nh.<br>
Cho đến khi ch&uacute;a Trịnh đuổi được ch&uacute;a Nguyễn ra
khỏi Thuận- Quảng, (năm 1776), L&ecirc; Qu&iacute;
Đ&ocirc;n gi&atilde; chức Hiệp Trấn, Tham T&aacute;n
qu&acirc;n sự Thuận H&oacute;a&hellip; đ&atilde;
ch&eacute;p trong s&aacute;ch Phủ Bi&ecirc;n Tạp Lục rằng:
&ldquo;Ch&acirc;u Nam Bố Ch&iacute;nh gồm hai tổng: Tổng
Trứ Lễ v&agrave; Tổng Lương X&aacute;. Tống Trứ Lễ gồm 17
x&atilde;, 7 phường, 6 trang, trong đ&oacute; c&oacute; Cao
Lao m&agrave; kh&ocirc;ng ph&acirc;n biệt Cao Lao
Thượng, Cao Lao Trung, Cao Lao Hạ.<br>
Theo gia phả của d&ograve;ng họ L&ecirc; Quang hiện
c&ograve;n lưu th&igrave; dưới thời Nguyễn (Gia Long), năm
T&acirc;n Dậu (1801) &ocirc;ng L&ecirc; Quang
H&agrave;o người l&agrave;ng Cao Lao Hạ thuộc tổng Thị Lễ được
bỗ nhiệm giữ chức chỉ huy đội II của Vệ Chấn V&otilde; (Vệ gồm
3.600 l&iacute;nh) thuộc đạo Trung Qu&acirc;n."</p>
            <span style="font-weight: bold;">(ii)
language</span>: short notes about
phonetics/phonology, and dialectal lexical items
            <p style="text-align: justify;">"Lời
ăn tiếng
n&oacute;i của người l&agrave;ng Cao Lao Hạ
c&oacute; những từ hơi kh&aacute;c với c&aacute;c nơi nhất
l&agrave; ngữ &acirc;m Cao Lao th&igrave; rất
kh&aacute;c, cho n&ecirc;n khi nghe tiếng n&oacute;i của
người Cao Lao Hạ, người l&agrave;ng kh&aacute;c biết ngay biết
ngay đ&oacute; l&agrave; người Cao Lao Hạ (Hạ Trạch).
Th&ocirc;ng thường, người Hạ Trạch n&oacute;i dấu hỏi
th&agrave;nh dấu nắng <span style="font-style: italic;">(sic.)</span>
v&agrave; hạ thấp &acirc;m tiết. Một
số thổ &acirc;m tuy c&oacute; kh&aacute;c với đ&ocirc;i
v&ugrave;ng, nhưng kh&ocirc;ng nhiều, chỉ ri&ecirc;ng ngữ
&acirc;m l&agrave; đặc biệt Cao Lao. C&oacute; thể
kh&aacute;i qu&aacute;t rằng ngữ &acirc;m Cao Lao Hạ
to&agrave;n l&agrave; hạ thấp. V&iacute; dụ: Cửa sổ
n&oacute;i l&agrave; Cựa sộ (đa số l&agrave; sử dụng
dấu nặng thay dấu hỏi, ng&atilde;)."</p>
            <p>Tiếng
Cao Lao Hạ c&oacute; những đặc biệt địa
phương như: <br>
- Phụ
&acirc;m đầu &ldquo;tr&rdquo; th&agrave;nh
&ldquo;trl&rdquo;. V&iacute; dụ: con
tr&acirc;u = con trl&acirc;u; c&acirc;y tre = c&acirc;y
trle; ăn trộm = ăn trlộm; một trăm = một trlăm
v.v.<br>
- Phụ
&acirc;m: &ldquo;nh&rdquo; th&agrave;nh
&ldquo;d&rdquo; hoặc &ldquo;gi&rdquo;. V&iacute;
dụ:
c&aacute;i nh&agrave;; c&aacute;i d&agrave;; nhờ cậy;
dờ cậy; nhiều &iacute;t: diều &iacute;t; con nhện: con dện
v.v&hellip;<br>
-
Nguy&ecirc;n &acirc;m ă gh&eacute;p với chữ m = ăm, khi
ph&aacute;t
&acirc;m th&agrave;nh am v&iacute; dụ: năm th&igrave;
đọc th&agrave;nh nam, mắm th&igrave; đọc th&agrave;nh
m&aacute;m v.v. </p>
            <p>Sau
đ&acirc;y xin thống k&ecirc; một số từ đặc biệt của
địa phương so với tiếng phổ th&ocirc;ng sắp xếp theo thứ tự: a, b,
c.</p>
