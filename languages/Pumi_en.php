<h1>Pumi Corpus </h1>
</div>
<div class="text-left">
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Pumi, also called Prinmi, is a
Sino-Tibetan language spoken in
Southwest China. Two sets of data are
currently available:</p>
<p style="text-align: justify;">- In 2012, <a
 href="https://en.wikipedia.org/wiki/Guillaume_Jacques" target="_blank">Guillaume
Jacques</a> deposited three texts collected in the prefecture of Muli
(Liangshan, Sichuan province). The dialect concerned is that spoken in
the township of Shuiluo (水洛乡); it belongs to the Northern dialect
group. </p>
<p style="text-align: justify;">- In 2017, Henri&euml;tte Daudey
and Gerong Pincu deposited five texts collected in the&nbsp;Yongning
plain (Ninglang County, Yunnan). The dialect at issue is that described
and analyzed in <a
 href="https://www.sil.org/system/files/reapdata/69/27/67/69276721657901706692127480944602004968/A_Grammar_of_Wadu_Pumi_libre.pdf"
 target="_blank">the monograph by Henri&euml;tte Daudey (2014)</a>. The
documents were created in the FLEx software, and converted to the
format of the Pangloss Collection using scripts (in the Python 3
programming language) written by S&eacute;verine Guillaume.&nbsp; </p>
