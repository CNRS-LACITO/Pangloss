      <p>The Hayu inhabit an area of Nepal between 50 and 100 km. south east of Kathmandu, in the valley of the Sun Kosi river and across the Mahabharat range to the south. They are called 'Hayu' in Nepali and 'Wayu' in their own language (transcribed 'Vayu' by Brian Hodgson in the 19th Century). </p>
      <p>The language belongs to the Tibeto-Burman family. It was classified by Shafer (1955) in the West-Central Himalayish section (close to East Himalayish or 'Kiranti') of the Bodic Division of Sino-Tibetan. It is currently spoken in  Murajor, in Sukhajor VDC, Ramechhap District, and the villages of  Adhamara and Manedihi, overlooking the Marin river valley, in Sindhuli District. There are probably about 300 speakers, all bilingual in Nepali, the Indo-Aryan national language.</p>
      <p>The present materials were recorded and first transcribed in 1972 by B. Michailovsky and Martine Mazaudon in Murajor.</p>
      <p>&nbsp;</p>
      <p class="intertitre"><strong>Annotation</strong></p>
      <p>The transcription is IPA-based, except that 'y' is used for the palatal glide (IPA [j]). The transcription of loanwords from Nepali partly reflects Nepali orthography; this transcription is currently being revised to more accurately reflect the recorded pronunciation.      </p>
