      <h1 align="center">An Appeal with folded hands (1956) </h1>
      <p>&nbsp;</p>
      <p style="color:#F00"><b>To see the text in the Sirijanga alphabet, please install the font <a href="http://scripts.sil.org/cms/scripts/page.php?site_id=nrsi&id=namdhinggosil">namdhinggosil</a>.
This font is known to work with Firefox and IE9, and not with Chrome or Safari.
</b></p>
      <p class="intertitre">&nbsp;</p>
  
       <h4 align="left">Introduction by Bairagi Kainla </h4>
     
      <p class="intertitre">&nbsp;</p>
<p>"The composer of this song is the singer himself, Subba Khadga Bahadur Nembang. Dr Richard K. Sprigg stayed for a few days in the home of Subba Khadga Bahadur (Double Subba) in Sartap village in January 1956 (VS 2012), in the course of his research on the Limbu script.  He recorded the song, together with songs sung by other distinguished guests, at a gathering on the porch of the house of Subba Khadga Bahadur's brother, Subba Nar Bahadur, in Pauwa village, Panchthar (then Dhankuta) district. Also present were Mrs. Sprigg, Ran Dhoj Nembang of Yambong village, Phedangma Raj Man Nembang of Yambong Hangyok, and other persons who took an interest in Limbu language and oral traditions, including Brijhang Jabegu of Ukase Lubaphu village." —Bairagi Kainla </p>
      <p>&nbsp;</p>
      <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-LIF_APPEAL_SOUND&idref=crdo-LIF_APPEAL"><img src="../../images/icones/Txt_Inter_parchemin1.jpg" alt="a" width="20" height="20" border="0"> An appeal with folded hands</a><br></p>
      <p>&nbsp;</p>
      
      <h4 align="left">Notes:</h4>
      <p>The "Appeal" is an early example of an original literary composition written in the Sirijanga (Limbu) script, which had been revived (and revised) in the 1920s. In it, the author urges his compatriots to study Limbu tradition (mundhum) and script, and to avoid alcohol. Bairagi Kainla recalls village notables singing the song as they walked to the polls to vote in Nepal's first democratic election in 1959.</p>
      <p>&nbsp;</p>
      <p>The recording presented here is from a digitalization, made at the LACITO in 2005, of Dr Sprigg's original tape recordings (by permission). The transcription in Limbu script, the romanization, and the translation into Nepali are by Bairagi Kainla. The English translation is by Boyd Michailovsky of the Lacito/CNRS.</p>
      <p>&nbsp;</p>
      <h4 align="left">Documents:</h4>
      <p><a href="Limbu_TwoSubbas_1956.jpg">Photograph</a> of Subba Khadga Bahadur Nembang and his brother Subba Nar Bahadur, Pauwa village, Panchthar, 1956 (R. K. Sprigg, by permission).</p>
      <p>&nbsp;</p>
      <p><a href="Limbu_APPEAL_MSS_v2.pdf">Autograph manuscript</a> transcription (in Limbu script) and English translation of the "Appeal with folded hands", by Iman Singh Chemjong (archives of Dr Sprigg). The text and spelling are slightly different from those of the recording and transcription archived here.
</p>
<p>&nbsp;</p>
<h4 align="left">Biographical data:</h4>
<p>Dr Richard Keith Sprigg (1922-2011), phonetician, phonologist and general linguist, was a member of the team from the School of Oriental and Aftrican Studies, University of London, which conducted research in Nepal during 1955-1956. Accompanied by his wife Ray, he set out on foot from Darjeeling district in West Bengal in January 1956 and reached Kathmandu some months later. On the way he investigated several languages of the hills of East Nepal, including Limbu, Bantawa, and Tamang.</p>
<p>&nbsp;</p>
<p>Bairagi Kainla (Til Bikram Nembang, b. 1939), son of Subba Khadga Bahadur Nembang, is a well-known Nepali poet, as well as a Limbu lexicographer and the editor and translator of several books of Limbu traditional texts (mundhum). He served as Chancellor of the Nepal Academy from 2010 to 2014.</p>

      
