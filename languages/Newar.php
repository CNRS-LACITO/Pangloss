      <p class="intertitre">Dans le recensesment népalais de 2011, quelques 846,000 personnes ont reclamé le néwar comme "mother tongue".</p>

<p>Le néwar est la langue de la civilisation qui a regné sur la vallée de Kathmandu jusqu'en 1768. Elle est parlé dans la vallée de Kathmandu, dans quelques villes et villages plus à l'est (Banepa, Dhulikhel, Dolakha), et par des communautés de commerçants ailleurs au Népal. Il est écrit, dans l'alphabet dévanagari et certains de ses variants, depuis mille ans environ.</p>

<p>Le néwar est une langue de la famille tibéto-birmane, fortement influencée par le sanskrite et d'autres langues indo-aryennes. La variété représentée dans le texte "Hari Das" est celle de Bhaktapur (néw. Khopa), l'une des grandes villes de la vallée de Kathmandu.</p>

