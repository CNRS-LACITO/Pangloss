            <h1 align="center"><a name="LangLo_en"></a>2.
L&agrave;ng
Lỡ dialect
of&nbsp;Thổ&nbsp;</h1>
            <p class="intertitre">
            <a href="#LangLo"><span style="font-style: italic;">Giới
thiệu bằng tiếng Việt</span></a></p>
            <p>&nbsp;</p>
            <h4 align="left">Resources
:</h4>
            <p class="intertitre">
            <img src="../../images/icones/h_parleur.gif" alt="a"
 height="20" width="20"><a
 href="../tools/list_rsc_en.php?lg=Th%E1%BB%95&amp;aff=Th%E1%BB%95"
 target="_blank"> All
available resources here </a></p>
            <p class="intertitre">&nbsp;</p>
            <p style="text-align: justify;">&ldquo;Henri
Maspero (1912) used this dialect in his comparative work. (...) My
attention had&nbsp;been drawn to this dialect by the fact that the
word for &lsquo;cooked rice&rsquo;, Vietnamese cơm /<span
 style="font-weight: bold;">kɤm</span><sup>A1</sup>/,
was gươm /<span style="font-weight: bold;">ɣɯəm</span><sup>A1</sup>/
in L&agrave;ng Lỡ: a form that seemed aberrant and inexplicable.
Serious fieldwork about the Thổ dialect of L&agrave;ng Lỡ was in
order, so as to solve this intriguing problem. (...) Vietnamese is the
language in which Vietic spirants [such as <span
 style="font-weight: bold;">ɣ</span>]
originate: the presence of spirants in Central-Northern dialects of
Vietnamese, and in the Thổ dialect of L&agrave;ng Lỡ, appears to be
due to a phenomenon of propagation of spirants by <span
 style="font-style: italic;">irregular imitation</span>,
not a propagation of the phonological change itself. Here is the
detailed scenario: Vietnamese, after it developed a series of spirants,
expanded southwards, and came into contact with a Thổ dialect. The
relationship of prestige is clearly asymmetrical: Vietnamese is
culturally and politically dominant; the local language is dominated.
Despite their divergence since the Proto-Vietic
stage, the two
languages have preserved enough shared vocabulary to allow
intercomprehension and a degree of awareness of phonetic
correspondences. Speakers of the Thổ dialect unconsciously perceive
Vietnamese spirants &ndash; sounds that are absent from their
language &ndash; as prestige elements that are characteristic of
the dominating language. By a sort of affectation, speakers introduce
these spirants into words of their own dialect, either by
&ldquo;expellor loans&rdquo; (a label coined by this author)
whereby the Vietnamese word replaces the corresponding local word, or
by borrowing only the spirantized segment from Vietnamese, which
replaces the homologous segment in the cognate word in Thổ.&rdquo; </p>
            <p>(excerpted
from:&nbsp;Michel Ferlus. 2001.&nbsp;<a
 href="https://halshs.archives-ouvertes.fr/halshs-00922722/"
 target="_blank">Les hypercorrections dans le thổ de
L&agrave;ng Lỡ (Nghệ An, Vietnam), ou les pi&egrave;ges du
comparatisme</a>. <i>Quinzi&egrave;mes
Journ&eacute;es de Linguistique d'Asie Orientale</i>,
Jun 2001, Paris, France; translated as: &ldquo;<a target="_blank"
 href="https://halshs.archives-ouvertes.fr/halshs-01128106">Hypercorrections
in the Thổ dialect of L&agrave;ng Lỡ (Nghệ An, Vietnam): an example
of pitfalls for comparative linguistics</a>&rdquo;.)</p>
