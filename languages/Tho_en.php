            <h1 align="center">Pangloss
Collection - Thổ Corpus&nbsp;</h1>
            <p>Two language
varieties are presented here: Cuối Chăm,
and <a href="#LangLo_en">L&agrave;ng
Lỡ Thổ</a>.</p>
            <p>&nbsp;</p>
            <h1 align="center">1.
Cuối
Chăm
Corpus </h1>
            <p class="intertitre">&nbsp;</p>
            <p class="intertitre">
            <a href="#Vietnamese"><span style="font-style: italic;">Giới
thiệu bằng tiếng Việt</span></a></p>
            <p>&nbsp;</p>
            <h4 align="left">Resources
:</h4>
            <p class="intertitre">
            <img src="../../images/icones/h_parleur.gif" alt="a"
 height="20" width="20"><a
 href="../tools/list_rsc_en.php?lg=Cu%E1%BB%91i+Ch%C4%83m&amp;aff=Cu%E1%BB%91i+Ch%C4%83m"
 target="_blank"> All
available resources here </a></p>
            <p class="intertitre">&nbsp;</p>
            <p>&ldquo;The Cuoi
Cham
language (in Vietnamese: Cuối Chăm) is a member of the Tho (Thổ)
subgroup of the Vietic group within the Austroasiatic family. (...) The
Tho dialects are spoken in some
areas of the northern part of Nghe An province; Henri Maspero referred
to them as &ldquo;Southern Muong&rdquo;. They have a high
degree of lexical similarity with Vietnamese, partly inherited, and
partly acquired through Vietnamese influence. However, the Tho dialects
are phonetically more conservative than Vietnamese.&rdquo; </p>
            <p>(excerpted
from:&nbsp;Ferlus, Michel. &ldquo;<a
 href="https://halshs.archives-ouvertes.fr/halshs-00927642"
 target="_blank">Quelques particularit&eacute;s du
cu&ocirc;i cham, une langue viet-muong du Ngh&ecirc;-an
(Vietnam)</a>&rdquo;,
Neuvi&egrave;mes Journ&eacute;es de Linguistique de l'Asie
Orientale, May 1994, Paris, France; translated as: &ldquo;<a
 href="https://halshs.archives-ouvertes.fr/halshs-01079521">Some
peculiarities of Cuoi Cham, a Vietic language of Nghe-An (Vietnam)</a>&rdquo;.)</p>
            <p></p>
            <div
 style="line-height: 1.35; padding-left: 2em; text-indent: -2em;"
 class="csl-bib-body"> <span class="Z3988"
 title="url_ver=Z39.88-2004&amp;ctx_ver=Z39.88-2004&amp;rfr_id=info%3Asid%2Fzotero.org%3A2&amp;rft_val_fmt=info%3Aofi%2Ffmt%3Akev%3Amtx%3Ajournal&amp;rft.genre=article&amp;rft.atitle=Arem%2C%20a%20Vietic%20language&amp;rft.jtitle=Mon-Khmer%20Studies&amp;rft.volume=43&amp;rft.aufirst=Michel&amp;rft.aulast=Ferlus&amp;rft.au=Michel%20Ferlus&amp;rft.date=2014&amp;rft.pages=1-15&amp;rft.spage=1&amp;rft.epage=15"></span></div>
            <strong>The
archived documents: vocabulary</strong>
            <p>The recordings made
available here contain lexical data. They were recorded in January 1994
from two male speakers, aged 68 and 22. For more information on the
speakers, location..., see the documents' Metadata. Each word is said
by
the first consultant, then by the second. (To display the speaker code
for each token on the recording, tick the "Notes" box on the page
displaying the transcription and annotation.)&nbsp;</p>
            <p> </p>
            <p class="intertitre"><strong>Highlights
for research</strong></p>
            <p class="intertitre"><strong><span
 style="font-weight: normal;">Here are selected
highlights of the&nbsp;</span></strong>Cuối
Chăm data<strong><span style="font-weight: normal;">.</span></strong></p>
            <ul>
              <li>Diachronic
research: Registrogenesis</li>
            </ul>
            <p>Cuối Chăm underwent
the same consonant shift as Chinese, Vietnamese, Muong, and many other
East/Southeast Asian languages: a merger of voiced and unvoiced onsets.
Among former unvoiced initials, unvoiced stops remain unvoiced (<span
 style="font-weight: bold;">p</span>,
            <span style="font-weight: bold;">t</span>,
...) whereas unvoiced nasals and sonorants become voiced <span
 style="font-weight: bold;">(ʰm</span>&gt;<span
 style="font-weight: bold;">m</span>,
            <span style="font-weight: bold;">ʰl</span>&gt;<span
 style="font-weight: bold;">l</span>,
...), developing high-series characteristics: (i) clear voice (modal
phonation type), (ii) high tones, and (iii) a tendency towards vowel
opening. Among former voiced initials, stops become devoiced (<span
 style="font-weight: bold;">b</span>&gt;<span style="font-weight: bold;">p</span>,
            <span style="font-weight: bold;">d</span>&gt;<span
 style="font-weight: bold;">t</span>,
...) and nasals and sonorants remain voiced (<span
 style="font-weight: bold;">m</span>,
            <span style="font-weight: bold;">l</span>,
...), developing low-series characteristics: (i) breathy/whispery
phonation type, (ii) low tones, and (iii) a tendency to vowel closing.
Muong developed purely tonal systems, i.e. characteristics (i) and
(iii) are not preserved in the current system of these languages. On
the other hand, the Tho language varieties, including Cuoi, present
full-fledged eight-tone systems but also retain differences in degree
of vowel opening: opening of *<span style="font-weight: bold;">o</span>ː
and *<span style="font-weight: bold;">ɔ</span>ː
in the high series, and closing of *<span style="font-weight: bold;">a</span>ː
(and *<span style="font-weight: bold;">ɛ</span>ː)
in the low series.</p>
            <ul>
              <li>Synchronic
phonetic research<br>
              </li>
            </ul>
            <p>&ldquo;For me
languages that are in danger of
disappearing are of extreme importance; without a good sense of <span
 style="font-weight: bold;">how languages vary</span>,
not only in
terms of the symbolic units such as phonemes and allophones but also <span
 style="font-weight: bold;">in the details of their
phonetic
implementation</span>, we have
little hope of understanding the
possible range of language. Endangered languages in particular
represent important but often-ignored source of information about what
is possible in Language.&rdquo; (Richard Wright, Director of the <a
 href="http://depts.washington.edu/phonlab/people/wright.htm"
 target="_blank">University of Washington Phonetics Lab</a>)
            </p>
            <p>Cuối Chăm has a
number of&nbsp;characteristics of potential interest to
phoneticians, not all of which are areally widespread. It has
a&nbsp;system of phonetically complex tones. When approaching such
systems, the first step consists in identifying the contrastive tones,
and providing them with abstract labels. That part of the work was
accomplished by Michel Ferlus, who numbered the tones from 1 to 8 and
identified their diachronic origin
(they correspond to etymological A1, A2, B1... through D2).
The next step will consist in studying their phonetic properties, which
include the time course of fundamental frequency, and also
phonation-type specifications: e.g. tones 5 and 6 (etymological C1 and
C2) have final glottal constriction. Data from the two speakers (one
part of the older generation, the other 46 years younger) reveals
salient differences in phonetic realizations, while the system remains
the same. The acoustic data available here would offer a sufficient
basis for a simple phonetic study of tones in words of one, two or
three syllables, and their interaction with other phonemic dimensions
such as vowel length (which is contrastive in Cuối Chăm).</p>
            <p>Among vowels and
consonants: (i) The phonetic realization of&nbsp;consonants shows
variation between stop and fricative allophones, e.g.&nbsp;[<span
 style="font-weight: bold;">kʰ</span>]~[<span style="font-weight: bold;">x</span>]
for /<span style="font-weight: bold;">kʰ</span>/,&nbsp;[<span
 style="font-weight: bold;">pʰ</span>]~[<span
 style="font-size: 11pt; line-height: 107%; font-family: &quot;Doulos SIL&quot;; font-weight: bold;"
 lang="VI">ɸ</span>]
for /<span style="font-weight: bold;">pʰ</span>/.
(ii) Final stops <span style="font-weight: bold;">-p -t -c -k</span>
are often released by the second speaker when in word-final position
(which, given the nature of the task, is also utterance-final
position). In Vietnamese,&nbsp;the typical realization is
unreleased, but a release is occasionally found; there appear to be
speaker-specific preferences. (iii) The fricative /s/
is&nbsp;realized with varying degrees of (noncontrastive)
aspiration. (iv) Initial voiced stops <span style="font-weight: bold;">b</span>,
            <span style="font-weight: bold;">d</span>
tend to be realized as preglottalized&nbsp;<span
 style="font-weight: bold;">ɓ</span>,
            <span style="font-weight: bold;">ɗ</span>,
i.e. adopting the pronunciation of the closest equivalent sounds in
Vietnamese, the national language,&nbsp;which is exerting
increasingly strong influence on Cuối Chăm. The same phenomenon is
observed among various languages of Vietnam that have voiced stops,
e.g. Hmongic languages of L&agrave;o Cai (A. Michaud, field notes,
2013). (v) The trill /<span style="font-weight: bold;">r</span>/
is sometimes realized close to [<span style="font-weight: bold;">ʒ</span>]
or to a mildly retroflex [<span style="font-weight: bold;">ʐ</span>].</p>
            <p></p>
            <strong>Status of the
notations: field notes&nbsp;</strong>
            <p>The words were
transcribed in the field by Michel Ferlus. In 2014-2015, Phạm
Thị Thu H&agrave; typed Michel Ferlus's manuscript notes. Following
the author's recommendation,&nbsp;minor corrections were made to
align all&nbsp;International Phonetic Alphabet transcriptions with
the description of
the&nbsp;phonemic system published as <a
 href="https://halshs.archives-ouvertes.fr/halshs-01079521"
 target="_blank">Ferlus (1994)</a>.&nbsp;It
should be kept in mind that these are field notes elaborated in the
course of a short&nbsp;trip. To take the example of the tone
system, while the categories can be considered to be established with
full confidence, it may be that the tone of a few lexical items was
mis-labelled.</p>
            <p>The important
difference between the linguist's initial
notes and the final stage when data have been thoroughly confirmed is
emphasized by <a href="http://hdl.handle.net/10125/24619"
 target="_blank">Morey
2014</a>:638: &ldquo;linguists
should make it a practise to explicitly indicate whether the proposed
linguistic categories have been recognized by native speaker
consultants and&nbsp;whether words cited have been confirmed in
those categories by native speakers&rdquo;. We hope
that&nbsp;the online availability of the present data set will
encourage new fieldwork, ideally classical immersion fieldwork as
described by Dixon (2007).</p>
            <p></p>
            <p><strong>Annotation
conventions&nbsp;</strong> </p>
Short vowels are
written without a diacritic, and long vowels with an added diacritic,
e.g. distinguishing <span style="font-weight: bold;">aː</span>
(long vowel) and <span style="font-weight: bold;">a</span>
(short vowel).
(The latter notation is thus equivalent to&nbsp;<span
 style="font-weight: bold;">ă</span>.)
            <p><strong></strong></p>
