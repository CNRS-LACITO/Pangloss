            <h1 align="center"><a name="Vietnamese"></a>Ngữ
liệu tiếng Cuối Chăm</h1>
            <p>Tiếng
Cuối Chăm l&agrave; một trong những phương ngữ Thổ, thuộc tiểu
chi Việt-Chứt (ti&ecirc;̉u chi Việt-Chứt
thuộc
ngữ hệ Nam &Aacute;, ngo&agrave;i Việt v&agrave; Mường
c&ograve;n gồm
c&aacute;c
ng&ocirc;n ngữ như Chứt, Thổ, M&agrave;y, Rục,
S&aacute;ch, M&atilde; Liềng, Arem...).
&ldquo;C&aacute;c phương ngữ
Thổ được sử
dụng ở một số khu vực ph&iacute;a Bắc tỉnh Nghệ An. Nh&agrave;
nghi&ecirc;n cứu Henri Maspero đ&atilde; gọi c&aacute;c
ng&ocirc;n ngữ n&agrave;y với c&aacute;i t&ecirc;n
&ldquo;Nam Mường&rdquo; (Southern Muong). C&aacute;c
ng&ocirc;n ngữ n&agrave;y c&oacute; một sự tương ứng
kh&aacute; cao với tiếng Việt về mặt từ vựng (một phần
l&agrave; bởi c&oacute; chung một nguồn gốc, một phần
l&agrave; do sự ảnh hưởng &ndash; vay mượn từ tiếng Việt). Tuy
vậy, so với tiếng Việt, c&aacute;c&nbsp;phương ngữ Thổ vẫn lưu
giữ được nhiều đặc điểm ngữ &acirc;m cổ xưa hơn.&rdquo;</p>
            <p>(Tr&iacute;ch
từ: Ferlus, Michel. &ldquo;<a
 href="https://halshs.archives-ouvertes.fr/halshs-00927642"
 target="_blank">Một số đặc trưng của tiếng Cuối Chăm
&ndash; một ng&ocirc;n ngữ Vietic ở Nghệ An (Việt
Nam)&rdquo; [Quelques particularit&eacute;s du
cu&ocirc;i cham, une langue viet-muong du Ngh&ecirc;-an
(Vietnam)]</a>&rdquo;,
Neuvi&egrave;mes Journ&eacute;es de Linguistique de l'Asie
Orientale, May 1994, Paris, France; bản tiếng Anh: &ldquo;<a
 href="https://halshs.archives-ouvertes.fr/halshs-01079521">Some
peculiarities of Cuoi Cham, a Vietic language of Nghe-An (Vietnam)</a>&rdquo;.)</p>
            <p>Nh&agrave;
nghi&ecirc;n cứu Nguyễn Hữu Ho&agrave;nh (2009:10) nhận định:
&ldquo;Trong c&aacute;c c&ocirc;ng tr&igrave;nh
nghi&ecirc;n cứu về lịch sử c&aacute;c ng&ocirc;n ngữ Việt
&ndash; Mường, tư liệu tiếng Cuối đ&atilde; được sử dụng
kh&aacute; nhiều v&agrave; tr&ecirc;n thực tế, tiếng Cuối
l&agrave; một nguồn ngữ liệu hết sức quan trọng đối với
c&ocirc;ng việc đ&oacute;&rdquo;.</p>
            <div
 style="line-height: 1.35; padding-left: 2em; text-indent: -2em;"
 class="csl-bib-body"> <span class="Z3988"
 title="url_ver=Z39.88-2004&amp;ctx_ver=Z39.88-2004&amp;rfr_id=info%3Asid%2Fzotero.org%3A2&amp;rft_val_fmt=info%3Aofi%2Ffmt%3Akev%3Amtx%3Ajournal&amp;rft.genre=article&amp;rft.atitle=Arem%2C%20a%20Vietic%20language&amp;rft.jtitle=Mon-Khmer%20Studies&amp;rft.volume=43&amp;rft.aufirst=Michel&amp;rft.aulast=Ferlus&amp;rft.au=Michel%20Ferlus&amp;rft.date=2014&amp;rft.pages=1-15&amp;rft.spage=1&amp;rft.epage=15"></span></div>
            <strong>C&aacute;c
t&agrave;i liệu được lưu trữ: ngữ li&ecirc;̣u v&ecirc;̀ từ
vựng</strong>
            <p>Phần ghi
&acirc;m ở đ&acirc;y bao gồm c&aacute;c dữ liệu về từ vựng.
Ch&uacute;ng được ghi &acirc;m v&agrave;o th&aacute;ng
1 năm 1994, với người ph&aacute;t &acirc;m l&agrave; hai
cộng t&aacute;c vi&ecirc;n nam giới, một người 68 tuổi, người
c&ograve;n lại 22 tuổi. Mỗi một từ được hai cộng t&aacute;c
vi&ecirc;n lần lượt ph&aacute;t &acirc;m.</p>
            <p class="intertitre"><strong>Những
điểm nổi bật đ&aacute;ng để đi s&acirc;u nghi&ecirc;n cứu</strong></p>
            <p class="intertitre">Dưới
đ&acirc;y l&agrave; những điểm nổi bật về ngữ liệu tiếng Cuối
Chăm:</p>
            <ul>
              <li>Đối với
nghi&ecirc;n cứu lịch đại: Qu&aacute; tr&igrave;nh
h&igrave;nh th&agrave;nh &acirc;m vực (Registrogenesis)</li>
            </ul>
            <p>Ở tiếng Cuối Chăm
cũng diễn ra qu&aacute; tr&igrave;nh biến đổi phụ &acirc;m
tương tự như ở tiếng H&aacute;n, tiếng Việt, tiếng Mường
v&agrave; nhiều ng&ocirc;n ngữ kh&aacute;c ở Đ&ocirc;ng
/ Đ&ocirc;ng Nam &Aacute;, đ&oacute; l&agrave; sự
h&ograve;a trộn giữa c&aacute;c phụ &acirc;m đầu hữu thanh
v&agrave; v&ocirc; thanh. Đối với c&aacute;c phụ
&acirc;m đầu vốn l&agrave; c&aacute;c &acirc;m
v&ocirc; thanh: c&aacute;c &acirc;m tắc v&ocirc; thanh
giữ nguy&ecirc;n t&iacute;nh chất v&ocirc; thanh (<span
 style="font-weight: bold;">p</span>,
            <span style="font-weight: bold;">t</span>,
...); trong khi đ&oacute;, c&aacute;c &acirc;m mũi
v&ocirc; thanh v&agrave; &acirc;m vang biến đổi
th&agrave;nh &acirc;m hữu thanh (<span style="font-weight: bold;">ʰm</span>&gt;<span
 style="font-weight: bold;">m</span>,
            <span style="font-weight: bold;">ʰl</span>&gt;<span
 style="font-weight: bold;">l</span>,
...), ph&aacute;t triển th&agrave;nh loạt &acirc;m cao
(&acirc;m vực cao) với những t&iacute;nh chất như: (i)
ph&aacute;t &acirc;m s&aacute;ng (chất giọng
s&aacute;ng, clear voice), (ii) thanh điệu ở &acirc;m vực cao,
v&agrave; (iii) nguy&ecirc;n &acirc;m c&oacute; xu
hướng mở hơn. Đối với c&aacute;c phụ &acirc;m đầu vốn
l&agrave; c&aacute;c &acirc;m hữu thanh: c&aacute;c
&acirc;m tắc bị v&ocirc; thanh h&oacute;a (<span
 style="font-weight: bold;">b</span>&gt;<span style="font-weight: bold;">p</span>,
            <span style="font-weight: bold;">d</span>&gt;<span
 style="font-weight: bold;">t</span>,
...), c&aacute;c &acirc;m mũi v&agrave; &acirc;m vang
giữ nguy&ecirc;n t&iacute;nh chất hữu thanh (<span
 style="font-weight: bold;">m</span>,
            <span style="font-weight: bold;">l</span>,
...), ph&aacute;t triển th&agrave;nh loạt &acirc;m thấp
(&acirc;m vực thấp) với những t&iacute;nh chất như: (i)
ph&aacute;t &acirc;m với chất giọng h&agrave; hơi (chất
giọng thở, breathy voice), (ii) thanh điệu ở &acirc;m vực thấp,
v&agrave; (iii) nguy&ecirc;n &acirc;m c&oacute; xu
hướng hẹp hơn.</p>
            <p>C&aacute;c
ng&ocirc;n ngữ như tiếng Việt, tiếng Mường đ&atilde;
ph&aacute;t triển hệ thống thanh điệu ho&agrave;n chỉnh;
c&aacute;c đặc trưng theo kiểu đối lập về chất giọng (i)
v&agrave; phẩm chất của nguy&ecirc;n &acirc;m (iii)
kh&ocirc;ng c&ograve;n được duy tr&igrave; ở c&aacute;c
ng&ocirc;n ngữ n&agrave;y nữa. Tuy nhi&ecirc;n,
c&aacute;c phương ngữ nh&oacute;m Thổ (bao gồm Cuối Chăm)
ph&aacute;t triển hệ
thống t&aacute;m thanh điệu ho&agrave;n
chỉnh nhưng lại vẫn duy tr&igrave; được những kh&aacute;c biệt
về độ mở của nguy&ecirc;n &acirc;m. V&iacute; dụ: hai
nguy&ecirc;n &acirc;m *<span style="font-weight: bold;">o</span>ː
v&agrave; *<span style="font-weight: bold;">ɔ</span>ː
được ph&aacute;t &acirc;m với đ&ocirc;̣ mở lớn hơn (tương
đương
với <span style="font-weight: bold;">ɔ</span>ː
v&agrave; <span style="font-weight: bold;">ɒ</span>ː)
trong c&aacute;c &acirc;m ti&ecirc;́t thu&ocirc;̣c loạt
cao; v&agrave;
được ph&aacute;t &acirc;m với đ&ocirc;̣ mở nhỏ hơn (tương
đương
với <span style="font-weight: bold;">o</span>ː
v&agrave; <span style="font-weight: bold;">ɔ</span>ː)
trong c&aacute;c &acirc;m ti&ecirc;́t thu&ocirc;̣c loạt
th&acirc;́p.</p>
            <ul>
              <li>Đối với
nghi&ecirc;n cứu ngữ &acirc;m đồng đại:<br>
              </li>
            </ul>
            <p>&ldquo;Với
t&ocirc;i, c&aacute;c ng&ocirc;n ngữ đang c&oacute;
nguy cơ biến mất quan trọng v&ocirc; c&ugrave;ng.
N&ecirc;́u kh&ocirc;ng &yacute; thức v&ecirc;̀ <span
 style="font-weight: bold;">c&aacute;ch thức
c&aacute;c
ng&ocirc;n ngữ bi&ecirc;́n đ&ocirc;̉i</span>
(kh&ocirc;ng chỉ ở v&acirc;́n đ&ecirc;̀ c&aacute;c đơn
vị k&yacute;
hi&ecirc;̣u như &acirc;m vị v&agrave; bi&ecirc;́n
th&ecirc;̉
&acirc;m vị m&agrave; c&ograve;n ở <span style="font-weight: bold;">những
chi
ti&ecirc;́t
cụ th&ecirc;̉ v&ecirc;̀ những b&ocirc;̉ sung ngữ
&acirc;m học</span>)
ch&uacute;ng ta sẽ c&oacute; rất &iacute;t hi vọng để hiểu
về c&aacute;c khả năng trong ng&ocirc;n ngữ. C&aacute;c
ng&ocirc;n ngữ c&oacute; nguy cơ biến mất c&oacute; tầm
quan trọng đặc biệt, l&agrave; nguồn cung cấp th&ocirc;ng tin
về những khả năng trong ng&ocirc;n ngữ, nhưng nguồn dữ liệu
n&agrave;y lại thường bị bỏ qua.&rdquo; (Richard Wright,
trưởng ph&ograve;ng thực nghi&ecirc;̣m ngữ &acirc;m học,
đại
học Washington / <a
 href="http://depts.washington.edu/phonlab/people/wright.htm"
 target="_blank">University of Washington Phonetics Lab</a>)
            </p>
            <p>Tiếng Cuối Chăm
c&oacute; một loạt những đặc trưng c&oacute; khả năng thu
h&uacute;t sự quan t&acirc;m của c&aacute;c nh&agrave;
ngữ &acirc;m học. Tiếng Cuối Chăm c&oacute; một hệ thống thanh
điệu với c&aacute;c đặc điểm ngữ &acirc;m phức tạp. Khi tiếp
cận một hệ thống như vậy, bước đầu ti&ecirc;n l&agrave;
x&aacute;c định c&aacute;c thanh đối lập v&agrave;
d&ugrave;ng c&aacute;c k&yacute; hiệu (trừu tượng) để gọi
t&ecirc;n ch&uacute;ng. Michel Ferlus đ&atilde;
ho&agrave;n tất phần c&ocirc;ng việc n&agrave;y.
&Ocirc;ng đ&atilde; d&ugrave;ng c&aacute;c con số từ 1
đến 8 để đ&aacute;nh dấu c&aacute;c thanh v&agrave;
x&aacute;c định nguồn gốc lịch đại của ch&uacute;ng
(ch&uacute;ng tương ứng với c&aacute;c thanh A1, A2, B1... D1,
D2 theo từ nguy&ecirc;n). Bước tiếp theo cốt l&agrave;
t&igrave;m hiểu c&aacute;c t&iacute;nh chất ngữ
&acirc;m của c&aacute;c thanh, bao gồm thời lượng
qu&atilde;ng F0 (the time course of fundamental frequency), đặc
trưng về kiểu tạo &acirc;m (phonation type). V&iacute; dụ:
thanh 5 v&agrave; 6 (tức C1 v&agrave; C2 theo từ
nguy&ecirc;n) c&oacute; t&iacute;nh chất thắt thanh hầu. Dữ
liệu thu được từ 2 cộng t&aacute;c vi&ecirc;n (một người thuộc
lớp gi&agrave;, người c&ograve;n lại thuộc lớp trẻ,
c&aacute;ch nhau 46 tuổi) cho thấy sự kh&aacute;c biệt
r&otilde; rệt trong c&aacute;ch ph&aacute;t &acirc;m,
nhưng lại được ch&uacute; &acirc;m giống nhau trong bảng từ. Dữ
liệu ghi &acirc;m ở đ&acirc;y sẽ cung cấp cơ sở đầy đủ cho một
nghi&ecirc;n cứu ngữ &acirc;m đơn giản về thanh điệu của từ
c&oacute; một, hai hoặc ba &acirc;m tiết v&agrave; những
tương t&aacute;c của n&oacute; với những th&ocirc;ng số
&acirc;m vị học kh&aacute;c như độ d&agrave;i của
nguy&ecirc;n &acirc;m (tiếng Cuối Chăm c&oacute; sự đối lập
về độ d&agrave;i của nguy&ecirc;n &acirc;m).</p>
            <p>V&ecirc;̀
nguy&ecirc;n &acirc;m v&agrave; phụ &acirc;m: (i)
C&aacute;ch ph&aacute;t &acirc;m c&aacute;c phụ
&acirc;m cho thấy sự tồn tại của c&aacute;c biến thể của
c&aacute;c &acirc;m tắc v&agrave; &acirc;m
x&aacute;t, v&iacute; dụ:&nbsp;[<span style="font-weight: bold;">kʰ</span>]~[<span
 style="font-weight: bold;">x</span>]
cho /<span style="font-weight: bold;">kʰ</span>/,&nbsp;[<span
 style="font-weight: bold;">pʰ</span>]~[<span
 style="font-size: 11pt; line-height: 107%; font-family: &quot;Doulos SIL&quot;; font-weight: bold;"
 lang="VI">ɸ</span>]
cho /<span style="font-weight: bold;">pʰ</span>/.
(ii) Phụ &acirc;m tắc cuối <span style="font-weight: bold;">-p -t -c -k</span>
thường bị bu&ocirc;ng (released) trong c&aacute;ch
ph&aacute;t &acirc;m của cộng t&aacute;c vi&ecirc;n
trẻ. Trong tiếng Việt, c&aacute;ch ph&aacute;t &acirc;m
điển h&igrave;nh l&agrave; kh&ocirc;ng bu&ocirc;ng
(unreleased); tuy nhi&ecirc;n thỉnh thoảng cũng c&oacute; thể
bắt gặp c&aacute;ch ph&aacute;t &acirc;m bu&ocirc;ng.
Đ&acirc;y cũng c&oacute; thể l&agrave; c&aacute;ch
ph&aacute;t &acirc;m c&aacute; biệt của cộng t&aacute;c
vi&ecirc;n. (iii) &Acirc;m x&aacute;t /s/&nbsp;được
ph&aacute;t &acirc;m với c&aacute;c biến thể
kh&aacute;c nhau (nhưng kh&ocirc;ng đối lập) về mức độ bật hơi.
(iv) Phụ &acirc;m đầu tắc hữu thanh <span style="font-weight: bold;">b</span>,
            <span style="font-weight: bold;">d</span>
được ph&aacute;t &acirc;m c&oacute; xu hướng tiền thanh hầu
h&oacute;a&nbsp;<span style="font-weight: bold;">ɓ</span>,
            <span style="font-weight: bold;">ɗ</span>.
Đ&oacute; l&agrave; sự &aacute;p dụng (bắt chước)
c&aacute;ch ph&aacute;t &acirc;m của những phụ &acirc;m
tương ứng trong tiếng Việt &ndash; ng&ocirc;n ngữ quốc gia, vốn
c&oacute; ảnh hưởng mạnh mẽ đến tiếng Cuối Chăm. Hiện tượng tương
tự được t&igrave;m thấy trong c&aacute;c ng&ocirc;n ngữ
kh&aacute;c ở Việt Nam m&agrave; c&oacute; c&aacute;c
phụ &acirc;m đầu n&agrave;y, như c&aacute;c ng&ocirc;n
ngữ Hmongic ở L&agrave;o Cai (theo ghi ch&eacute;p điền
d&atilde; của Alexis Michaud năm 2013). (v) &Acirc;m rung /<span
 style="font-weight: bold;">r</span>/
c&oacute; l&uacute;c được ph&aacute;t &acirc;m gần
giống [<span style="font-weight: bold;">ʒ</span>]
hoặc gần với &acirc;m quặt lưỡi nhẹ [<span style="font-weight: bold;">ʐ</span>].</p>
            <strong>Thực trạng của
tư liệu: C&aacute;c ghi ch&eacute;p điền d&atilde;&nbsp;</strong>
            <p>Bảng từ được Michel
Ferlus ch&uacute; &acirc;m trong l&uacute;c l&agrave;m
c&ocirc;ng t&aacute;c điền d&atilde; năm 1994. Trong năm
2014-2015, Phạm Thị Thu H&agrave; đ&atilde; g&otilde; lại
bảng từ n&agrave;y, chuyển từ bản ch&eacute;p tay sang bản điện
tử. Theo
gợi &yacute; của t&aacute;c giả,&nbsp;ch&uacute;ng
t&ocirc;i đ&atilde; c&oacute;
m&ocirc;̣t s&ocirc;́ chỉnh sửa nhỏ trong vi&ecirc;̣c sử
dụng c&aacute;c k&yacute; hi&ecirc;̣u phi&ecirc;n
&acirc;m
qu&ocirc;́c t&ecirc;́&nbsp;(International Phonetic
Alphabet) đ&ecirc;̉ khớp với h&ecirc;̣ th&ocirc;́ng
&acirc;m vị m&agrave;&nbsp;<a
 href="https://halshs.archives-ouvertes.fr/halshs-01079521"
 target="_blank">Ferlus (1994)</a>&nbsp;đ&atilde;
c&ocirc;ng b&ocirc;́. Lưu &yacute;, đ&acirc;y
l&agrave; những ghi
ch&eacute;p được thực hi&ecirc;̣n trong m&ocirc;̣t
chuy&ecirc;́n đi ngắn, v&igrave; v&acirc;̣y kh&ocirc;ng
tr&aacute;nh
khỏi vi&ecirc;̣c c&oacute; m&ocirc;̣t v&agrave;i sai
s&oacute;t. Lấy
v&iacute; dụ về hệ thống thanh điệu: c&aacute;c thanh trong hệ
thống n&agrave;y được x&acirc;y dựng một c&aacute;ch
đ&aacute;ng tin cậy, tuy nhi&ecirc;n cũng c&oacute; thể
c&oacute; một số từ ngữ bị ch&uacute; th&iacute;ch nhầm
v&ecirc;̀ d&acirc;́u thanh.</p>
            <p><a href="http://hdl.handle.net/10125/24619"
 target="_blank">Morey
(2014:638)</a> đ&atilde; nhấn
mạnh như sau về sự kh&aacute;c biệt quan trọng giữa những ghi
ch&eacute;p ban đầu của nh&agrave; ng&ocirc;n ngữ học
v&agrave; tư liệu ở giai đoạn cuối c&ugrave;ng sau khi
đ&atilde; được kiểm tra kỹ lưỡng: &ldquo;Nh&agrave;
ng&ocirc;n ngữ học n&ecirc;n coi đ&oacute; như một
qu&aacute; tr&igrave;nh thực h&agrave;nh để chỉ ra một
c&aacute;ch r&otilde; r&agrave;ng xem liệu những phạm
tr&ugrave; ng&ocirc;n ngữ học được giả định c&oacute; được
người bản địa nhận thấy hay kh&ocirc;ng; liệu những từ ngữ được
tr&iacute;ch dẫn c&oacute; được người bản địa x&aacute;c
định l&agrave; thuộc c&aacute;c phạm tr&ugrave;
đ&oacute; hay kh&ocirc;ng&rdquo;. Ch&uacute;ng
t&ocirc;i hi vọng bộ tư liệu hiện tại được đăng tải trực tuyến sẽ
g&oacute;p phần hỗ trợ cho những nghi&ecirc;n cứu điền
d&atilde; mới cũng như những nghi&ecirc;n cứu chuy&ecirc;n
s&acirc;u về tiếng Cuối Chăm n&oacute;i ri&ecirc;ng
v&agrave; c&aacute;c ng&ocirc;n ngữ Việt-Chứt
n&oacute;i
chung.</p>
Quy ước phi&ecirc;n &acirc;m: C&aacute;c nguy&ecirc;n
&acirc;m ngắn được phi&ecirc;n &acirc;m kh&ocirc;ng
