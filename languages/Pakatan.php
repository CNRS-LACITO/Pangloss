<h1>Corpus&nbsp;pakatan</h1>
</div>
<div class="text-left">
<p class="intertitre">&nbsp;</p>
Au sein du groupe
vietique, le
sous-groupe maleng comporte trois zones dialectales
<p class="intertitre" align="left">1)
Maleng, (Kha)
Pakatan, Malang,
Har&egrave;me (Rivi&egrave;re 1902).&nbsp;<span
 style="font-style: italic;">Autres noms:&nbsp;</span>Kha
Muong Ben et Kha Bo (Fraisse 1950).</p>
<p class="intertitre" align="left">2)
M&atilde;liềng. <span style="font-style: italic;">Autre nom:</span>
Pa Leng (Đặng Nghi&ecirc;m Vạn et al. 1986).</p>
<p class="intertitre" align="left">3)
Kha Phong, Maleng
Kari, Maleng
Br&ocirc;. <span style="font-style: italic;">Autre nom: </span>Kha
Nam Om (Fraisse 1949)</p>
<p class="intertitre" align="left">"L'ethnonyme
<span style="font-style: italic;">Maleng</span>
[<span style="font-weight: bold;">măləɛ̀ŋ&sup2;</span>]
signifie "&Ecirc;tre humain" et sert d'autonyme &agrave; la
quasi totalit&eacute; des communaut&eacute;s de ce sous-groupe.
Ceux des hautes vall&eacute;es de la Nam Theun (Khammouan, Laos) se
disent plus volontiers <span style="font-style: italic;">Kha</span>
avec leurs voisins. Les habitants du village de Pakatan [<span
 style="font-weight: bold;">paːk</span><span style="font-weight: bold;">⁷</span><span
 style="font-weight: bold;"> kat</span><span style="font-weight: bold;">aːn</span><span
 style="font-weight: bold;">&sup1;</span>],
"l'embouchure de la rivi&egrave;re Katan",
pr&eacute;f&egrave;rent &ecirc;tre appel&eacute;s <span
 style="font-style: italic;">(Kha) Pakatan</span>
car ils consid&egrave;rent que les <span style="font-style: italic;">Maleng</span>
qui vivent dans les zones montagneuses sont moins
&eacute;volu&eacute;s qu'eux. Il semble paradoxal d'observer
qu'un autonyme des plus honorables, signifiant &agrave; l'origine
"&Ecirc;tre humain", soit ressenti comme
d&eacute;pr&eacute;ciatif, par opposition socio-culturelle, et
se voie remplac&eacute; par <span style="font-style: italic;">Kha</span>
qui, dans toutes les autres r&eacute;gions du Laos et plus
particuli&egrave;rement dans le Nord, a une connotation
extr&ecirc;mement p&eacute;jorative &agrave; un point tel
qu'il est aujourd'hui proscrit de l'usage. L'autonyme <span
 style="font-style: italic;">Malang</span>
n'est attest&eacute; que dans un seul village; peut-&ecirc;tre
s'est-il substitu&eacute; &agrave; <span style="font-style: italic;">Maleng</span>
pour la m&ecirc;me raison que <span style="font-style: italic;">Pakatan</span>.
Ces populations ont &eacute;t&eacute; mentionn&eacute;es
pour la premi&egrave;re fois par le capitaine M. Rivi&egrave;re
(1902) sous le nom de <span style="font-style: italic;">Har&egrave;me</span>
[voir la page <a href="Arem.htm"><span style="font-style: italic;">Arem</span></a>]
qu'il ne localise pas avec pr&eacute;cision mais le vocabulaire
qu'il en donne, malgr&eacute; une notation approximative, permet de
les identifier aux <span style="font-style: italic;">Maleng</span>
en g&eacute;n&eacute;ral, tout comme les <span
 style="font-style: italic;">Kha Muong Ben</span>
et les <span style="font-style: italic;">Kha
Bo</span> que Andr&eacute;
Fraisse (1950) situait dans la vall&eacute;e de la Nam Theun,
autour du village de Pakatan. On peut se demander, &agrave;
l'instar de cet auteur, si les <span style="font-style: italic;">Thai
Bo</span>
dispers&eacute;s entre la Nam Theun et le M&eacute;kong ne sont
pas d'anciens <span style="font-style: italic;">Maleng</span>
qui se seraient laocis&eacute;s.&nbsp;</p>
<p class="intertitre" align="left">Les
<span style="font-style: italic;">M&atilde;liềng</span>
(forme vietnamis&eacute;e de la prononciation de <span
 style="font-style: italic;">Maleng</span>)
constituent une avanc&eacute;e du sous-groupe en territoire
vietnamien o&ugrave; ils forment une demi-douzaine de villages dans
le massif du Ho&agrave;nh Sơn au nord du Quảng B&igrave;nh. On
pense que, dans les si&egrave;cles pass&eacute;s, ils ont
d&ucirc; &ecirc;tre beaucoup plus
repr&eacute;sent&eacute;s et qu'ils devaient former le fonds de
la population dans cette r&eacute;gion. Leur langue est fortement
marqu&eacute;e par un dialecte vietnamien &agrave; quatre tons
qui n'est plus parl&eacute; dans leur voisinage.</p>
<p class="intertitre" align="left">Les
<span style="font-style: italic;">Kha
Phong</span>
(ancien exonyme devenu autonyme) ne subsistent plus que par deux ou
trois villages dans la haute r&eacute;gion du
c&ocirc;t&eacute; lao de la fronti&egrave;re et par un
village dans le sud-est du H&agrave; Tĩnh au Vietnam. La culture et
la langue des <span style="font-style: italic;">Kha Phong</span>
attestent de fortes influences lao. On pense qu'ils devaient habiter au
sud de la cha&icirc;ne du Phou Ac et qu'ils ont fui vers les
montagnes pour &eacute;chapper aux d&eacute;placements
forc&eacute;s de population vers la Tha&iuml;lande &agrave;
la fin du XIXe si&egrave;cle. Un petit groupe r&eacute;siduel,
les <span style="font-style: italic;">Maleng
Br&ocirc;</span>, se maintient
sur les pentes du Phou Ac, ceux-l&agrave; m&ecirc;mes que A.
Fraisse (1949) nommait les <span style="font-style: italic;">Kha Nam Om</span>
du nom d&eacute;form&eacute; de la Nam On, un affluent de la
Nam Theun. Les <span style="font-style: italic;">Kha Phong</span>
du Vietnam se disent aussi <span style="font-style: italic;">Maleng
kari</span>
[<span style="font-weight: bold;">măləɛŋ&sup2;
kăriː&sup1;</span>]
dans lequel <span style="font-style: italic;">kari</span>
peut
&ecirc;tre rapproch&eacute; de <span style="font-style: italic;">Chali</span>
et de <span style="font-style: italic;">Tri</span>,
noms donn&eacute;s
par ailleurs &agrave; certains groupes <span
 style="font-style: italic;">S&ocirc;-Bru</span>.&nbsp;</p>
<p class="intertitre" align="left">Dans
l'ensemble, ces
populations pratiquent une agriculture de montagne. Les <span
 style="font-style: italic;">M&atilde;liềng</span>
du Vietnam sont
estim&eacute;s &agrave; quelque 200 locuteurs. Nous ne
connaissons pas
les chiffres concernant le&nbsp;Laos mais on peut penser que le
total
des locuteurs de tout le sous-groupe <span style="font-style: italic;">Maleng</span>
ne doit gu&egrave;re d&eacute;passer le millier."&nbsp;</p>
