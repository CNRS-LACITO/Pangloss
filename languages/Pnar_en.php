        <p>Récit des hauts faits du héros de la communauté pnar qui s'est révolté contre l'occupation britannique (1835-1862). Il est mort pendu par l'armée britannique. A cette époque les pnars étaient dirigés par un roi pnar hindouisé : Ram Singh. Le récit prend place sous les  doubles hospices de la déesse hindoue Kali et des enfants de la rivière Kopili, c'est-à-dire de la population pnar de la religion traditionnelle (où l'ancêtre commune du territoire s'incarne dans sa principale rivière, la Kopili.</p>
<p>
Ce récit est à comparer à Kompani Maharani 'la compagnie des Indes' en war de Nongbareh qui donne la vision war idéalisée de la colonisation britannique.
