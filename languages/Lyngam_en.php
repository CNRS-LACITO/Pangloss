            <br/>
     
     <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-LYN_ADA029_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Daughters of iaw long sun and fire how they seperate and come back</a></p>
     <p class="intertitre">
        Meghalaya, Inde, 2013, Stephen Nongsiang, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>Il s'agit du premier des 4 fichiers de mythes de fondation sur l'origine du monde, connus par Rosaia Nianglang de Nongporiu, et redits ici par Stephen Nongsiang.</p>
            <p>Les filles de Iawlong, soleil et feu, comment elles se sont séparées après la mort de leur mère et que le feu l'ai dévorée et comment elles sont revenues au service des créatures de la terre.</p>
           <p> Comment Singe est parti voler les graines de plantes comestibles pour les humains dans le ciel où elles étaient gardées par Niangsi et Marsi. Il les avait introduites dans son pénis.</p>
          <p>  Comment la terre s'est crée avec le vent-parole,  l'océan primordial et à la champignon l'ancêtre des créatures.</p>
            <p>Iawlong fille de la champignon donne naissance au tonnerre et au tremblement de terre.            </p>
