      <p>This narrative in the Pije language, was told in August 1978 in Tiendanite by Mr. Timoth&eacute;e Daahma le we, who was eighty-four at the time. It is an important text as it gives the origins of traditional Kanak money and describes how it was distributed. The money was defined by Maurice Leenhardt as a &ldquo;seal&rdquo; marking the agreement and alliance between partners in ceremonial exchanges. A symbol of life, it accompanies important events in society (births, weddings, deaths), but can also seal the compensation of a wrong and the reestablishment of harmony.</p>
