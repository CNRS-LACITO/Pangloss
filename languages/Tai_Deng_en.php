<h1>Tai Deng (Tay Deng, Tay
D&egrave;ng, Tai Daeng, Th&aacute;i Thanh, Tay Thanh,
Man Thanh)</h1>
</div>
<div class="text-left"> <a href="#Vietnamese"><span
 style="font-style: italic;">Giới
thiệu bằng tiếng Việt / Presentation in Vietnamese</span></a><br>
<br>
The Pangloss Collection hosts data from six
Tai-Kadai languages recorded by Michel
Ferlus, mostly from 1997 to 2003.&nbsp;Note that these unique data
are <span style="font-weight: bold;">working
documents</span>
collected as part
of research on Tai manuscripts and languages: they were not originally
intended for applications such as acoustic-phonetic analysis; the
recordings are of uneven technical quality. <br>
<br>
<div style="text-align: justify;">"The Tay Daeng [dɛːŋᴬ&sup1;] (also
spelt <span style="font-style: italic;">D&egrave;ng</span>) are
called&nbsp;Tay Thanh or
Man Thanh by the Viet. Western authors often call them 'Red Tai' (or
'Red Tay'), interpreting the ethnonym in light of the adjective&nbsp;<span
 style="font-style: italic;">daeng</span> &lsquo;red&rsquo;; this name
is still frequently used in the literature, even though the civil
servant R. Robert (1941), who produced an excellent ethnographic
description, pointed out that their ethnonym must come from <span
 style="font-style: italic;">Muong Daeng</span> (presently Y&ecirc;n
Khương, in the upper reaches of the&nbsp;S&ocirc;ng
M&atilde;), in the province of Thanh H&oacute;a, where they originate,
and which remains their region <span style="font-style: italic;">par
excellence</span>. I believe that Robert's interpretation is valid, and
that the name 'Red Tai' should be proscribed, in all languages
[English: <span style="font-style: italic;">Red Tai</span>, French: <span
 style="font-style: italic;">Tai Rouges</span>, Vietnamese: <span
 style="font-style: italic;">Th&aacute;i đỏ</span>, and so on]. The
name&nbsp;<span style="font-style: italic;">thanh (Th&aacute;i Thanh)</span>,
specifically used in Nghệ An, is a Vietnamicized form of&nbsp;<span
 style="font-style: italic;">theng</span> [tʰɛːŋᴬ&sup1;], from the name
of the old <span style="font-style: italic;">Muong Theng</span> of
historical narratives, located in the plain of Điện Bi&ecirc;n and
considered as the cradle of the main part of the Thai populations of
Northern Laos and (to an even greater degree) of Northern Vietnam. In
past centuries, a strong expansion of the Tay
Daeng led them into the province of Sam Neua (Laos), where they
currently constitute the main strand in the population, and into
Western districts of Nghệ An. They are sometimes named Tay Meuy
[məːjC&sup1;] (also spelt Moey) in the language of their Tay Yo
neighbours, where the term means&nbsp;&lsquo;group, people&rsquo;, and
it is also under this ethnonym that they are known in&nbsp;Laos."
(Ferlus 2008:
299)&nbsp;<br>
</div>
<br>
<strong>The
archived documents: lexical surveys and&nbsp;read manuscripts
(c. 1997-2004)</strong>
<p>Concerning the
speakers, date of recording and other pieces of information, see the
metadata accompanying each document.&nbsp; </p>
<span style="font-weight: bold;">Vocabulary</span>
was elicited by means of the EFEO-CNRS-SOAS
Word List for Linguistic Fieldwork in
Southeast Asia, available <a
 href="https://halshs.archives-ouvertes.fr/halshs-01068533/"
 target="_blank">here</a>.
<br>
<br>
Some of the Tai <span style="font-weight: bold;">read
texts</span> are
from&nbsp;European libraries, others were investigated locally,
making photographs of the originals, and collecting oral renderings by
Tai consultants. Specifically, Tay D&egrave;ng manuscripts are from
the <a href="http://anom.archivesnationales.culture.gouv.fr/"
 target="_blank">Archives
d'Outre-Mer</a>
in Aix-en-Provence and from fieldwork. <br>
