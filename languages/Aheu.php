            <h1 align="center">Corpus
aheu (thavung)</h1>
            <p class="intertitre">&nbsp;</p>
            <h4 align="left">Ressources
:</h4>
            <p class="intertitre"> <img
 src="../../images/icones/h_parleur.gif" alt="a" height="20" width="20"><a
 href="../tools/list_rsc_en.php?lg=Thavung&amp;aff=Thavung" target="_blank">
Acc&egrave;s
&agrave; la liste des ressources thavung</a></p>
            <p class="intertitre"><img
 src="../../images/icones/h_parleur.gif" alt="a" height="20" width="20"><a
 href="../tools/list_rsc_en.php?lg=Aheu&amp;aff=Aheu" target="_blank">
Acc&egrave;s
&agrave; la liste des ressources aheu (ph&ocirc;n soung) </a></p>
            <p class="intertitre" align="left">Cette langue est connue
sous les noms de
Aheu,
(Kha) Thavung, S&ocirc;, (Kha)
Ph&ocirc;n Soung, ainsi que Kha Tong Luang.&nbsp;</p>
            <p style="text-align: justify;" class="intertitre">"Aucun
ethnonyme ne s'impose v&eacute;ritablement aux populations de ce
sous-groupe, le seul qu'ils reconnaissant volontiers &eacute;tant <span
 style="font-style: italic;">Kha</span>,
&agrave; l'instar des <span style="font-style: italic;">Maleng</span>,
et c'est ainsi qu'ils sont
d&eacute;sign&eacute;s par les populations voisines. Les <span
 style="font-style: italic;">(Kha) Thavung</span>
[<span style="font-weight: bold;">tʰăvɨːŋ&sup1;</span>]
ont &eacute;t&eacute;
d&eacute;couverts en 1965 gr&acirc;ce &agrave; quelques
familles
originaires de la r&eacute;gion de Lak Sao et
r&eacute;fugi&eacute;es
pr&egrave;s de Thakhek. La langue, imm&eacute;diatement
utilis&eacute;e
dans le comparatisme, a fait l'objet d'un lexique succinct (Ferlus
1979). Cependant, une liste de mots <span style="font-style: italic;">Kha
Tong Luong</span>, imparfaitement
not&eacute;s mais identifiable au
thavung, avait d&eacute;j&agrave; &eacute;t&eacute;
publi&eacute;e par
A.G. Haudricourt (1966) d'apr&egrave;s un document
ramen&eacute; par A.
Fraisse et contenant des vocabulaires recueillis par le Chao Muong de
Khamkeut. Les <span style="font-style: italic;">Ph&ocirc;n Soung</span>,
d&eacute;couverts en m&ecirc;me temps que les <span
 style="font-style: italic;">Thavung</span>
et venus de leur
r&eacute;gion d'origine dans les m&ecirc;mes circonstances
parlent un
dialecte tr&egrave;s proche du thavung. Si le terme <span
 style="font-style: italic;">Thavung</span>
n'a pas de sens apparent et
ne semble s'appliquer qu'&agrave; un seul village, <span
 style="font-style: italic;">Ph&ocirc;n Soung</span>,
en revanche,
signifie litt&eacute;ralement "les hauts monticules", expression
d&eacute;crivant une r&eacute;gion de montagnes peu
accentu&eacute;es
du bassin de la Nam Theun o&ugrave; &eacute;voluait cette
population.
Quant &agrave; <span style="font-style: italic;">Aheu</span>
/<span style="font-weight: bold;">ahəː&sup1;</span>/,
que nous avons retenu
comme nom du sous-groupe, il ne semble &ecirc;tre rien d'autre que
l'interrogatif "quoi" que leurs voisins auraient
s&eacute;lectionn&eacute; pour le d&eacute;signer. Ce
terme, pas plus
que les autres ethnonymes, n'est accept&eacute; par tous les
locuteurs
qui se reconnaissent mieux sous le nom de&nbsp;<span
 style="font-style: italic;">Kha</span>.
La population des
locuteurs&nbsp;<span style="font-style: italic;">Thavung</span>
forme
quelques villages non loin de Lak Sao dans le district de Khamkeut
(autrefois dans la province de Khammouan mais aujourd'hui dans celle de
Pak Sane). Les&nbsp;<span style="font-style: italic;">Ph&ocirc;n Soung</span>
sont localis&eacute;s &agrave; une centaine de
kilom&egrave;tres plus
au sud. A la fin du XIXe si&egrave;cle une grande partie de la
population du Khammouan, toutes ethnies confondues, a
&eacute;t&eacute;
d&eacute;port&eacute;e par les forces siamoises pour aller
peupler le
Nord-Est de la Thailande. C'est &agrave; la suite de ces
&eacute;v&eacute;nements que la langue des&nbsp;<span
 style="font-style: italic;">Thavung</span>
est aujourd'hui
parl&eacute;e dans trois villages du district de Song Daw, province
de
Sakon Nakhon. Confondus pendant longtemps avec les&nbsp;<span
 style="font-style: italic;">S&ocirc;</span>
proprement dits, de langue
katouique, dont ils avaient adopt&eacute; le nom, ils n'ont
&eacute;t&eacute; linguistiquement identifi&eacute;s que
tr&egrave;s
r&eacute;cemment alors qu'ils vivent dans une r&eacute;gion
parfaitement accessible. En dehors d'infimes diff&eacute;rences
phon&eacute;tiques et des emprunts de vocabulaire la langue de
ces&nbsp;
est la m&ecirc;me que le thavung parl&eacute; au Laos. Selon
les dires
des anciens, une partie des gens d&eacute;plac&eacute;s
seraient
revenus se r&eacute;installer dans leur r&eacute;gion d'origine
et il
n'est pas impossible que les&nbsp;<span style="font-style: italic;">Thavung</span>
actuels soient le r&eacute;sultat de ce retour au pays.</p>
            <p style="text-align: justify;" class="intertitre">Les <span
 style="font-style: italic;">Thavung</span>
pratiquent la riziculture
inond&eacute;e, l'horticulture et l'&eacute;levage. Leurs
villages sont
aussi prosp&egrave;res que ceux des Lao et des Tay. Les <span
 style="font-style: italic;">Ph&ocirc;n Soung</span>
qui sont
actuellement des agriculteurs sur br&ucirc;lis, avouent qu'ils
&eacute;taient nagu&egrave;re des semi-nomades forestiers ce
que
confirme &agrave; l'&eacute;vidence le type de construction
sommaire de
leurs maisons. Les <span style="font-style: italic;">S&ocirc; (Thavung)</span>
seraient environ 750, les <span style="font-style: italic;">Thavung</span>
et les <span style="font-style: italic;">Ph&ocirc;n
Soung</span> entre
200 et 300, cela nous permet d'&eacute;valuer le total des
locuteurs <span style="font-style: italic;">Aheu</span>
&agrave; un millier environ."</p>
            <p style="text-align: right;" class="intertitre">(Pages
14-15 de:
Ferlus, Michel. 1996. <a
 href="http://sealang.net/sala/archives/pdf8/ferlus1996langues.pdf"
 target="_blank">Langues et peuples viet-muong</a>. <span
 style="font-style: italic;">Mon-Khmer
Studies</span> 26. 7&ndash;28.)</p>
            <p class="intertitre" align="left"></p>
            <p style="text-align: justify;" class="intertitre">"Le lieu
de l'enqu&ecirc;te de 1993 &eacute;tait le village des aheu
(aussi
nomm&eacute;s ph&ocirc;n soung),&nbsp;accessible par une
route &agrave;
peine praticable. Les maison &eacute;taient rudimentaires, basses
et
recouvertes de toits de feuilles, et non de paillote. Dans cette
r&eacute;gion on pouvait rouler une heure sans voir &acirc;me
qui vive.
C'&eacute;tait une fabuleuse zone de chasse. La route qui part de
Lak
Sao et
va vers le sud a &eacute;t&eacute; perc&eacute;e par
l'administration
coloniale dans l'entre deux guerres.
Il est dit que des coolies ont &eacute;t&eacute;
attaqu&eacute;s par
des tigres" (Michel Ferlus, communication personnelle 2015).</p>
            <div style="text-align: justify;">Le
village de Sop Hia,
dans la vall&eacute;e de la rivi&egrave;re Nam Theun,
constituait le
lieu-dit le plus proche du village aheu, et servait donc de
rep&egrave;re approximatif pour localiser le village aheu,
situ&eacute;
&agrave; quelque distance, en contrehaut. Ce village a
&eacute;t&eacute; d&eacute;plac&eacute; au cours de la
premi&egrave;re
d&eacute;cennie du XXIe si&egrave;cle pour la construction du
barrage
hydro-&eacute;lectrique de <a
 href="https://en.wikipedia.org/wiki/Nam_Theun_2_Dam" target="_blank">Nam
Theun 2</a>. La position exacte
qu'occupait le village de Sop Hia (Ban
Sop Hia) est indiqu&eacute;e sur la carte en page 7 du projet
disponible <a
 href="http://www.poweringprogress.org//download//V1AppendixM,%20Khamkerd%20Resettlement%20Village%20Site%20Planning.pdf"
 target="_blank">ici</a>
(document de 2003,
consult&eacute; en septembre 2015).</div>
