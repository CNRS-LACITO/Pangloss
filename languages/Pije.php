      <p>Ce r&eacute;cit en langue pije a &eacute;t&eacute; recueilli en ao&ucirc;t 1978 &agrave; Tiendanite aupr&egrave;s de M. Timoth&eacute;e Daahma le we, alors &acirc;g&eacute; de pr&egrave;s de quatre-vingts ans. Il s'agit d'un texte important puisqu'il relate l'origine et la distribution de la monnaie traditionnelle en pays kanak. Cette monnaie a &eacute;t&eacute; d&eacute;finie par Maurice Leenhardt comme un &quot;sceau&quot; marquant l'accord et l'alliance entre partenaires dans les &eacute;changes c&eacute;r&eacute;moniels. Symbole de vie, elle accompagne les &eacute;v&eacute;nements importants de la vie sociale (naissance, mariage, deuil), mais peut aussi sceller la r&eacute;paration d'un dommage subi et permettre le r&eacute;tablissement d'un &eacute;quilibre rompu.</p>
