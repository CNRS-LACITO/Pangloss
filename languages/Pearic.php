            <p></p>
            <p style="text-align: justify;">La phonologie des
langues p&eacute;ariques
pr&eacute;sente une opposition sp&eacute;cifique et peu
commun&nbsp;de&nbsp;registres de voix dans leurs syst&egrave;mes
vocaliques. Ces
syst&egrave;mes combinent le trait /clair/ (voix modale) avec les
traits
marqu&eacute;s /souffl&eacute;/ et /craqu&eacute;/ pour
former quatre
combinaisons syllabiques : clair, souffl&eacute;,
craqu&eacute;,
souffl&eacute;-craqu&eacute;. Les langues p&eacute;ariques
jouent un
r&ocirc;le de premier ordre pour la reconstruction du
proto-m&ocirc;n-khmer. De forts indices sugg&egrave;rent que
les
langues p&eacute;ariques seraient plus li&eacute;es aux langues
m&ocirc;n-khm&egrave;res du
Nord que de celles du Cambodge et des zones alentour. Des
hypoth&egrave;ses au sujet de contacts linguistiques li&eacute;s
&agrave; d'anciennes routes commerciales &agrave;
travers
l'Asie du Sud-Est sont expos&eacute;es par&nbsp;Ferlus (1977,
2011).</p>
