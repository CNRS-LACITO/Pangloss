            <h2><a name="languages"></a>1.
A propos des langues p&eacute;ariques</h2>
            <p style="text-align: justify;">Les P&eacute;ar
sont un peuple compos&eacute; de
plusieurs groupes ethniques
(les P&eacute;ar ou Por, les Samr&eacute;, les Samray, les Suoy
et
les Chong)
localis&eacute;s dans la partie ouest du Cambodge et la partie
sud-est
de la Tha&iuml;lande. Les langues p&eacute;ariques forment un
groupe de
la branche m&ocirc;n-khm&egrave;re de la famille des langues
austroasiatiques.</p>
            <p style="text-align: justify;">Pour les langues
p&eacute;ariques, Sidwell (2009)
propose la classification suivante, sur la base d'une revue d'analyses
pr&eacute;c&eacute;dentes portant sur ces langues. Selon cette
classification, les langues p&eacute;ariques se divisent en deux
branches (p&eacute;ar et chong). Le chong peut ensuite
&ecirc;tre
subdivis&eacute; en quatre groupes.</p>
            <p style="margin-left: 40px;">-&nbsp;<span
 style="font-weight: bold;">P&eacute;ar</span>
de la province de Kampong, Cambodge (Baradat 1941)</p>
            <p style="margin-left: 40px;">-
            <span style="font-weight: bold;">Chong</span></p>
            <p style="margin-left: 80px;">-&nbsp;<span
