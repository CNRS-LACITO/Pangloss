            <h2><a name="languages"></a>1.
A propos des langues bahnariques</h2>
            <p>Les langues
bahnariques appartiennent &agrave; la
famille des langues austroasiatiques. Elles sont parl&eacute;es
dans
une large zone de l'Asie du Sud-Est (Vietnam, Cambodge, Laos). Ce
groupe de langues pr&eacute;sente une importante
diversit&eacute;
interne.&nbsp;Sidwell (2002, 2003, 2009)
les classe en quatre branches : les langues bahnariques de l'Ouest, les
langues bahnariques centrales (ce groupe est ensuite
subdivis&eacute; :
langues bahnariques centrales du Sud et langues bahnariques du Sud),
les langues bahnariques du Nord et les langues bahnariques de l'Est
(voir &eacute;galement Matras et Ferlus
1971 pour une pr&eacute;sentation g&eacute;n&eacute;rale
des langues
austroasiatiques, et Thomas 1979 &agrave; propos
des&nbsp;langues bahnariques de l'Ouest).</p>
            <p>De septembre 1966
&agrave; avril 1968, un projet
ethnographique du Centre National de la Recherche Scientifique (CNRS) a
&eacute;t&eacute; conduit par Jacqueline Matras (voir
Matras et Martin 1972; Matras-Troubetzkoy 1974) dans le cadre de
l'&eacute;laboration d'un <span style="font-style: italic;">Atlas
ethno-linguistique
de l'Asie du sud-est et du monde Indon&eacute;sien</span>,
dans la province de Rotanah Kiri (Nord-Est du Cambodge). Au cours de ce
projet, principalement centr&eacute; sur l'&eacute;tude d'un
village
brou, un important herbier a &eacute;t&eacute; recueilli,
apportant de
pr&eacute;cieuses informations sur les usages de ces plantes et du
vocabulaire li&eacute; &agrave; celles-ci. Matras a
confi&eacute; cet
herbier au
Mus&eacute;um d'Histoire Naturelle de Paris. L'ensemble des plantes
et
v&eacute;g&eacute;taux qui le composent ont
&eacute;t&eacute;
identifi&eacute;s par&nbsp;Marie Alexandrine Martin
&agrave; la fois en
khmer et en brou.
Martin a &eacute;galement fourni une description botanique
compl&egrave;te de l'herbier de Matras. En f&eacute;vrier
1969,&nbsp;Marie A. Martin est retourn&eacute;e sur le terrain,
dans la
province de Rotanah Kiri, afin de revoir certains des informateurs de
Matras, mais aussi pour collecter de nouveaux &eacute;chantillons
et
apporter un &eacute;clairage sur des identifications,
rest&eacute;es
jusque l&agrave; sans r&eacute;ponse. Les donn&eacute;es
ont
&eacute;t&eacute; collect&eacute;es &agrave; Ban Tuh,
un petit village
situ&eacute; pr&egrave;s de&nbsp;Stung Treng (district de
Ta Veaeng,
province de Rotanah Kiri). Selon&nbsp;Matras et Martin (1972), les
Brou
sont un peuple des montagnes qui entretiennent de forts liens avec
les&nbsp;Tampuan, dans la zone Nord-Est du Rotanah Kiri.</p>
            <p>Comme de nombreuses
langues m&ocirc;n-khm&egrave;res, le
brou (autres orthographes : bru ou brao) et le tampuan ne
poss&egrave;dent pas de tons mais sont toutes deux des langues
&agrave;
registres de qualit&eacute; de voix&nbsp;: les voyelles peuvent
&ecirc;tre prononc&eacute;es avec une voix claire/modale ou
avec une voix
rel&acirc;ch&eacute;e/chuchot&eacute;e (Bauer 1987; Crowley
2000;
Keller 2001). Les voyelles de ces langues poss&egrave;dent en outre
un contraste de
longueur (dur&eacute;e). Le brou tout comme le tampuan connaissent
plusieurs dialectes qui varient des points de vue phonologique et
lexical (Ferlus 1974; Keller <span style="font-style: italic;">et al.</span>
2008). Ces diff&eacute;rences pourraient &ecirc;tre dues
&agrave; l'influence du lao, parl&eacute; dans le pays
voisin.&nbsp;<br>
