     <h4 id="war_amwi">1. war amwi</h4><br/>
     
     <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-LYN_ADA001_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Trois personnes : séparation et retrouvailles d'une mère et de ses deux fils</a></p>
     <p class="intertitre">
        Thangbuli, Meghalaya, Inde, 2008, Lewis Buam, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>Après la mort de son mari, une femme se trouve seule, démunie avec ses deux jeunes fils. Elle tente de chercher fortune dans un plus gros village (actuellement au Bangladesh) mais doit traverser la large rivière entre le Meghalaya et le Bengladesh. Un  transporteur Hindu dans sa barque la trompe au moment où elle embarque pour mettre à bord ses enfants, par un geste brusque il s'éloigne, la fait prisonnière et s'en va. Les deux enfants se retrouvent seuls.</p> 
            <p>Une vache dont le veau nouveau né vient de mourir à côté de la rive les découvre pleurant et les nourrit de son lait. Elle les emmène le soir dans l'abri du vieil homme solitaire qui la garde. Les jours passent ainsi où les enfants suivent la vache et un jour le vieil homme les découvre. Ils vivent ensemble avec amour dans sa hutte. Les enfants deviennent très habiles et gagnent une compétition organisée par le roi Hindu de ce pays. Le roi les invite à sa cour avec le vieil homme pour combattre les ennemis. Par leur intelligence et leur courage ils mettent les ennemis en déroute et deviennent ministres. Le roi leur offre de choisir une récompense. Ils décident de prohiber la viande de vache. Ensuite le vieil homme préfère retourner à sa hutte et les jeunes hommes alternent entre vie de cour et vie de hutte avec le vieil homme. Les jeunes gens qui aimeraient retrouver leur mère amènent le roi à autoriser tous les sujets non Hindus à retourner dans leurs collines d'origine. Mais la mère est toujours prisonnière du conducteur de bateau. Les conducteurs de bateau sont tous interrogés et finalement la mère retrouvée est libérée. Les fils tuent le ravisseur. Quelques années plus tard, le roi solitaire lègue son pays à ses deux fils adoptifs.</p>
            <p>Ce conte idéalise la bonne relation d'antan entre hindous et wars au Bengladesh.            </p>
            <p class="intertitre">&nbsp;</p>
            
            <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-LYN_ADA002_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Deux frères orphelins</a></p>
     <p class="intertitre">
        Thangbuli, Meghalaya, Inde, 2008, Lewis Buam, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>Après la mort de leurs parents, deux frères travaillent dans leurs champs et vivent de leurs récoltes, seuls dans leur hutte. Une vieille femme s'introduit dans leur vie pour les aider aux taches ménagères et se transforme la nuit en jeune beauté. L'aîné brûle sa peau de vieille et l'épouse. Ils vivent à trois paisiblement. Mais la jeune femme en fait une méchante sorcière tue son mari et fait manger le corps au cadet. Celui-ci le découvre trop tard. La sorcière s'enfuit et épouse le roi. Elle se prétend malade. Suit une description des remèdes et des guérisseurs. La jeune sorcière en profite pour se faire nourrir de tigres et autres sources de force.</p>
           <p> Après un certain temps le frère cadet finit par retrouver la jeune sorcière devenue reine à la cour du roi. Il prévient le roi qu'elle est sorcière et révèle son point faible : les âmes de son clan sont dans un panier qu'elle cache. Quand le panier est détruit, elle meurt.</p>
           <p> Sens intéressant de cette histoire sur la religion des ancêtres et les bigoteries.</p>
            
            <p class="intertitre">&nbsp;</p>
            <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-LYN_ADA003_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Mythe de fondation pour les wars et les pnars : Liʔ Dɔʔ Kha, Lɔʔ Rəndi 'dame chaire du poisson, sieur Rendi'</a></p>
     <p class="intertitre">
        Thangbuli, Meghalaya, Inde, 2008, Lyndoh Soh Tariang, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>Ce conte est traditionnellement utilisé dans les cérémonies funéraires secondaires, pendant la nuit de veillée.            </p>
        <p class="intertitre">&nbsp;</p>
            
            <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA020_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Récit du Bahilon</a></p>
     <p class="intertitre">
        Thangbuli, Meghalaya, Inde, 2008, Marlyda Pohleng, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>Grand récit fondateur pour les pnars et pour les wars.</p>
            <p>La narratrice, fille d'un grand représentant religieux propose des récits d'une grande qualité poétique.</p>
            <p>L'histoire se déroule le long de la rivière Myntadu qui sépare les terres amwi et pnars.  Une femme accouche d'un enfant bien que sans mari,  à l'écart de son village dans un grotte, car il est considéré comme illégitime. (La grotte est aussi un lieu de passage entre le monde du dessous et la terre plate.). Cet enfant devient un jeune homme géant et prodigieux en force, en intelligence et en générosité. Il devient le protecteur des siens mais sa puissance désintéressée provoque la jalousie d'un roi puissant et rapace. Il est finalement victime d'une traîtrise : il est invité à un banquet où les soldats du roi le font boire et le tuent ivre.            </p>
            <p class="intertitre">&nbsp;</p>
            
            <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA021_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Le chacal et la tigresse</a></p>
     <p class="intertitre">
        Thangbuli, Meghalaya, Inde, 2008, Marlyda Pohleng, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>Au début ils sont très amis. Ils plantent des yams mais le chacal profite de la naïveté de  la tigresse. Une autre fois ils vont à la chasse. Le chacal se prétendant malade se fait nourrir par la tigresse et par ses propres enfants. En leur absence il dévore en outre l'intérieur d'une vache et en fait son habitation.            </p>
        <p class="intertitre">&nbsp;</p>
        
        <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA023_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Une femme et sa fille</a></p>
     <p class="intertitre">
        Thangbuli, Meghalaya, Inde, 2003, Marlyda Pohleng, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>Une femme fait des gâteaux traditionnels que sa fille va vendre au marché. </p>
           <p>Sur le chemin, elle traverse un pont. Les chiens et les porcs sauvages ne parviennent pas à lui voler les gâteaux. Un jour au marché elle rencontre un chacal qui apprend à lire aux enfants en chantant. Ils en sont très contents. Un jour le chacal les emmène dans une grotte sous le pont et révèle sa perversité en tuant la fillette.            </p>
        <p class="intertitre">&nbsp;</p>
            
            <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA024_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Plantes et pratiques médicinales (1)</a></p>
     <p class="intertitre">
        Thangbuli, Meghalaya, Inde, 2008, sʔɔn, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>sʔɔn le guérisseur par les plantes (broʔ suraŋ) de Thangbuli. Longue description des plantes, racines, décoctions, pierres, os, fœtus, peaux de serpents et tous leurs mélanges éventuellement avec de l'huile ou de l'eau utilisés pour les maladies physiques ou psychiques.  Également les prières.            </p>
        <p class="intertitre">&nbsp;</p>
            
            <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA025_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Plantes et pratiques médicinales (2)</a></p>
     <p class="intertitre">
        Thangbuli, Meghalaya, Inde, 2008, Thibon Dkar, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>Guérisseuse par les plantes et surtout par les massages.</p>
        <p>&nbsp;</p>
