     <h4 id="war_nongtalang">4. war nongtalang            </h4>
     <p align="center">&nbsp;</p>
     <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA004_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Rythmes d'instruments traditionnels</a></p>
     <p class="intertitre">
        Lamin, Meghalaya, Inde, 2005, Sixme Gashnga, Wanmon Gashnga, Curfew Lamin, Pailot Gashnga, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>Instruments : thangmuri (bambous à notes différentes, frappés), buhuri sorte de petit hautbois, tambour. Intervenants : Sixme Gashnga et Wanmon Gashnga  qui jouent du buhuri ; Curfew Lamin qui joue les rythmes de thangmuri et Pailot Gashnga qui joue le tambour seŋ hənta pour les danses avec femmes.</p>
           <p> Les 5 principaux rythmes de Buhuri sont présentés dans 5 pièces de musiques différentes. Ces musiques sont jouées traditionnellement pour rɔŋ khusi, le festival du plaisir.            </p>
           <p class="intertitre">&nbsp;</p>
            
            <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA005_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Opposition phonologique tʃɔ ! (interjection) / tʒi 'riz'</a></p>
     <p class="intertitre">
      Kudeng Thymmai, Meghalaya, Inde, 2006, Lakhmie Pohtam Sohsley, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        
        <p>Opposition phonologique tʃɔ ! (interjection) / tʒi 'riz'    où /tʒ/  est en fait un dévoisement de /ɟ/ différent de /tʃ/. </p>
        
        	<p class="intertitre">&nbsp;</p>
        	<p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA006_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Récit d'un mythe de fondation pour les wars et les pnars : Liʔ Dɔʔ Kha, Lɔʔ Rəndi 'dame chaire du poisson, sieur Rendi'</a></p>
     <p class="intertitre">
       Sohkha, Meghalaya, Inde, 2002, Clan Thongper, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
       <p>Récit d'un Mythe de fondation pour les wars et les pnars : Liʔ Dɔʔ Kha, Lɔʔ Rəndi 'dame chaire du poisson, sieur Rendi' pendant une veillée de tʃənjə c'est à dire de rituel funéraire secondaire d'intégration des os à ceux de l'ancêtre du clan à Sohkha.</p>
          <p>  Récit mené par le clan Thongper. Enregistré à l'origine sur minicassette, d'où la qualité de son un peu moins bonne. La qualité littéraire de cette version, dans son contexte rituel et récité par les meilleurs représentants de deux clans wars très importants est en revanche exceptionnelle. </p>
            <p>Récit parlé-chanté en deux groupes rivaux : le clan Thongper qui mène et le clan Pohlong,  qui enchaînent les épisodes. Cette version est particulièrement intéressante du point de vue poétique et des procédés de réduplication avec assonances.</p>
           <p> Un homme nommé sieur Rendi pêche un gros poisson. </p>
           <p>Rentré chez lui il ne le mange pas mais le laisse sécher au dessus de sa cheminée. Quand il revient chez lui de ses champs la maison est rangée et le dîner est servi. Au bout d'un certain temps, il se cache pour savoir qui accomplit ceci. Il découvre une merveilleuse jeune femme chez lui et la peau vide du poisson à sa place. Il entre précipitamment, brûle la peau. Il épouse la jeune femme qui enfante les gens qui sont les wars et les pnars avec les graines de l'ancêtre clanique qu'elle incarne. Elle lui précise que la pureté d'une propreté absolue devra régner dans la maison. Un jour il oublie le balai à l'intérieur du foyer. Elle s'enfuit et retourne vivre dans la rivière clanique des wars et des pnars.            </p>
           <p class="intertitre">&nbsp;</p>
            
            <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA007_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Liste lexicale illustrant l'opposition : / tʒ /  /tʃ/ </a></p>
     <p class="intertitre">
       Kudeng Thymmai, Meghalaya, Inde, 2006, Lakhmie Pohtam Sohsley, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
       <p>Liste lexicale illustrant l'opposition : / tʒ /  /tʃ/ en fait deux fricatives distinctes.</p>
            <p class="intertitre">&nbsp;</p>
            <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA009_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Récit du long voyage à travers l'océan pour rencontrer la reine Larmati</a></p>
     <p class="intertitre">
       Kudeng Thymmai, Meghalaya, Inde, 2005, Thakur Pohtam Teang, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
       <p>Récit du long voyage à travers l'océan pour rencontrer la reine Larmati. Récit fantastique pour aller rencontrer chez elle la reine d'Angleterre.            </p>
       <p class="intertitre">&nbsp;</p>
       
        <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA010_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Khrishno et Rondika</a></p>
     <p class="intertitre">
       Kudeng Thymmai, Meghalaya, Inde, 2006, Thakur Pohtam Teang, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
       <p>Long et important conte qui réintègre dans la religion des ancêtres wars certains aspects l'épopée hindoue de Krishna dans le Maharabata.</p>
           <p> Ce récit est présenté avec le début du récit, transcrit glosé et traduit en anglais dans Daladier (2012) « A Multi-purpose Project for the Preservation of War Oral Literature » Hyslop, Morey, Post eds. NEILS 4, Cambridge India, pp.166-194.            </p>
           <p class="intertitre">&nbsp;</p>
       
        <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA011_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Conte traditionnel du chevreuil, des patates douces et du tigre</a></p>
     <p class="intertitre">
       Kudeng Thymmai, Meghalaya, Inde, 2006, Thakur Pohtam Teang, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
       <p>Un chevreuil mange les feuilles de patates douces d'un cultivateur. Il s'en aperçoit, lui tend un piège. Le chevreuil entre dans le piège et s'endort. Le croyant mort le cultivateur l'en sort. Le chevreuil s'échappe et se réfugie près d'une grotte où vit une chèvre. Un tigre arrive mais prend peur de la chèvre qui rumine en bougeant sa barbiche sans inquiétude. Le tigre appelle le chacal, ils s'enfuient. Le chacal meurt d'épuisement et le tigre courre encore.            </p>
       <p class="intertitre">&nbsp;</p>
       
        <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA012_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Conte traditionnel : Bal avec les ancêtres à l'intérieur du ciel</a></p>
     <p class="intertitre">
       Kudeng Thymmai, Meghalaya, Inde, 2006, Thakur Pohtam Teang, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
       <p>Description romanesque de l'invention des danses rituelles.</p>
           <p> Un vieux sage d'antan explique à une jeune fille ce qu'est un rituel, qu'est-ce que le sacrifice et comment aller dans le monde du ciel. Après bien des périples, elle épouse un prince du ciel dont les parents cultivent les fleurs magiques dont les graines sont celles des clans humains.</p>
            <p>Les danses rituelles de rɔŋ khusi, le festival du plaisir (voir "Exemples de rythmes sur instruments traditionnels"),  font venir les ancêtres protecteurs et les désirs amoureux des humains. L'ancêtre fleurit dans tous les sens du terme dans ses filles terrestres qui reproduisent les clans.            </p>
            <p class="intertitre">&nbsp;</p>
       
        <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA013_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Récit traditionnel : Suɲ, Irmaj, Hɛrmɔn, les trois soeurs ancêtres</a></p>
     <p class="intertitre">
       Kudeng Thymmai, Meghalaya, Inde, 2006, Thakur Pohtam Teang, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
       <p>Ce récit comporte une généalogie céleste expliquant le monde actuel tel que perçu par les wars non chistianisés. Hɛrmɔn ancêtre des rivières claniques est la mère de Liʔ Dɔʔ Kha  (voir "Récit parlé-chanté d'un Mythe de fondation pour les wars et les pnars : Liʔ Dɔʔ Kha, Lɔʔ Rəndi 'dame chaire du poisson, sieur Rendi'") l'ancêtre des wars et des pnars. suɲ est l'ancêtre du monde de passage entre la terre et le ciel. La sœur 'Fleur' de Liʔ Dɔʔ Kha  est l'ancêtre des khasis. Le mari d' Hɛrmɔn est Harata Khan War, lié aux wars et aux Kherwars (Munda du Nord). [ les Karbis, TB, se réclament aussi de cet ancêtre probablement parce qu'ils occupent ses terres claniques, au Nord du Meghalaya]. L'une des descendantes de Hɛrmɔn est l'ancêtre qui s'incarne dans l'océan Indien, elle régit les interventions anglaises et Hindous et préserve dans une certaine mesure les territoires pnars et wars du Bengladesh.            </p>
       <p class="intertitre">&nbsp;</p>
       
        <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA014_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Conte traditionnel : Les filles et petite filles de Liʔ Dɔʔ Kha</a></p>
     <p class="intertitre">
       Kudeng Thymmai, Meghalaya, Inde, 2006, Willi Pohtam Chernia, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
       <p>Le conte traditionnel commence par un chant d'amour de Woh Thakur.</p>
           <p> WohWilli  est ami de woh Thakur et parent de woh Monti, mais lui-même est devenu chrétien ; il a assisté au récit "Récit traditionnel : Suɲ, Irmaj, Hɛrmɔn, les trois soeurs ancêtres" de woh Thakur dans sa maison et son récit est une suite importante qui explique comment les clans Pohtam en provenance de la rivière amwi Kɛrvi  et ceux qui venaient de Jaintiapur au Bengladesh sont venus s'installer à Kudeng Thymmai, en territoire Pnar qui a été partagé grâce à l'accord des ancêtres.</p>
            <p>Une des petites filles de Liʔ Dɔʔ Kha n'arrête pas de pleurer, on essaie de l'échanger sans succès, on la met sur un radeau au bord de l'océan Indien. Elle accoste en Angleterre. Le roi lui offre la surface de terre d'une peau de vache. Par magie, la peau s'étend à toute la surface de Londres. Elle devient une reine et le chef de la Compagnie des Indes.            </p>
            <p class="intertitre">&nbsp;</p>
       
        <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA015_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Conte traditionnel d'une revanche du destin</a></p>
     <p class="intertitre">
       Kudeng Thymmai, Meghalaya, Inde, 2006, Willi Pohtam Chernia, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
       <p>Un homme seul vit avec son jeune fils après la mort de sa femme dans ses pauvres champs. Il rencontre un chevreuil qui lui fait découvrir le pays des trésors et son destin change.            </p>
       <p class="intertitre">&nbsp;</p>
       
        <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA016_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> Lexique en war Nongtalang de Nongbareh</a></p>
     <p class="intertitre">
      Nongbareh, Meghalaya, Inde, 2008, Jil Sea Pohlong, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
       <p>Ce dialecte est issu de Nongtalang, comme le sont Kudeng Thymmai, Lamin et Sohkha. Il est plus innovateur que les autres phonétiquement et cela va dans le même sens que mes notes grammaticales sue ce dialecte ; voir aussi le corpus (notamment de grand récit de Larsendriang).</p>
       
