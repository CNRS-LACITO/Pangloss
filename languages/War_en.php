     <p class="intertitre">
        Kudeng Thymmai, Meghalaya, Inde, 2008, Markus Pohshna, <a href="../../membres/daladier.htm">Anne Daladier</a></p>
        <p>Chantés dans sa maison par Markus Pohshna à Kudeng Thymmai, Janvier 2009.</p>
           <p> (à comparer avec les chants de Loŋ Haj enregistrés en war amwi à Thangbuli (2002) et avec ceux de Nongtalang en 2009).</p>
           <p> Le War de  Mawngap est un dialecte proche mais distinct à la fois de l'Amwi et du Nongtalang.</p>
            
            <p class="intertitre">&nbsp;</p>
            <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-AML_ADA019_SOUND"><img src="../../images/icones/sound1_bleu.jpg" alt="a" width="20" height="20" border="0"> La guerre entre les gens de Padu et ceux de Nongtalang</a></p>
