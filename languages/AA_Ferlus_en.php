            <h2><a name="languages"></a>1.
Some notes about Mon-Khmer (Austroasiatic) languages</h2>
            <h3>A classification
into ten groups</h3>
            <p>Mon-Khmer
is part
of a broader family: Austroasiatic. Ferlus (1996: 9) proposed a
classification of Austroasiatic languages in&nbsp;ten groups, based
on proposals by Thomas and Headley
(1970):</p>
            <p style="margin-left: 80px; text-align: justify;">1- <span
 style="font-weight: bold;">Kasic</span>
languages (spoken in the northearstern India and Bangladesh)</p>
            <p style="margin-left: 80px; text-align: justify;">2- <span
 style="font-weight: bold;">Palaungic</span>
languages (spoken in Burma and the Burma-Laos border areas. In <span
 style="font-style: italic;">Michel Ferlus's
Austroasiatic
archives</span>: <span style="font-weight: bold; font-style: italic;">Rmet</span>
language, <span style="font-style: italic; font-weight: bold;">Palaung</span>
language and&nbsp;<small><big><span
 style="font-weight: bold; font-style: italic;">Kh&aacute;ng</span>
language</big></small>)</p>
            <p style="margin-left: 80px; text-align: justify;">3- <span
 style="font-weight: bold;">Khmuic</span>
languages (spoken in Laos, the Laos-Thailand border areas and the
Laos-Vietnam border areas. In&nbsp;<span style="font-style: italic;">Ferlus'
Austroasiatic
archives</span>: <span style="font-style: italic; font-weight: bold;">Phay</span>&nbsp;
language and&nbsp;<span style="font-weight: bold; font-style: italic;">Ksing
Mun</span> language)</p>
            <p style="margin-left: 80px; text-align: justify;">4- <span
 style="font-weight: bold;">Vietic</span>
languages (mainly spoken in Vietnam)</p>
            <p style="margin-left: 80px; text-align: justify;">5- <span
 style="font-weight: bold;">Katuic</span>
languages (spoken in Laos, Thailand and Vietnam. In <span
 style="font-style: italic;">Michel Ferlus's
Austroasiatic
archives</span>: <span style="font-weight: bold; font-style: italic;">Bru</span>
language and <span style="font-style: italic; font-weight: bold;">Mankong</span>
language)</p>
            <p style="margin-left: 80px; text-align: justify;">6- <span
 style="font-weight: bold;">Bahnaric</span>
languages (spoken in Vietnam, Cambodia and Laos. In <span
 style="font-style: italic;">Michel Ferlus's
Austroasiatic
archives</span>: <span style="font-style: italic; font-weight: bold;">Nya-heun</span>
language and <span style="font-style: italic; font-weight: bold;">Laven</span>
language)</p>
            <p style="margin-left: 80px; text-align: justify;">7- <span
 style="font-weight: bold;">Khmer</span>
(mainly spoken in Cambodia but also in Thailand, Chanthaburi Province
and in the southern Vietnam)</p>
            <p style="margin-left: 80px; text-align: justify;">8- <span
 style="font-weight: bold;">Pearic</span>
languages (spoken in Cambodia and Thailand)</p>
            <p style="margin-left: 80px; text-align: justify;">9- <span
 style="font-weight: bold;">Monic</span>
languages (spoken in Burma and Thailand. In <span
 style="font-style: italic;"><span style="font-style: italic;">Michel </span>Ferlus'
Austroasiatic
archives</span>: <span style="font-weight: bold; font-style: italic;">M&ocirc;n</span>
language)</p>
            <p style="margin-left: 80px; text-align: justify;">10- <span
 style="font-weight: bold;">Aslian</span>
languages (spoken in Malaysia
and Thailand)</p>
            <br>
            <h3>Current
sociolinguistic situation: language replacement in progress, at an
increasingly rapid pace</h3>
            <p>Taking the Mảng
language as an illustration of these
languages' current situation, a monograph (<span
 style="font-style: italic;">The Mảng language</span>,
Nguyễn Văn Lợi, Nguyễn Hữu
Ho&agrave;nh&nbsp;&amp;&nbsp;Tạ&nbsp;Văn
Th&ocirc;ng 2009) points out the high risk of language loss and
cultural loss associated with the population's resettlement, following
the construction of the Sơn La hydroelectric dam.&nbsp;</p>
            <p style="text-align: justify;">"Kết
quả điều tra
d&acirc;n số năm 1999 cho biết, d&acirc;n tộc Mảng hiện
c&oacute; 2663
người, cư tr&uacute; tập trung tại c&aacute;c huyện
S&igrave;n Hồ, Mường Lay (thuộc tỉnh Lai
Ch&acirc;u), Mường T&egrave; (thuộc tỉnh Điện Bi&ecirc;n).
(...) Theo c&aacute;c t&aacute;c giả của c&ocirc;ng
tr&igrave;nh: "Những nh&oacute;m d&acirc;n tộc
thu&ocirc;c ngữ hệ Nam &Aacute; ở T&acirc;y Bắc Việt Nam",
xuất
bản năm 1972, ở nước ta người Mảng c&oacute; 241 gia
đ&igrave;nh, 1398 người (...).
So s&aacute;nh số lượng người Mảng theo t&agrave;i liệu
n&agrave;y với số liệu vừa tr&igrave;nh
b&agrave;y, đồng thời đối chiếu danh s&aacute;ch c&aacute;c
bản người Mảng đang sinh sống
hiện nay với danh s&aacute;ch 19 bản người Mảng cư tr&uacute;
trước kia, ch&uacute;ng ta
nhận thấy rằng: Sau 30 năm, số lượng người Mảng tăng gần gấp
đ&ocirc;i v&agrave; c&oacute;
sự thay đổi khu vực cư tr&uacute;: c&oacute; những bản trước
kia kh&ocirc;ng c&oacute; người Mảng,
nay c&oacute; tộc người n&agrave;y sinh sống, đồng thời,
c&oacute; những bản trước kia c&oacute;
người Mảng, nay kh&ocirc;ng c&ograve;n nữa." (pp. 15-17)</p>
            <p style="text-align: justify;">"Do
điều kiện lịch
sử, địa l&iacute;, d&acirc;n số hiện nay, văn h&oacute;a
v&agrave; ng&ocirc;n ngữ Mảng c&oacute; nguy cơ mai một,
dẫn đến ti&ecirc;u vong. Điều lưu &yacute; l&agrave;, địa
b&agrave;n cư tr&uacute; của người Mảng hiện nay đều nằm trong
v&ugrave;ng l&ograve;ng hồ thủy điện Sơn La, việc di dời
c&aacute;c l&agrave;ng bản của họ đi nơi kh&aacute;c
l&agrave; kh&ocirc;ng tr&aacute;nh khỏi. Do đ&oacute;,
việc thu thập t&agrave;i liệu về ng&ocirc;n ngữ, văn
h&oacute;a Mảng trở n&ecirc;n v&ocirc; c&ugrave;ng cấp
thiết." (p. 19)<br>
            </p>
            <p>"Tiếng Mảng hiện
nay
ph&acirc;n bố ở một vị tr&iacute; tương đối biệt lập,
t&aacute;ch khỏi c&aacute;c ng&ocirc;n ngữ Mon - Khmer
kh&aacute;c. Trong v&agrave;i ba thế kỉ gần đ&acirc;y,
ng&ocirc;n ngữ n&agrave;y tiếp x&uacute;c nhiều với
c&aacute;c ng&ocirc;n ngữ Th&aacute;i t&acirc;y - nam
(Th&aacute;i Đen, Th&aacute;i Trắng ở Việt Nam, Đai ở
V&acirc;n Nam) v&agrave; c&aacute;c ng&ocirc;n ngữ Tạng
- Miến (c&aacute;c ng&ocirc;n ngữ nh&oacute;m Miến Điện -
L&ocirc; L&ocirc; như Di, H&agrave; Nh&igrave;,
Ph&ugrave; L&aacute;, C&ocirc;ống..., ở bi&ecirc;n giới
Việt Trung).<br>
Tiếng Mảng đ&atilde; trải qua một qu&aacute; tr&igrave;nh
biến đổi l&acirc;u d&agrave;i v&agrave; s&acirc;u sắc
trong sự tiếp x&uacute;c với c&aacute;c ng&ocirc;n ngữ
Th&aacute;i - Kađai v&agrave; Tạng - Miến -- c&aacute;c
ng&ocirc;n ngữ kh&aacute;c về nguồn gốc v&agrave; một số
đặc điểm loại h&igrave;nh học. Tiếng Mảng c&oacute; nhiều từ
vay mượn từ tiếng Th&aacute;i. Một số đặc điểm ngữ &acirc;m,
ngữ ph&aacute;p của ng&ocirc;n ngữ n&agrave;y
kh&ocirc;ng giống với c&aacute;c ng&ocirc;n ngữ Mon -
Khmer. T&iacute;nh chất cận &acirc;m tiết t&iacute;nh
(sesquisyllable) ở tiếng Mảng kh&ocirc;ng r&otilde; rệt như ở
c&aacute;c ng&ocirc;n ngữ Mon - Khmer kh&aacute;c, kể cả
c&aacute;c ng&ocirc;n ngữ Việt - Mường (Vietic) như Arem, Rục.
Tiếng Mảng c&oacute; hệ thống thanh điệu ph&aacute;t triển;
thanh điệu biến đổi trong ngữ lưu." (pp. 36-37)</p>
            <p style="text-align: right;">(Nguyễn
Văn Lợi,
Nguyễn Hữu Ho&agrave;nh&nbsp;&amp;&nbsp;Tạ&nbsp;Văn
Th&ocirc;ng. 2009. <span style="font-style: italic;">Tiếng Mảng</span>.
H&agrave; Nội: NXB Khoa học
X&atilde; hội.)</p>
