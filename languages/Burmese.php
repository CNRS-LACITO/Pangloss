<p class="intertitre">&nbsp;</p>
       <h4 class="intertitre"><a name="presentation"></a>Pr&eacute;sentation </h4>
      <p> Le birman est une langue de la famille sino-tibétaine, de la branche tibéto-birmane. Cette branche de la famille comprend entre 250 à 400 langues selon les sources et fait l'objet d'un consensus parmi les linguistes.<br/>
L'appartenance du birman à la famille des langues tibéto-birmanes est un fait non discutable comme l'attestent de nombreux travaux en linguistique historique (Benedict 1942, Shafer 1955, Matisoff 1973). Cependant, cette langue possède de nombreuses caractéristiques phonologiques, morphosyntaxiques, pragmatiques ou lexicales, rares ou non attestées dans les autres langues de la famille, des traits structurels, généralement acquis par contact (cf. les travaux sur l'aire linguistique ou Sprachbund en Asie du Sud-Est continentale, i.e. une zone géographique dans laquelle des langues de familles linguistiques diverses partagent des traits structurels: Matisoff 1991, Enfield 2005, Vittrant 2010).<br/>
Le birman est une langue tonale, à tendance isolante, à morphologie pauvre (analytique), sans flexion, et faisant usage de classificateurs et de séries verbales. C'est une langue très contextuelle, pour laquelle on peut parler de complexité cachée (Bisang 2009) </p>
      <p><br/>
      </p>
<h4 class="intertitre">Le birman: Présentation générale (D'après Vittrant 2004 : 83-93)</h4>
<p><b>1- La langue birmane : locuteurs, territorialisation et appartenance génétique</b></br>
<p>Langue nationale en Birmanie (ou Myanmar) et langue officielle depuis 1948, le birman tous dialectes confondus, est la langue maternelle de plus de la moitié de la population (54 millions d'après Watkins 2007), soit environ 32 millions de locuteur.
Une carte ethnolinguistique de la Birmanie (cf. cartes ci-jointes) montre que le birman, langue de l'ethnie dominante, est parlée principalement dans la vallée centrale (vallée de l'Irrawaddy) — les minorités ethniques occupant les hautes terres périphériques du pays. Elle montre aussi que les langues parlées sur le territoire birman appartiennent à des familles linguistiques diverses.
Le birman fait partie de la branche Lolo-burmese ou Burmish  des langues tibéto-birmanes (famille sino-tibétaine) tout comme le maru, le atsi, langues parlées par des populations vivant au nord de la Birmanie à la frontière des états Shan et Kachin. 
Parmi les autres langues de la famille tibéto-birmane que l'on trouve dans ce pays, on notera le karen, le kachin (connu aussi sous le nom de Jingpho), dont les peuples vivent dans les états de même nom au sein de l'Union Birmane, et les langues Kuki-Chin (kuki-naga) parlées majoritairement dans l'état chin.
</p>
<p>
Les autres familles linguistiques représentées en Birmanie sont les familles Hmong-Mien, Austro-asiatique et Tai-Kadai. Les langues les plus importantes de ces deux dernières familles sont respectivement le môn et le shan ; la première est parlée dans le sud de la Birmanie (état môn et Tenasserim) et appartient à la branche môn-khmer de la famille austro-asiatique. La seconde, le shan, qui estparlée dans le Nord-Est du pays (région frontalière de la Thaïlande et de la Chine), est la plus importante des langues tai de Birmanie .
Le birman est une langue à tradition écrite. Les premiers textes connus datent du 12e siècle (stèle de Myazedi, Pagan). L'écriture birmane est dérivée du nâgari — ou devanâgari —, écriture évoluée du brâhmi et utilisée pour la transcription de langues indo-européennes de l'Inde tel le sanskrit ou le pali. De ce fait, l'alphabet (ou alpha-syllabaire) apparaît souvent mal adapté pour transcrire une langue tonale telle que le birman. Il comporte 33 consonnes dont certaines ne sont d'ailleurs utilisées que pour la transcription des termes d'origine pali. Ces consonnes sont toujours prononcées suivies d'un [a] au ton plat et bas. 
Les différents tons sont indiqués dans l'écriture de la voyelle en syllabe ouverte ou en syllabe fermée par une nasale. Les tons ne sont pas tous marqués dans l'écriture. Cependant, malgré sa complexité (64 voyelles écrites en combinaison avec le ton),  le système réussit à indiquer sans ambiguïté le ton de chaque voyelle. 
</p>

<p><b>2- A propos du pali en Birmanie aujourd'hui</b><br>
Utilisé dès la création de l'état birman au XIe siècle, considéré encore aujourd'hui comme une langue de prestige, le pali est toujours très présent en Birmanie. Une grande part du vocabulaire emprunté appartient à la langue courante, et le pali est toujours la langue de la religion par excellence (à la manière du latin dans la tradition catholique). En outre, il reste une référence en matière de grammairemalgré l'avènement de l'anglais dans ce domaine depuis la colonisation. Les emprunts au pali sont parmi les plus nombreux et les plus anciens (avec les emprunts au môn) et concernent en général le vocabulaire abstrait, philosophique ou religieux. Notons encore que la formation de néologismespour désigner de nouveaux concepts ou des techniques récentes, continue de faire appel au pali — tout comme le français fait appel au grec et au latin pour la création de mots nouveaux dans le domaine des sciences.
</p>
<p><b>3- Diglossie : langue littéraire et langue vernaculaire</b></br>
Une distinction très nette existe entre la langue parlée — ou langue vernaculaire — et la langue écrite (ou langue littéraire) des discours officiels, écrits administratifs, romans, mais aussi journaux, inscriptions, panneaux de signalisation etc... La différence essentielle entre ces deux idiomes réside dans l'existence d'un double système de particules grammaticales (marques verbales de fin de phrase, marques syntaxiques et connecteurs) généralement distinctes. La langue littéraire a, par ailleurs, un système grammatical plus nuancé et donc morphosyntaxiquement plus riche. Cette diglossie n'est cependant pas totale, et il ne s'agit pas à proprement parlé de deux dialectes distincts.
</p>
<p><b>4- Typologie de la langue en bref</b></br>
Nous présenterons ici les caractéristiques linguistiques du birman standard, ou dialecte de Birmanie centrale, d'après les enregistrements (monologues, dialogues, récits) collectés dans cette région (Yangon, Pagan).

<h5>Caractéristiques phonologiques :</h5>
<ul>
<li> Le birman est une LANGUE TONALE : 4 tons distingués selon des critères de hauteur, longueur, direction et de qualité de voix)</li>
<li>L'opposition d'aspiration (occlusives, nasales, liquides) qui est un trait distinctif en birman, a un rendement élevé. L'opposition de sonorité est pertinente, mais le voisement des consonnes initiales dépend souvent du contexte</li>
</ul>

<h5>Caractéristiques morphologiques</h5>
<ul>
<li>Le birman est une langue essentiellement MONOSYLLABIQUE. Il existe cependant beaucoup de mots composés qui rendent peu apparent ce monosyllabisme structurel.</li>
<li>C'est aussi une langue très ANALYTIQUE, à tendance isolante ; elle possède peu d'affixes, et elle est de ce fait beaucoup moins agglutinante que d'autres langues de la famille.</li>
</ul>

<h5>Caractéristiques syntaxiques</h5>
<ul>
<li>Le birman fait usage en grande proportion de PHRASES NOMINALES, à côté des phrases à prédicat verbal. Il est courant dans un énoncé nominal de faire suivre le (dernier) syntagme nominal d'une marque de politesse et/ou d'exclamation</li>
<li>Une phrase verbale (ou à prédicat verbal) contient au minimum un constituant verbal (syntagme verbal non-étendu) qui est le SEUL ELEMENT OBLIGATOIRE. Ce constituant verbal ne porte aucun indice actanciel. La présence des arguments est facultative, i.e. liée à l'accessibilité du référent qu'ils représentent.</li>
<li>Les participants au procès, quand ils sont présents, peuvent ou non être affectés d'un morphème fonctionnel dont le sens propre est locatif. Ce marquage fonctionnel apparaît s'il y a une ambiguïté ou en fonction énonciative, i.e. topicalisation, focalisation (Vittrant 1999)</li>
<li>Parmi les éléments constituant les syntagmes nominaux, on notera l'existence de nombreux CLASSIFICATEURS (Vittrant 2002), et d'un système de pronom riche et marqué pour le genre de l'interlocuteur (et non du référent désigné).</li>
<li>Le constituant verbal comprend au minimum un lexème verbal et une particule verbale finale (PVF) délimitant le constituant verbal. Entre ces deux éléments peuvent apparaître facultativement différents types de morphèmes (AUX) véhiculant principalement des NOTIONS ASPECTUELLES OU MODALES, LE TEMPS GRAMMATICAL n'étant pas marqué dans le syntagme verbal en birman. Plusieurs lexèmes verbaux peuvent être juxtaposés formant des CONSTRUCTIONS VERBALES EN SERIE (CVS), un phénomène courant dans la région (Vittrant 2006).</li>
</ul>

</p>
