            <p class="intertitre" align="left"> </p>
            <p class="intertitre" align="left">Au sein du groupe
vietique, le
sous-groupe chứt comprend: chứt, s&aacute;ch, salang, rục,
m&agrave;y.</p>
            <p class="intertitre" align="left">"Les <span
 style="font-style: italic;">S&aacute;ch</span>
du Vietnam et les <span style="font-style: italic;">Salang</span>
[<span style="font-weight: bold;">sălaːŋ&sup1;</span>]
du Laos,
r&eacute;partis des deux c&ocirc;t&eacute;s de la
fronti&egrave;re
&agrave; la latitude du col de Mụ Giạ, sont une seule et
m&ecirc;me
population dont l'autonyme est <span style="font-style: italic;">Chứt</span>
[<span style="font-weight: bold;">cɨt⁷</span>].
Une autre population
dont la langue est pratiquement la m&ecirc;me que celle de ces
derniers, quoique sensiblement plus archa&iuml;que, est
form&eacute;e
des <span style="font-style: italic;">Rục</span>
qui vivent
pr&egrave;s des <span style="font-style: italic;">S&aacute;ch</span>,
et des <span style="font-style: italic;">M&agrave;y</span>
qui
s'&eacute;tendent plus au nord jusqu'au contact des <span
 style="font-style: italic;">M&atilde;liềng</span>.
Les <span style="font-style: italic;">Kha
Mụ Giạ</span> (Fraisse 1950) et les <span style="font-style: italic;">Tắc
Củi</span>
(Guignard 1911) sont
parfaitement identifiables aux <span style="font-style: italic;">Rục</span>
gr&acirc;ce au court vocabulaire donn&eacute; par chaque
auteur. On
peut consid&eacute;rer que ces populations parlent la
m&ecirc;me langue
et c'est la raison pour laquelle nous avons choisi le mot <span
 style="font-style: italic;">Chứt</span>
pour d&eacute;signer ce
sous-groupe. Rappelons que le P&egrave;re L&eacute;opold
Cadi&egrave;re
(1905) a &eacute;t&eacute; le premier &agrave; donner un
vocabulaire <span style="font-style: italic;">S&aacute;ch</span>,
qui fut peu
apr&egrave;s utilis&eacute; par M.A. Ch&eacute;on (1907)
dans une
&eacute;tude comparative avec le nguồn et le mường. La langue
des&nbsp;<span style="font-style: italic;">Chứt</span>
a &eacute;t&eacute; fortement
influenc&eacute;e par la langue de leurs voisins Nguồn qu'ils
nomment <span style="font-style: italic;">Tu
Vang</span>.</p>
            <p class="intertitre" align="left">D'apr&egrave;s les
auteurs vietnamiens <span style="font-style: italic;">Chứt </span>signifierait
"montagne, falaise rocheuse" dans la langue de cette ethnie, tandis que
            <span style="font-style: italic;">Rục
            </span>"grotte,
rivi&egrave;re souterraine" est repr&eacute;sent&eacute;
dans le
dialecte vietnamien local. Ces ethnonymes font
r&eacute;f&eacute;rence
&agrave; l'aire d'habitat. <span style="font-style: italic;">M&agrave;y</span>
n'a pas de sens connu. Les significations de <span
 style="font-style: italic;">S&aacute;ch </span>et
de <span style="font-style: italic;">Salang</span>
seront
&eacute;tudi&eacute;es plus loin. Les <span style="font-style: italic;">Tắc
Củi </span>seraient "ceux qui
allument le feu par percussion". Notons
bien qu'ici l'usage de <span style="font-style: italic;">Chứt </span>est
restreint aux ethnies de cette langue, alors qu'au Vietnam il est
utilis&eacute; pour d&eacute;signer l'ensemble des petits
groupes
viet-muong du Quảng B&igrave;nh et du H&agrave; Tĩnh: <span
 style="font-style: italic;">Arem, S&aacute;ch,
Rục, M&agrave;y,
M&atilde;liềng </span>et <span style="font-style: italic;">Kha Phong</span>,
malgr&eacute; l&eacute; diff&eacute;rences ethnographiques
et
linguistiques.</p>
            <p class="intertitre" align="left">Les <span
 style="font-style: italic;">S&aacute;ch</span>
et les <span style="font-style: italic;">Salang
            </span>sont agriculteurs
dans les
hautes vall&eacute;es des montagnes calcaires entre les provinces
du&nbsp;Quảng B&igrave;nh et de Thakhek tandis que les <span
 style="font-style: italic;">Rục </span>et
les <span style="font-style: italic;">M&agrave;y</span>
&eacute;taient, il y a
encore quelques d&eacute;cennies, des semi-nomades forestiers avant
d'&ecirc;tre regroup&eacute;s et fix&eacute;s par les
autorit&eacute;s.
On peut &eacute;valuer le nombre de locuteurs de ces parlers
&agrave;
1.500 approximativement."&nbsp;</p>
