      <p class="intertitre"><a href="../corpus/show_text_en.php?id=cocoon-38cdabfc-e3f9-3ae0-ab64-6cbb95cd8569&idref=cocoon-5cffecc9-3678-308b-9751-451937a1e731"><img src="../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> The Wind</a><br>
        Turkey, 2007, Nurettin Akdemir, <a href="../../membres/lacroix.htm">René Lacroix</a></p>
      <p>A man asked his wife how much she loved him. She answered: &quot;As much as the cool wind in a hot day&quot;. Disappointed with this answer, the man killed his wife. It was a hot day.  As a cool wind arose, the man realized how much his wife loved him and he killed himself.</p>
