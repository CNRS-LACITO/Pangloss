<h1>Saek (Sek, S&egrave;k; in Thai:&nbsp;ภาษาแสก)</h1>
</div>
<div class="text-left"> <a href="Tai_en.htm#Vietnamese"><span
 style="font-style: italic;">Giới
thiệu bằng tiếng Việt / Presentation in Vietnamese</span></a><br>
<br>
The Pangloss Collection hosts data from six
Tai-Kadai languages recorded by Michel
Ferlus, mostly from 1997 to 2003.&nbsp;Note that these unique data
are <span style="font-weight: bold;">working
documents</span>
collected as part
of research on Tai manuscripts and languages: they were not originally
intended for applications such as acoustic-phonetic analysis; the
recordings are of uneven technical quality. <br>
<br>
About the Saek language, see: <br>
Chamberlain, James R. 1998. "The Origin of the Sek: Implications for
Tai and Vietnamese History". Journal of the Siam Society 86.1 &amp;
86.2: 27-48.<br>
Gedney, W. J. (1993). William J. Gedney's the Saek language: glossary,
texts, and translations. Michigan papers on South and Southeast Asia,
no. 41.&nbsp;Center for South and Southeast Asian Studies, University
of
Michigan.<br>
Hudak, Thomas J., and William J. Gedney. 2010. <span
 style="font-style: italic;">William J. Gedney's concise Saek-English,
English-Saek lexicon</span>. Oceanic Linguistics special publication,
no. 37. Honolulu: University of Hawai'i Press.<br>
Morev, L. N. 1988. <span style="font-style: italic;">Yozik Sek [The
Saek Language]</span>. Moscow: Nauka.<br>
Pacquement, Jean. 2017. &nbsp;<a
 href="https://www.academia.edu/32405598/The_Saek_Language_the_first_dialect_and_the_second_dialect"
 target="_blank">The Saek language: the first dialect and the second
dialect</a>. <br>
Pacquement,&nbsp;Jean. 2017. <a
 href="https://www.academia.edu/26527312/Saek_Language_in_the_Early_Years_of_the_20th_Century_and_Nowadays"
 target="_blank">Saek language in the early years of the 20th century
and nowadays</a>.<br>
<br>
