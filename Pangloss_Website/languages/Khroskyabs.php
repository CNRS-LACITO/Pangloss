            <p class="intertitre" align="left">Le khroskyabs est une langue rgyalronguique dans la famille trans-himalayenne (ou sino-tibétain) parlée dans les districts de Chuchen et de Dzamthang, ainsi que dans la ville de 'Barkhams, au Sichuan, en Chine. C'est une langue SOV présentant un système phonologique élaboré et une morphologie verbale polysynthétique.</p>
           
            <p style="text-align: justify;" class="intertitre">Le khroskyabs est, parmi toute langue trans-himalayenne connue, celle qui possède le plus grand nombre de groupes de consonnes initiaux. Le dialecte de Wobzi, notamment, en a 715 groupes attestés (Lai 2017: 100), dont le groupe le plus élaboré a cinq consonnes consécutives : ʁɴzbrɑ́ 'oser (générique)'.</p>
            <p style="text-align: justify;" class="intertitre">En ce qui concerne la morphologie verbale, le khroskyabs manifeste une morphologie gabaritique principalement préfixante. L'indexation personelle est hiérarchique à marquage de l'inverse, par conséquent, un verbe transitif est capable d'indexer des informations de deux actants en même temps. La morphologie dérivationnelle subit de nombreux processus morpho-phonologiques, particulièrement, le préfixe causatif s- a des variantes selon le contexte phonologique : kʰɑ̂ 'donner' → s-kʰɑ̂ 'faire donner', gí 'habiller' → z-gí 'faire habiller', ʁbɑ́ɣ 'exploser' → ʁ&lt;z&gt;bɑ́ɣ, vzɑ́r 'être piquant' → l-zɑv 'rendre piquant', etc (Lai 2016).</p>
            <p style="text-align: justify;" class="intertitre">Le khroskyabs présente non seulement des traits conservateurs qui facilitent l'étude linguistique comparative trans-himalayenne, mais aussi des phénomènes innovatrices typologiquement rares ou même uniques. C'est donc une langue importante dans la famille et pour la linguistique générale. Malheureusement, la plupart des dialectes du khroskyabs ne sont pas encore suffisamment documentés. Je profite de cette occasion pour encourager ceux qui sont intéressés à nous rejoindre pour améliorer ce domaine de recherche.</p
