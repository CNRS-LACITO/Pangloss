            <h4 align="left">Nart Sagas</h4>

            <p>The Narts are legendary heros of the Caucasus. The
three stories presented were recorded in 1973 by the eminent Adyghe
scholar Asker Hadaghatl'a (in Russian, Gadagatl'). They had previously
been collected and published in volume 7 of his vast anthology of Nart
sagas (as they have come to be called in English) in a variety of
Adyghe languages:<br>

Gadagatl', Asker. 1968-1971. <i>Nartxer</i> [<i>The
Narts</i>]. Maikop. 7 vols.</p>

            <p>The sagas have the following titles:&nbsp;: </p>

            <ul class="liste-liens">

              <li> <a href="../corpus/show_text_en.php?id=cocoon-8c4ba415-f67e-35f7-80f2-aa2c7bcf307f"><img src="../images/icones/Txt_Inter_parchemin.jpg" alt="a" border="0" height="20" width="20"> The Hill of
Nart Baoutch</a> (Gadagatl' 1971 v. 7 no. 596, p. 65)</li>

              <li> <a href="../corpus/show_text_en.php?id=cocoon-f4a37dbe-8b61-344b-b0b1-074fa1ccf9d6"><img src="../images/icones/Txt_Inter_parchemin.jpg" alt="a" border="0" height="20" width="20"> The
Marriage of Nart Ghoutch'epl</a> (Gadagatl' 1971 v. 7 no. 641, p.
158)</li>

              <li> <a href="../corpus/show_text_en.php?id=cocoon-bc9f2164-3b52-367e-b868-cc496b069ddb"><img src="../images/icones/Txt_Inter_parchemin.jpg" alt="a" border="0" height="20" width="20"> The Nart
