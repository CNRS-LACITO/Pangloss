            <p class="intertitre">&nbsp;</p>
            <p>"The Arem people
are located in Vietnam&rsquo;s Quảng B&igrave;nh province,
Bố-trạch district, T&acirc;n-trạch commune, C&agrave;
R&ograve;ong hamlet. Around 1960, there were 53 people, 30 men and
23 women (Vương Ho&agrave;ng Tuy&ecirc;n 1963: 69). Forty years
later, there were 102 people (Trần Tr&iacute; D&otilde;i 1999:
110). In the past, Arem lived in rock shelters or makeshift huts. They
exchanged products from hunting and gathering with the neighboring
populations, Vietnamese, Bru kh&ugrave;a or Lao. The need to find
marital partners outside the group has the consequence that only a
minority of Arem, about a quarter, regularly speak their language. The
Arem language is in great danger of extinction.&nbsp;</p>
            <p>The term Arem
(Vietnamese spelling of areːm) seems to be an exonym. Those concerned
prefer to be named cmbrau [<span style="font-weight: bold;">cmrawˀ</span>]."
            </p>
            <p>(excerpted
from:&nbsp;Ferlus, Michel. 2014. <a
 href="http://www.mksjournal.org/mks43ferlus.pdf" target="_blank">Arem,
a Vietic language</a>. <i>Mon-Khmer
Studies</i> 43. 1&ndash;15.)</p>
