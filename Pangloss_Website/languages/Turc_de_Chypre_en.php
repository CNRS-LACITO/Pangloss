      <p class="intertitre">

Ce projet de documentation du dialecte turc parlé à Chypre s'est déroulé en juillet et août 2016. Il n'aurait jamais pu être réalisé sans l'aide d'Erk et Emre Günce et de leur famille. Erk, locuteur du turc standard et du turc chypriote, a introduit René Lacroix auprès des informateurs, les a interviewés et a transcrit quelques extraits des enregistrements.</p>
<p>

René Lacroix remercie chaleureusement toutes les personnes qui ont rendu possible son terrain à Chypre, y compris les locuteurs enregistrés, pour l'aide précieuse qu'ils lui ont apportée, leur accueil chaleureux et le temps qu'ils ont bien voulu lui consacrer.
</p>
