            <h2><a name="language"></a>1.
A propos du khamou</h2>
            <p style="text-align: justify;">Les
Khamou&nbsp;(autres orthographes : Khamu, Khammu,
Khmu ou
Kemu) vivent principalement au nord du Laos : Nam-Tha,
Ph&ocirc;ngsali, Sam-Neua et Luang Phrabang. Ils sont
consid&eacute;r&eacute;s comme un peuple autochtone dans ces
zones. Il
y a d'autres groupes localis&eacute;s en Chine du Sud-Ouest
(province
de Yunnan), au nord du Vietnam (Son-Tay et&nbsp;Cua-Rao), en Birmanie
et en Tha&iuml;lande (ville de Chiang-Ray et
province de Nan).</p>
            <p style="text-align: justify;">Ils parlent le
khamou, une langue de la branche khmuique
de la famille austroasiatique. Cette langue est proche du ksingmul
(xinh mun)
au
Vietnam et au Laos, et du phong (kniang) au Laos
(voir Ferlus 1977; Sidwell 2014). Le khamou constitue une formidable
introduction &agrave; des caract&eacute;ristiques typiques du groupe
m&ocirc;n-khmer tels que
la
morphologie infixale (Ferlus
1977a) et la structure sesquisyllabique (Matisoff 1973).</p>
            <p style="text-align: justify;">Il existe plusieurs
dialectes du khamou, qui
pr&eacute;sentent des diff&eacute;rences dans leur inventaire
consonantique ou dans l'existence de registres de voix distinctifs,
parfois parvenus au stade d'un v&eacute;ritable
syst&egrave;me tonal (Suwilai 2001, 2004). Sur la base de ces
diff&eacute;rences, le khamou peut &ecirc;tre divis&eacute;
en deux
principaux dialectes : le <span style="font-style: italic;">khamou de
l'Ouest</span>
poss&egrave;de moins de
phon&egrave;mes consonantiques et a connu une bi-partition de
registres
de voix<span style="font-style: italic;"></span>
(rel&acirc;ch&eacute;/souffl&eacute; vs. tendu/modal) alors
que le <span style="font-style: italic;">khamou
de l'Est</span> ne
poss&egrave;de ni registres ni tons.&nbsp;<span
 style="font-style: italic;"></span>Le khamou de l'Est, plus
conservateur, distingue trois ordres de consonnes initiales: occlusives
vois&eacute;es, non vois&eacute;es,
aspir&eacute;es; et nasales&nbsp;vois&eacute;es,
non vois&eacute;es, glottalis&eacute;es (Suwilai <span
 style="font-style: italic;">et al.</span>
2002). Des recherches approfondies ont &eacute;t&eacute; men&eacute;es
sur ces
deux
vari&eacute;t&eacute;s du khamou (voir Svantesson 1983; Suwilai
2004;
Lindell <span style="font-style: italic;">et
al.</span> 1979, 1981).</p>
            <p>Le khamou est de
nos jours une langue toujours
attest&eacute;e et vivace, mais il reste beaucoup &agrave; faire pour
sa documentation et son &eacute;tude.&nbsp; </p>
            <br>
            <h2><strong><a name="archived_doc"></a>2.
Les documents archiv&eacute;s</strong></h2>
            <p style="text-align: justify;"><span>Michel
Ferlus a recueilli des donn&eacute;es khamoues de 1963 &agrave;
la fin
des ann&eacute;es 70. Au fil de ces ann&eacute;es de collecte et
d'analyse, Ferlus a appris le khamou, qu'il parle couramment.</span><span> Le recueil de son corpus khamou
repr&eacute;sente son
travail de terrain le plus approfondi : il a collect&eacute;
suffisamment de donn&eacute;es pour r&eacute;aliser
un
dictionnaire et une grammaire de cette langue, ainsi qu'un recueil de
textes de tradition orale int&eacute;gralement transcrits et
annot&eacute;s. Ces
ressources
apportent d'importants &eacute;clairages &agrave; la fois sur
le plan
de la linguistique historique et sur le plan de la
compr&eacute;hension
des us et coutumes des Khamou. Mais plut&ocirc;t que de consacrer des
ann&eacute;es &agrave; la publication de cette fabuleuse somme de
connaissances au sujet d'une unique langue, Michel Ferlus a suivi le
fil de recherches en phonologie historique qui l'ont amen&eacute;
&agrave; explorer une grande diversit&eacute; de langues. La
linguistique historique se nourrit en effet de la diversit&eacute; des
parlers pris en compte; certes les donn&eacute;es doivent &ecirc;tre
solides (recueillies avec pr&eacute;cision), mais quelques centaines de
mots (parfois quelques dizaines) peuvent suffire &agrave;
l'enqu&ecirc;teur aguerri pour d&eacute;celer des
ph&eacute;nom&egrave;nes historiques. De la sorte, la publication des
donn&eacute;es khamou s'est trouv&eacute;e ajourn&eacute;e. A la date
de 2016, les tapuscrits de Michel Ferlus attendent encore leur
entr&eacute;e dans l'&egrave;re num&eacute;rique, o&ugrave; ils
rejoindront les enregistrements d&eacute;sormais mis &agrave; la
disposition de tous. Les linguistes qui souhaiteraient participer
&agrave; la poursuite de l'entreprise de recollement,
num&eacute;risation et mise en forme des collections sont
invit&eacute;s &agrave; contacter leur auteur (Michel Ferlus) et
l'&eacute;quipe de la Collection Pangloss.&nbsp;</span></p>
            <p style="text-align: justify;"><span>Les documents enregistr&eacute;s se composent
essentiellement de r&eacute;cits et de
chants et incantations. Pour donner une id&eacute;e de la richesse des
donn&eacute;es khamoues de Michel Ferlus, on ne citera ici qu'un
exemple repr&eacute;sentatif : l'<span style="font-style: italic;">histoire
de Chuang/Cheuang</span>.
(A propos de ce r&eacute;cit l&eacute;gendaire, voir : Ferlus,
Michel. 1979. "Le r&eacute;cit khamou
de Chuang et ses implications historiques pour le Nord-Laos", in Asie
du sud-est et Monde Insulindien, vol. 10, no. 2-4.) &nbsp;James
Chamberlain &eacute;crit au sujet de la version tai de ce r&eacute;cit:</span>
            <span
></span></p>
            <div style="text-align: justify;"><span
>"... le
po&egrave;me &eacute;pique de Thao Tung ou Cheuang est
peut-&ecirc;tre
la pi&egrave;ce la plus importante de toute la
litt&eacute;rature de
l'Asie du Sud-Est continentale, aussi monumentale pour
l'&eacute;tude
de la civilisation d'Asie du Sud-Est&nbsp;que l'est le Ramayana
pour
Inde ou
que l'Iliade pour l'Occident. [...] Pour les historiens, cette
&eacute;pop&eacute;e repr&eacute;sente une description
d&eacute;taill&eacute;e d'un lieu et d'une p&eacute;riode de
l'histoire
de l'Asie du Sud-Est qui nous restent globalement inconnus. Ce lieu et
cette p&eacute;riode furent tr&egrave;s probablement le site de
grands
bouleversements, lorsque les Tha&iuml; de la p&eacute;riode
pr&eacute;-bouddhiste ont migr&eacute; vers l'Ouest, depuis le
Nord du
Vietnam &agrave; travers le Laos du Nord vers le Nord de la
Tha&iuml;lande, la Birmanie, la province du Yunnan [sud de la
Chine] et
l'Assam [territoire &agrave; l'extr&ecirc;me Est de l'Inde],
subjuguant les populations austroasiatiques plus anciennement
&eacute;tablies. [...] En
substance, le r&eacute;cit de Cheuang est le texte le plus complet
existant &agrave; propos de l'ancienne civilisation tha&iuml;e,
et
repr&eacute;sente un source d'informations consid&eacute;rable
[...]." </span><span
></span><br>
            <span
></span></div>
            <div style="text-align: right;"><span
>Chamberlain,
James. 1990. "Thao Hung or Cheuang: A Tai Epic Poem" in Mon-Khmer
Studies, vol. 18-19, pp. 14-34; notre traduction.</span></div>
