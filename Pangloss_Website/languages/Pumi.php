   <p class="intertitre">Le pumi, aussi appelé prinmi, est une langue sino-tibétaine parlée dans le sud-ouest de la Chine. Les données proposées ici ont été recueillies dans la préfecture de Muli (Liangshan, province du Sichuan). Le dialecte concerné est celui du comté de Shuiluo (水洛乡) ; il se rattache à l'ensemble dialectal nord.</p>
