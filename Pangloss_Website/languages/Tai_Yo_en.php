<h1>Tai Yo, also known as Tay Yo, Th&aacute;i Mường, Th&aacute;i
Mương, and Th&aacute;i H&agrave;ng Tổng</h1>
</div>
<div class="text-left"> <a href="#Vietnamese"><span
 style="font-style: italic;">Giới
thiệu bằng tiếng Việt / Presentation in Vietnamese</span></a><br>
<br>
The Pangloss Collection hosts data from six
Tai-Kadai languages recorded by Michel
Ferlus, mostly from 1997 to 2003.&nbsp;Note that these unique data
are <span style="font-weight: bold;">working
documents</span>
collected as part
of research on Tai manuscripts and languages: they were not originally
intended for applications such as acoustic-phonetic analysis; the
recordings are of uneven technical quality. <br>
<p>About Tai Yo,
a.k.a. Tay Yo, see pp. 304-309 of:&nbsp;</p>
<div style="line-height: 1.35; padding-left: 2em; text-indent: -2em;"
 class="csl-bib-body">
<div class="csl-entry">Ferlus,
Michel. 2008. The Thai
dialects of Nghệ An, Vietnam (Tay Daeng, Tay Yo, Tay Muong). In Anthony
Diller, Jerold A. Edmondson &amp; Luo Yongxian (eds.), <i>The
Tai-Kadai languages</i>,
298&ndash;316. London &amp; New York:
Routledge.</div>
<span class="Z3988"
 title="url_ver=Z39.88-2004&amp;ctx_ver=Z39.88-2004&amp;rfr_id=info%3Asid%2Fzotero.org%3A2&amp;rft_val_fmt=info%3Aofi%2Ffmt%3Akev%3Amtx%3Abook&amp;rft.genre=bookitem&amp;rft.atitle=The%20Tai%20dialects%20of%20Ngh%E1%BB%87%20An%2C%20Vietnam%20(Tay%20Daeng%2C%20Tay%20Yo%2C%20Tay%20Muong)&amp;rft.place=London%20%26%20New%20York&amp;rft.publisher=Routledge&amp;rft.aufirst=Michel&amp;rft.aulast=Ferlus&amp;rft.au=Michel%20Ferlus&amp;rft.au=Anthony%20Diller&amp;rft.au=Jerold%20A.%20Edmondson&amp;rft.au=Luo%20Yongxian&amp;rft.date=2008&amp;rft.pages=298-316&amp;rft.spage=298&amp;rft.epage=316"></span></div>
<p> This language
variety is closely related to Tai Paw:&nbsp;diachronically, Tai Pao
and Tai Yo are considered as originating in
the same language (referred to by Michel Ferlus and other linguists as
Tai Muong). Their great proximity is reflected in the peculiar
treatment of some Proto-Southwestern Tai phonemes in both Tai Pao and
Tai Yo.</p>
<p><span style="font-weight: bold;">Tay Yo as the outcome of an early
population movement from China?</span></p>
<p style="text-align: justify;">"The Tay Yo lexicon contains words
whose pronunciation is not in keeping with that of other dialects of
Tai [roughly corresponding to Southwestern Tai in Li Fang-kuei's
classification]: there are differences in terms of consonants, vowels,
or tones. (...) Some of these words, such as&nbsp;fiː<sup>A2</sup>
&lsquo;fire&rsquo; and&nbsp;lɨk<sup>DS2</sup> &lsquo;son, child&rsquo;,
are well-attested in the Yaay branch. J.R. Chamberlain (1991) noticed
these issues, and concluded, somewhat hastily, that Tay Maen should be
classified as part of Northern Tai. Against this view, one must raise
the objection that, apart from a short list of words, the Tay Yo
lexicon does fall squarely in the [Southwestern] Tai branch: the sound
correspondences are regular. The evolution of the velar final,&nbsp;*x
&gt;h, is shared by Tay Yo and Yaay languages, but does not constitute
evidence of close phylogenetic relationship because the list of words
that underwent this change is not the same in the two branches. Still,
the atypical lexical items found in Tay Yo raise a real issue, which
one must try to elucidate. Interestingly, Weera Ostapirat (1998)
brought out in the Chinese dialect of&nbsp;J&iacute;zh&agrave;o, on the
Southwest coast of Guangdong, a layer of vocabulary that is cognate
with Tai-Kadai, and specifically with Hainan B&ecirc;. Among the
seventeen examples provided (reproduced here in Ostapirat's notation),
one finds&nbsp;vi&sup3;&sup1; (A2)
&lsquo;fire&rsquo;, nam&sup1;&sup1; (C2) &lsquo;water&rsquo;,
man&sup3;&sup1; (A2)
&lsquo;he, she&rsquo;, and most tellingly ky&sup1;&sup1; (C2)
&lsquo;to eat&rsquo;, a form which it is highly tempting to compare
with Tay Yo kiː⁵⁵ (A2) despite the differences in vowel and tone. It is
likely that the Tai-B&ecirc; substratum in the Chinese dialect
of&nbsp;J&iacute;zh&agrave;o
is a trace of a language (now extinct) that used to constitute a link
between the Tai-Yaay languages of Guangxi and Hainan B&ecirc;. The
geographical location of&nbsp;J&iacute;zh&agrave;o is eminently
compatible with this hypothesis.&nbsp;In this perspective, how should
one explain the presence of a similar stratum in Tay Yo, a language
whose current location is not part of the geographical continuum
linking the Tai-Yaay area with that of B&ecirc;? The hypothesis
proposed by Chamberlain (1991: 120) is that the Yaay branch (Northern
Tai) was once spread over the entire area from Guizhou to Thanh
H&oacute;a and&nbsp;Nghệ An. This is admittedly a possibility, but a
drawback of this proposal of a considerably large Yaay area is that it
spirits away the Viet-Muong (Vietic) languages, which are firmly
implanted in the northern part of Vietnam and whose cradle should
accordingly be located somewhere within this area. As for me, I think
that this is a case of early population displacement. Many such events
took place at the time of Chinese domination: the central power set in
place populations that were loyal to it, in order to control the
country better. Later on, the displaced language must have been covered
up by the language of some of the Tai who came over from Muong Theng,
giving birth to the language that would become Tay Yo (and, more
broadly, Tay Muong), only leaving a limited substratum. The other part
of the Tai expansion from Muong Theng must be supposed to be the origin
of present-day Tay Daeng."&nbsp;(Ferlus 2008: 308-309)</p>
<strong>The
archived documents: two lexical surveys, and&nbsp;read manuscripts
(c. 1997-2004); and folklore and vocabulary recorded in 2014</strong>
<p>The recordings made
available here were recorded from 1997
to 2004; an extra set of
recordings was conducted in 2014. For information on the
speakers, see the metadata accompanying each document.&nbsp; </p>
<span style="font-weight: bold;">Vocabulary</span>
was elicited by means of the EFEO-CNRS-SOAS
Word List for Linguistic Fieldwork in
Southeast Asia, available <a
 href="https://halshs.archives-ouvertes.fr/halshs-01068533/"
 target="_blank">here</a>.
<br>
<br>
Some of the Tai <span style="font-weight: bold;">read
texts</span> are
from&nbsp;European libraries, others were investigated locally,
making photographs of the originals, and collecting oral renderings by
Tai consultants. Specifically, (i) Tay Yo manuscripts are from the
library of <a href="http://www.efeo.fr/"><span
 style="font-style: italic;">Ecole
Fran&ccedil;aise d'Extr&ecirc;me-Orient</span></a>
in Paris ("Tha&iuml; 8", "Tha&iuml; 9" and "Tha&iuml; 10"
manuscripts of the EFEO catalogue) and from the Maspero collection of
the <a href="http://www.aibl.fr/societe-asiatique/" target="_blank"><span
 style="font-style: italic;">Soci&eacute;t&eacute;
Asiatique</span></a>;
(ii) Lai Pao manuscripts are from Michel Ferlus's fieldwork with Pr.
Trần Tr&iacute; D&otilde;i (Vietnam National University,
Hanoi); (iii) Tay D&egrave;ng manuscripts are from the <a
 href="http://anom.archivesnationales.culture.gouv.fr/" target="_blank">Archives
d'Outre-Mer</a>
in Aix-en-Provence and from fieldwork; (iv) Tai Don texts are from
fieldwork and from Minot (1949); (v) the Kh&uuml;n (Kh&oelig;n)
manuscript was
collected during fieldwork. Further detail is provided in each
document's metadata. In due course, we
would like to make the manuscrits available in this archive, together
with
time-aligned annotations and translations. <br>
