       <p class="intertitre">Pumi, also called Prinmi, is a Sino-Tibetan language spoken in Southwest China. The data proposed for consultation were collected in the prefecture of Muli (Liangshan, Sichuan province). The dialect concerned is that spoken in the township of Shuiluo (水洛乡); it belongs to the Northern dialect group.</p>
