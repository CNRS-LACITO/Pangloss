<h3><a name="tones_nouns"></a>The
lexical tones of nouns</h3>
<p>The complexities of the Yongning Na tone
system make it necessary
to
use
two nonstandard symbols in addition to the International Phonetic
Alphabet 'tone-letters': </p>
<ul>
  <li>the pound symbol # stands for the last syllable of
the
lexical word: thus #H is a High tone that can only be realized after
the last syllable of the word to which it is lexically attached. This
tone therefore does not surface when a word is spoken in isolation.<br>
  </li>
  <li>the dollar symbol $ is used in the expression H$, which refers to
a lexical tone category distinct from both H# (a final H tone) and #H
(a floating H tone). </li>
</ul>
