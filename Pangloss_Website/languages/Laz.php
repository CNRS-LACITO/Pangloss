      <p>Un homme interroge  sa femme pour savoir à quel point elle l'aime. Sa femme lui répond : &quot;Autant que le vent frais, par un jour de grande chaleur&quot;. Déçu de cette réponse, l'homme tue sa femme. C'était un jour de grande chaleur. Alors qu'un vent frais se met à souffler, l'homme réalise à quel point sa femme l'aimait et il se tue lui-même.</p>
