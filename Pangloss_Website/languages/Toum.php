15-16 de: Ferlus, Michel.
1996. <a href="http://sealang.net/sala/archives/pdf8/ferlus1996langues.pdf" target="_blank">Langues et peuples viet-muong</a>. <span style="font-style: italic;">Mon-Khmer
Studies</span> 26. 7–28.)</p>
