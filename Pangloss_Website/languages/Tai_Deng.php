<h1>Tai Deng (Tay Deng, Tai Daeng, Th&aacute;i Thanh)</h1>
</div>
<div class="text-left"> <a href="Tai_en.htm#Vietnamese"><span
 style="font-style: italic;">Giới
thiệu bằng tiếng Việt / Pr&eacute;sentation en vietnamien</span></a><br>
<br>
<div style="text-align: justify;">
La Collection Pangloss accueille des donn&eacute;es de six langues
tai-kadai collect&eacute;es par Michel Ferlus, essentiellement
entre 1997 et 2003. Il s'agit de documents de travail, recueillis au
fil de recherches qui concernaient essentiellement les
&eacute;critures et les syst&egrave;mes phonologiques: ces
enregistrements n'&eacute;taient pas destin&eacute;s
&agrave; l'origine &agrave; des applications telles qu'une
analyse de phon&eacute;tique acoustique. La qualit&eacute;
technique des enregistrements est in&eacute;gale. <br>
<p>Le tai paw (tay paw) est
tr&egrave;s proche du tai yo, ou tay yo. Le tai yo et le
tai paw sont consid&eacute;r&eacute;s comme deux dialectes
d'une m&ecirc;me langue (d&eacute;sign&eacute;e par le
terme de&nbsp;Tai Muong; en vietnamien: Th&aacute;i Mương). La
grande proximit&eacute; entre tai yo et tai paw se manifeste en
particulier dans le traitement original de certains phon&egrave;mes
du proto-tai en tai yo comme en tai paw.</p>
<strong></strong>"Les Tay Daeng [dɛːŋᴬ&sup1;] (&eacute;crit aussi
D&egrave;ng) sont appel&eacute;s Tay Thanh ou Man Thanh par les Viet.
Chez les auteurs occidentaux, ils sont souvent d&eacute;nomm&eacute;s
Tai/Tay rouges d'apr&egrave;s la signification du mot
&lsquo;daeng&rsquo; et ce nom leur est encore fr&eacute;quemment
attribu&eacute; dans la litt&eacute;rature. Toutefois, selon
l'administrateur R. Robert (1941) qui leur a r&eacute;serv&eacute; une
excellente et c&eacute;l&egrave;bre description ethnographique, leur
ethnonyme proviendrait de Muong Daeng (aujourd'hui Y&ecirc;n Khương sur
le haut S&ocirc;ng M&atilde;), province de Thanh H&oacute;a,
d'o&ugrave; ils sont originaires et qui reste leur r&eacute;gion par
excellence. Nous pensons qu'il faut retenir cette explication et
proscrire l'appellation de Tay rouge dans toutes les langues. Le terme <span
 style="font-style: italic;">thanh (Th&aacute;i Thanh)</span>, plus
particuli&egrave;rement utilis&eacute; au Nghệ An, est la forme
vietnamis&eacute;e de theng [tʰɛːŋᴬ&sup1;], du nom de l'ancien Muong
Theng des r&eacute;cits historiques, localis&eacute; dans la plaine de
Điện Bi&ecirc;n et consid&eacute;r&eacute; comme le berceau de la
plupart des populations Thai du Nord-Laos et surtout du Nord-Vietnam.
Dans les si&egrave;cles pass&eacute;s, une forte expansion des Tay
Daeng les a conduit dans la province de Sam Neua (Laos), o&ugrave; ils
constituent aujourd'hui le fond de la population, et dans les districts
de l'ouest du Nghệ An. Ils sont parfois nomm&eacute;s Tay Meuy
[məːjC&sup1;] (&eacute;crit aussi Moey) dans la langue de leurs voisins
Tay Yo o&ugrave; ce terme signifie &lsquo;groupe, gens&rsquo;, et c'est
par cet ethnonyme qu'ils sont &eacute;galement connus au Laos." (Ferlus
2008:
299)&nbsp;</div>
<span style="font-weight: bold;"></span></div>
<p>Pour conna&icirc;tre les
noms des locuteurs, les lieux pr&eacute;cis d'enqu&ecirc;te et
autres informations, voir les m&eacute;tadonn&eacute;es qui
accompagnent chacun des documents.&nbsp; </p>
Le vocabulaire
a &eacute;t&eacute; &eacute;licit&eacute; au moyen de
la liste d'enqu&ecirc;te EFEO-CNRS-SOAS, disponible&nbsp;<a
 href="https://halshs.archives-ouvertes.fr/halshs-01068533/"
 target="_blank">ici</a>.
<br>
<br>
<div style="text-align: justify;">Certains des manuscrits lus
proviennent de biblioth&egrave;ques
europ&eacute;ennes; d'autres ont &eacute;t&eacute;
&eacute;tudi&eacute;s sur place, en r&eacute;alisant des
photos des originaux et des enregistrements audio de lectures par des
informateurs (consultants) tai. Les manuscrits tay d&egrave;ng
proviennent des&nbsp;<a
 href="http://anom.archivesnationales.culture.gouv.fr/" target="_blank">Archives
d'Outre-Mer</a> &agrave; Aix-en-Provence, et des enqu&ecirc;tes de
terrain de Michel Ferlus. Un d&eacute;veloppement informatique est
actuellement en cours
(2017) pour disposer d'outils permettant de rendre les
enregistrements consultables avec un alignement temporel avec les
manuscrits et leur annotation. Le
d&eacute;veloppement informatique, initialement con&ccedil;u et
assur&eacute; par Matthew Deo (initiateur du projet documentaire '<a
 href="http://moon-light.fr/" target="_blank">Moon-light</a>'),
va &ecirc;tre repris&nbsp;&agrave; partir de l'automne 2017
par <a href="http://lacito.vjf.cnrs.fr/membres/guillaume.htm"
 target="_blank">S&eacute;verine Guillaume</a>.
