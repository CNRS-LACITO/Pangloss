            <p class="intertitre" align="left"></p>
            <p class="intertitre" align="left">Presentation of the
language by <a href="https://cnrs.academia.edu/GuillaumeJacques"
 target="_blank">Guillaume
Jacques</a>:&nbsp;</p>
            <p style="text-align: justify;" class="intertitre">"Japhug
(Chabao 茶堡) is a Rgyalrong language spoken in the north-east of
Mbarkhams/Maerkang 马尔康 county (Rnga.ba district). (...) In spite of
being genetically related to Chinese, the Rgyalrong languages are
typologically quite divergent from the isolating type. They are the
only fully polysynthetic languages in China, or even in the entire Asia
if we except isolated languages of the sub-polar and polar regions such
as Ket, Ainu or Chukchi. These languages are of exceptional interest
for both linguistic typology and historical linguistics.&nbsp;</p>
            <p style="text-align: justify;" class="intertitre">Unlike
modern Chinese languages which have a very simple syllable structure
with few or no initial clusters, Rgyalrong languages have the most
complex syllabic structure of all Sino-Tibetan languages. Rgyalrong
languages typically have large consonant inventories; for instance,
Japhug has a consonantal system with 49 phonemes. (...) More than 340
consonant clusters are attested in Japhug, and including groups with up
to three or even four consonants. Some of the complex clusters of Old
Tibetan which are preserved in none of the attested modern Tibetan
languages are still present in Rgyalrong. For instance, Old Tibetan <span
 style="font-style: italic;">bsgyur(d)</span> &lsquo;to change (past
stem)&rsquo; appears as <span style="font-weight: bold;">&beta;zɟɯr</span>
in Japhug. (...) Japhug has an intriguing five-way contrast between
/velar stop+j/ as in <span style="font-weight: bold;">kjo</span>
&lsquo;to cause to glide&rsquo;, palatal stops as in <span
 style="font-weight: bold;">co</span> &lsquo;valley&rsquo;, /dental
affricates+j/ as in <span style="font-weight: bold;">ɯ-mtsjoʁ</span>
&lsquo;beak&rsquo;, alveolo-palatal affricates as in <span
 style="font-weight: bold;">tɕoʁtsi</span> &lsquo;table&rsquo; and
/uvular stop+j/ as in <span style="font-weight: bold;">qjoʁ</span>
&lsquo;to vomit&rsquo;. Palatal stops can be followed by -ɣ-, -r- or
-l- as in <span style="font-weight: bold;">ɲcʰɣaʁ</span> &lsquo;birch
bark&rsquo;. (...)</p>
            <p style="text-align: justify;" class="intertitre">Rgyalrong
languages have a complex verbal morphology, unlike anything else found
in the Sino-Tibetan family. They present all the characteristics of
polysynthetic languages: 1) A high word to morpheme ratio (most verbal
forms include at least three morphemes, and can have up to eight). 2)
Head-marking typology (grammatical<br>
relationship marked mainly on the verb and within the noun phrase,
possession marked on the possessed rather than on the possessor),
verbal agreement with two arguments. 3) Presence of nominal
incorporation. (...)</p>
            <p style="text-align: justify;" class="intertitre">In
derivational morphology, Rgyalrong languages preserve many affixes and
morphological processes which may be reconstructible to
proto-Sino-Tibetan, such as the causative <span
 style="font-weight: bold;">sɯ-</span>, <span
 style="font-weight: bold;">sɯɣ-</span>, <span
 style="font-weight: bold;">z-</span> (it has three regular and four
irregular allomorphs), the anticausative prenasalizations and the
oblique nominalization prefix <span style="font-weight: bold;">sɤ-</span>,
and others which are not accepted by all scholars such as agent or
patient nominalization kɯ-/kɤ-, action noun nominalization in tɯ-,
passive a- &lt; *ŋa- etc.</p>
            <p style="text-align: justify;" class="intertitre">In
inflexional morphology, Rgyalrong languages constitute an important
piece of evidence in the debate regarding the antiquity of verbal
agreement in the Sino-Tibetan<br>
family. The Rgyalrong agreement system presents many similarities with
Kiranti, a group of Sino-Tibetan languages spoken in Nepal, and most
scholars accept the fact that at least some of the verbal morphology
found in Rgyalrong and Kiranti is cognate.</p>
            <p style="text-align: justify;" class="intertitre">Given
the considerable interest of these languages for both typology and
comparative linguistics, it is surprising that so few scholars engage
in the description of these highly endangered languages, whose
importance for the study of the Sino-Tibetan family as a whole can be
aptly compared to the role of Sanskrit in Indo-European. They open
radically new perspectives for Old Chinese historical phonology and
morphology." <br>
            </p>
