<h1>Tai Paw (Tay Pao, Tai Pao)</h1>
</div>
<div class="text-left"> <a href="Tai_en.htm#Vietnamese"><span
 style="font-style: italic;">Giới
thiệu bằng tiếng Việt / Presentation in Vietnamese</span></a><br>
<br>
The Pangloss Collection hosts data from six
Tai-Kadai languages recorded by Michel
Ferlus, mostly from 1997 to 2003.&nbsp;Note that these unique data
are <span style="font-weight: bold;">working
documents</span>
collected as part
of research on Tai manuscripts and languages: they were not originally
intended for applications such as acoustic-phonetic analysis; the
recordings are of uneven technical quality. <br>
<br>
<div style="text-align: justify;">"The Tay Muong [mɨəŋᴬ&sup2;], also
known as Tay Pao [paːwC&sup1;]
after the Thai name (Nam Pao) of the&nbsp;S&ocirc;ng Cả river, are
mainly located in the&nbsp;district of Tương Dương,
whose former&nbsp;<span style="font-style: italic;">chef-lieu</span>
was Cửa R&agrave;o, where the Nam Neun [nənᴬ&sup2;] and Nam M&ocirc;
[moːC&sup2;] rivers join to form the S&ocirc;ng Cả. The Tay Pao are
referred to as Th&aacute;i
H&agrave;ng Tổng by the Vietnamese. They were first signalled by Albert
Louppe (1934), who provided an ethnographic description. The same
ethnonym can also cover speakers of Tay Maen or Tay Yo (but not
speakers of Tay Thanh, i.e. Tai Deng)
located in the districts of the S&ocirc;ng Cả river basin. Tay Pao has
never been described, any more than Tay Yo.<br>
</div>
In the course of their migrations towards other provinces and
neighbouring countries, these populations occasionally received other
ethnonyms. Thus, the
Phou Thai of the province of Savannakhet (Laos) originate in the Tay
Muong and Tay Yo areas of Nghệ An." (Ferlus 2008:
299-300)&nbsp;<br>
<br>
<strong>The
archived documents: lexical surveys and&nbsp;read manuscripts
(c. 1997-2004)</strong>
<p>Concerning the
speakers, date of recording and other pieces of information, see the
metadata accompanying each document.&nbsp; </p>
<span style="font-weight: bold;">Vocabulary</span>
was elicited by means of the EFEO-CNRS-SOAS
Word List for Linguistic Fieldwork in
Southeast Asia, available <a
 href="https://halshs.archives-ouvertes.fr/halshs-01068533/"
 target="_blank">here</a>.
<br>
<br>
Some of the Tai <span style="font-weight: bold;">read
texts</span> are
from&nbsp;European libraries, others were investigated locally,
making photographs of the originals, and collecting oral renderings by
Tai consultants. Specifically, Lai Pao manuscripts are from Michel
Ferlus's fieldwork with Pr.
Trần Tr&iacute; D&otilde;i (Vietnam National University,
Hanoi), as explained in <a
 href="https://www.youtube.com/watch?v=DY9aCMziuZ0" target="_blank">a
documentary film (in Vietnamese)</a>. Work is currently under way
(August-December 2017) to make the manuscrits available in this
archive, together
with
time-aligned annotations and translations. <br>
<br>
Tai Paw (Tay Pao, Tai Pao) is closely related to Tai
Yo:&nbsp;diachronically,
Tai Pao and Tai Yo are considered as originating in the same language
(referred to by Michel Ferlus and other linguists as Tai Muong). Their
great proximity is reflected in the peculiar treatment of some
Proto-Southwestern Tai phonemes in both Tai Pao and Tai Yo.<br>
