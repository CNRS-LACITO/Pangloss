      <p align="left">&nbsp;</p>
      <p>Ihanzu is most probably a dialect of the Nyilamba language. It is spoken in the northern part of the Iambi area, in central Tanzania. According to local informants, the speakers of Ihanzu are the hunter clan of the Nyilamba group.</p>
