            <p class="intertitre" align="left">La langue arem
appartient au groupe
vietique de la famille austroasiatique.&nbsp;</p>
            <p class="intertitre" align="left">"Les&nbsp;<span
 style="font-style: italic;">Arem</span>
sont la derni&egrave;re des ethnies viet-muong &agrave; avoir
&eacute;t&eacute; reconnue par les sp&eacute;cialistes
(Vương Ho&agrave;ng Tuy&ecirc;n 1963). Ils ne semblent pas
avoir d'autonyme d'origine; <span style="font-style: italic;">Arem</span>,
dont la prononciation varie de [<span style="font-weight: bold;">arɛːm</span>]
sur le mod&egrave;le du viet &agrave; [<span style="font-weight: bold;">areːm</span>]
dans leur propre langue, est probablement un mot d'origine
s&ocirc;-bru. On le retrouve cit&eacute; par M.
Rivi&egrave;re (1902) sous la graphie <span style="font-style: italic;">Har&egrave;me</span>
mais pour d&eacute;signer des <a href="Maleng.htm" target="_blank"><span
 style="font-style: italic;">Maleng</span></a>.
Il est probable que ce terme s'appliquait autrefois aux semi-nomades
forestiers, un peu comme <span style="font-style: italic;">Kha Tong
Luang</span> en lao. Les <span style="font-style: italic;">Arem</span>
se disent &eacute;galement <span style="font-style: italic;">Chomrau/Chombrau</span>
[<span style="font-weight: bold;">cmraw/cmbraw</span>] mais la
structure de ce mot, inhabituelle dans le syst&egrave;me
phon&eacute;tique de leur langue, montre qu'il s'agit d'un emprunt.
L'ethnonyme <span style="font-style: italic;">Umo</span> (Đặng
Nghi&ecirc;m Vạn
et al. 1986) doit s'appliquer aux <span style="font-style: italic;">Arem</span>
puisque ce mot signifie dans leur langue "grotte, trou" donc un lieu
possible d'habitation.&nbsp;</p>
            <p class="intertitre" align="left">Depuis leur
d&eacute;couverte dans les ann&eacute;es soixante par les chercheurs
vietnamiens dans les montagnes du Quảng B&igrave;nh, les&nbsp;<span
 style="font-style: italic;">Arem</span> sont en constante
r&eacute;gression d&eacute;mographique &agrave; cause des effets de la
guerre et des conditions d'existence. Actuellement, ils ne sont qu'un
peu plus d'une centaine mais, si l'on exclut les &eacute;l&eacute;ments
exog&egrave;nes et les enfants, il ne reste qu'une vingtaine d'adultes
qui parlent leur langue, d'ailleurs de plus en plus influenc&eacute;e
par le vietnamien. Autrefois semi-nomades forestiers vivant de grottes
en abris, ils ont &eacute;t&eacute; fix&eacute;s pr&eacute;cairement et
les tentatives pour les amener au jardinage ne sont gu&egrave;re
concluantes."&nbsp;</p>
            <p style="text-align: right;" class="intertitre">(Page 13
de: Ferlus, Michel.
1996. <a
