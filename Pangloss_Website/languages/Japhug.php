:
&nbsp;</p>
            <p class="intertitre" align="left">"La langue japhug est
parl&eacute;e dans la partie tib&eacute;taine du Sichuan (par
moins de dix mille personnes); j'&eacute;tudie
la vari&eacute;t&eacute; du
village
de Kamnyu, situ&eacute; ici:</p>
