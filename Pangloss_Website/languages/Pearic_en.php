            <h2><a name="languages"></a>1.
About the Pearic languages</h2>
            <p style="text-align: justify;">The
Pear&nbsp;people consists of several ethnic groups: the Pear or Por,
the Samr&eacute;, the Samray, the Suoy and the
Chong,
located in&nbsp;western Cambodia and southeastern
Thailand.&nbsp;Pearic languages&nbsp;are a group of the
Mon-Khmer branch
of the Austroasiatic family.</p>
            <p style="text-align: justify;">Sidwell (2009) offers a
classification of Pearic
languages in two main branches:&nbsp;Pear and&nbsp;Chong.
Chong can be further subdivided in four groups.</p>
            <p style="margin-left: 40px;">-&nbsp;<span
 style="font-weight: bold;">Pear</span>
of Kampong Thom Province, Cambodia (Baradat 1941)</p>
            <p style="margin-left: 40px;">-
            <span style="font-weight: bold;">Chong</span></p>
            <p style="margin-left: 80px;">-
            <span style="font-style: italic; font-weight: bold;">Southern
Chong</span></p>
            <p style="margin-left: 120px;"><span
 style="font-style: italic;">- Suoi</span>
of Kampong Speu Province, Cambodia (Pannetier m.s.; Baradat 1941)</p>
            <p style="margin-left: 120px;">-
            <span style="font-style: italic;">Saoch</span></p>
            <p style="margin-left: 160px;">-
Chung of Cambodia, spoken around Phum Veal Renh, Prey Nob District in
Kampong Som/Sihanoukville (Pannetier 1941; Choosri 2007)</p>
            <p style="margin-left: 160px;">-
Chung of Thailand, spoken in Kanchaburi Province (Choosri 2007)</p>
            <p style="margin-left: 80px;">-
            <span style="font-style: italic; font-weight: bold;">Western
Chong (Chong)</span></p>
            <p style="margin-left: 120px;"><span
 style="font-style: italic;">- Chong</span>
of Chanthaburi Province, Thailand (Baradat 1941)</p>
            <p style="margin-left: 120px;">-
Other branch:</p>
            <p style="margin-left: 160px;">-
Chong <span
 style="font-size: 11pt; line-height: 115%; font-family: &quot;Calibri&quot;,&quot;sans-serif&quot;;">həəp</span>
(Martin 1974)</p>
            <p style="margin-left: 160px;">-
Khlong Phlu Chong (Ungsitibonporn 2001)</p>
            <p style="margin-left: 120px;">-
Other branch:</p>
            <p style="margin-left: 160px;">-
Chong <span
 style="font-size: 11pt; line-height: 115%; font-family: &quot;Calibri&quot;,&quot;sans-serif&quot;;">lɔɔ
            </span>(Martin 1974)</p>
            <p style="margin-left: 160px;">-
Wang Kraphrae Chong (Ungsitibonporn 2001)</p>
            <p style="margin-left: 160px;">-
Chong (Huffman 1985)</p>
            <p style="margin-left: 80px;">-
            <span style="font-style: italic; font-weight: bold;">Central
Chong (Samre)</span></p>
            <p style="margin-left: 120px;"><span
 style="font-style: italic;">- Samre</span>
of Pursat Province, Cambodia</p>
            <p style="margin-left: 120px;">-
            <span style="font-style: italic;">Samre</span>
(Ploykaew 2001a, 2001b)</p>
            <p style="margin-left: 120px;">-
            <span style="font-style: italic;">Chong</span>
(Baradat 1941)</p>
            <p style="margin-left: 120px;">-
            <span style="font-style: italic;">Kasong</span>,
spoken in Bo Rai District, Trat Province, Thailand (Sunee 2002;
Thongkham 2003) also known as Chong of Trat (Pannetier m.s.; Isarangura
1935)</p>
            <p style="margin-left: 80px;">-
            <span style="font-style: italic; font-weight: bold;">Northern
Chong (Somray)</span></p>
            <p style="margin-left: 120px;"><span
 style="font-style: italic;">- Somray</span>
of Battambang, Cambodia (Baradat 1941)</p>
            <p style="margin-left: 120px;">-
            <span style="font-style: italic;">Somre</span>
of Siem Reap, Cambodia which is a dead language (Moura 1883)</p>
            <p></p>
            <p style="text-align: justify;">The phonology of
Pearic languages presents a specific and
unusual four-way contrast
in register (phonation types) in their vowel systems. These
systems&nbsp;combine the modal feature /clear/ with the marked
features
/breathy/ and /creaky/, yielding&nbsp;four syllabic combinations:
clear,
breathy, creaky, breathy-creaky.&nbsp;Pearic languages
are&nbsp;important for proto-Mon-Khmer
reconstruction. There are strong indications that these languages
are
more closely related to the Northern Mon-Khmer languages than those
of Cambodia and its immediate surroundings; for hypotheses about
propagation along ancient trade routes across Southeast Asia, see
Ferlus (1977,
2011).</p>
