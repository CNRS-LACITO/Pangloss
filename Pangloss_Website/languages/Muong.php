            <p class="intertitre" align="left">"Les <span
 style="font-style: italic;">Mường</span>
sont une des plus importantes
ethnies minoritaires du Vietnam. Il n'y a pas &agrave; proprement
parler une langue mường, mais plut&ocirc;t un ensemble de dialectes
dont chacun est nomm&eacute; par sa localisation. Les
premi&egrave;res
r&eacute;v&eacute;lations sur des parlers mường sont dues
&agrave;
Ch&eacute;on (1905 et 1907) et Cadi&egrave;re (1905), pour ne
citer que
les devanciers. On se doit de mentionner les remarquables travaux
ethnographiques de J. Cuisinier (1948, 1951) qui restent, aujourd'hui
encore et malgr&eacute; des &eacute;tudes plus
r&eacute;centes, des
r&eacute;f&eacute;rences in&eacute;gal&eacute;es.
Depuis la fin de la
domination chinoise au Xe si&egrave;cle, les Mường sont soumis
&agrave;
l'influence constante du vietnamien ce qui ne manque pas de poser des
probl&egrave;mes au comparatisme linguistique. Le terme <span
 style="font-style: italic;">mường</span>
serait d'origine thay/tay et
signifierait "principaut&eacute;", sens qu'il a
pr&eacute;serv&eacute;
dans ces langues. Les Mường seraient donc les "gens des
principaut&eacute;s", c'est &agrave; dire les "provinciaux" par
rapport
&agrave; un pouvoir central, ce qui sous-entend qu'ils ont
&eacute;t&eacute;, &agrave; un moment de leur histoire,
organis&eacute;s dans des structures &eacute;tatiques tay ou de
type
tay. Leur v&eacute;ritable autonyme est <span
 style="font-style: italic;">Mol</span>
[<span style="font-weight: bold;">mɔːl⁴</span>]
qui, selon les dialectes,
peut prendre la forme de <span style="font-style: italic;">Mon</span>
ou de <span style="font-style: italic;">Mual</span>.
Leur habitat
couvre plusieurs provinces depuis Y&ecirc;n B&aacute;i et Sơn
La
jusqu'au Thanh H&oacute;a et ils ont pour voisins les Tay (ou
Th&aacute;i) &agrave; l'ouest et les Viet &agrave; l'est.
Les <span style="font-style: italic;">Nguồn</span>,
"ceux d'amont", ainsi
nomm&eacute;s parce qu'ils occupent le fond de certaines
vall&eacute;es
au Quảng B&igrave;nh, seraient les descendants de soldats
originaires
du Thanh H&oacute;a install&eacute;s dans la r&eacute;gion
vers le
XVIIe si&egrave;cle pour contenir les tribus belliqueuses
autochtones,
probablement les anc&ecirc;tres des <span style="font-style: italic;">S&aacute;ch</span>.
Ils ne semblent pas avoir retenu les autonymes en usage chez leurs
cousins du nord.</p>
            <p class="intertitre" align="left">En raison de
l'&eacute;tendue de leur habitat, le mode de vie des <span
 style="font-style: italic;">Mường </span>et
des <span style="font-style: italic;">Nguồn</span>
pr&eacute;sente un large
&eacute;ventail. Certains pratiquent la riziculture
irrigu&eacute;e,
comme les Viets, tandis que d'autres ont un mode de vie de type
montagnard. En rectifiant les anciennes donn&eacute;es
d&eacute;mographiques, on peut aujourd'hui les estimer &agrave;
un peu
plus de 700.000 locuteurs."&nbsp;</p>
