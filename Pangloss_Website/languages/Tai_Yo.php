<h1>Tai Yo (parler aussi appel&eacute; Tay
Yo, Th&aacute;i Mường, Th&aacute;i Mương, ou Th&aacute;i
H&agrave;ng Tổng)</h1>
</div>
<div class="text-left"> <a href="#Vietnamese"><span
 style="font-style: italic;">Giới
thiệu bằng tiếng Việt / Pr&eacute;sentation en vietnamien</span></a><br>
<br>
<div style="text-align: justify;">
La Collection Pangloss accueille des donn&eacute;es de six langues
tai-kadai collect&eacute;es par Michel Ferlus, essentiellement
entre 1997 et 2003. Il s'agit de documents de travail, recueillis au
fil de recherches qui concernaient essentiellement les
&eacute;critures et les syst&egrave;mes phonologiques: ces
enregistrements n'&eacute;taient pas destin&eacute;s
&agrave; l'origine &agrave; des applications telles qu'une
analyse de phon&eacute;tique acoustique. La qualit&eacute;
technique des enregistrements est in&eacute;gale. <br>
<p>Le tai yo, ou tay yo, est
tr&egrave;s proche du tai paw (tai pao, tay paw). Le tai yo et le
tai paw sont consid&eacute;r&eacute;s comme deux dialectes
d'une m&ecirc;me langue (d&eacute;sign&eacute;e par le
terme de&nbsp;Tai Muong; en vietnamien: Th&aacute;i Mương). La
grande proximit&eacute; entre tai yo et tai paw se manifeste en
particulier dans le traitement original de certains phon&egrave;mes
du proto-tai en tai yo comme en tai paw.</p>
<strong></strong>"Le
tay yo est principalement
parl&eacute; dans les trois districts de Quỳ Hợp, Quỳ
Ch&acirc;u et Quế Phong de la province de Nghệ An. On ne dispose
pas de publication sur ce dialecte in situ. La tr&egrave;s
int&eacute;ressante &eacute;tude de J.R. Chamberlain (1991) sur
le tay maen (m&egrave;ne) du Laos concerne un dialecte originaire
du district de Tương Dương et est malheureusement assez
influenc&eacute; par le lao. On a pu profiter des notes
in&eacute;dites laiss&eacute;es par Henri Maspero (Ferlus 1993)
qui a travaill&eacute; dans la r&eacute;gion vers 1920. Nos
informateurs ont &eacute;t&eacute; MM. Lang Vǎn O et Lang Vǎn
Do&atilde;n son fils, M. Vi Ngọc Ch&acirc;n et Mme Lang Thị
Hồng, tous des environs de Quỳ Ch&acirc;u.&nbsp;L'influence du
vietnamien se
fait fortement sentir sur la prononciation du tay yo. L'initiale
palatale j est fr&eacute;quemment prononc&eacute;e z. Les
finales palatales c-ɲ se sont d&eacute;velopp&eacute;es sous
l'influence des finales <span style="font-style: italic;">ch-nh</span>
du viet.<br>
</div>
<p>
Le tay yo (et aussi le tay muong) se singularise par une
&eacute;volution originale des fricatives v&eacute;laires
initiales du proto thai: *x &gt;h et *ɣ &gt;k." (Ferlus 2008:
304-305)&nbsp;</p>
<span style="font-weight: bold;">Le
tay yo, fruit d'un d&eacute;placement de population ancien depuis
la Chine?</span>
<p>"Le vocabulaire tay yo contient
des mots dont le phon&eacute;tisme est en disharmonie avec celui
des autres langues thai, la diff&eacute;rence pouvant porter sur le
consonantisme, le vocalisme ou le ton. (...) Quelques uns de ces mots,
comme fiː<sup>A2</sup>
&lsquo;feu&rsquo; et lɨk<sup>DS2</sup>
&lsquo;fils, enfant&rsquo;, sont
r&eacute;guli&egrave;rement attest&eacute;s dans la branche
yaay. J.R. Chamberlain (1991),
&agrave; qui ces probl&egrave;mes n'ont pas
&eacute;chapp&eacute;, en a conclu un peu h&acirc;tivement
&agrave; propos du tay maen que cette langue devait &ecirc;tre
class&eacute;e dans le northern tai. Il faut objecter que, mis
&agrave; part une courte liste de mots, le vocabulaire du tay yo
rel&egrave;ve bien de la branche thai selon
des correspondances r&eacute;guli&egrave;res.
L'&eacute;volution de la fricative v&eacute;laire *x &gt;h,
commune au tay yo et aux langues yaay, n'est pas la preuve d'une
parent&eacute; proche car la liste des mots attestant
ce changement n'est pas la m&ecirc;me dans les deux branches.
Toutefois, le vocabulaire atypique contenu dans
le tay yo pose un r&eacute;el probl&egrave;me qu'il faut tenter
d'&eacute;lucider.
R&eacute;cemment, Weera Ostapirat (1998) a mis en
&eacute;vidence dans le dialecte chinois de
J&iacute;zh&agrave;o, sur la
c&ocirc;te sud-ouest du Guangdong, une strate de vocabulaire
apparent&eacute;e au tai en
g&eacute;n&eacute;ral et plus particuli&egrave;rement au
b&ecirc; de Hainan. Parmi les dix-sept mots qui nous sont
donn&eacute;s, on rel&egrave;ve
(notations de l'auteur) vi&sup3;&sup1; (A2)
&lsquo;feu&rsquo;, nam&sup1;&sup1; (C2)
&lsquo;eau&rsquo;, man&sup3;&sup1; (A2)
&lsquo;il, elle&rsquo;, et surtout ky&sup1;&sup1; (C2)
&lsquo;manger&rsquo;, forme que l'on ne
peut&nbsp;s'emp&ecirc;cher de comparer au tay yo kiː⁵⁵ (A2) en
d&eacute;pit des diff&eacute;rences de voyelle et de ton. Il
est vraisemblable que ce substrat tai-b&ecirc; du dialecte chinois
de J&iacute;zh&agrave;o est la survivance d'une langue disparue
qui &eacute;tait le cha&icirc;non reliant les
langues thai-yaay du Guangxi et le b&ecirc; de Hainan. La situation
g&eacute;ographique de
J&iacute;zh&agrave;o corrobore tout &agrave; fait cette
vue. Comment dans ce cas expliquer la pr&eacute;sence d'une strate
similaire dans le tay yo dont la localisation actuelle ne
s'ins&egrave;re pas dans le continuum
g&eacute;ographique qui relie la r&eacute;gion des langues
thai-yaay et celle du b&ecirc;? L'hypoth&egrave;se
d'une ancienne r&eacute;partition de la branche yaay (northern tai)
sur une zone allant du Guizhou au Thanh H&oacute;a
et au Nghệ An, a &eacute;t&eacute; avanc&eacute;e par
Chamberlain (1991: p. 120). Cette hypoth&egrave;se est recevable,
mais elle pr&eacute;sente l'inconv&eacute;nient d'escamoter le
berceau des langues viet-muong pourtant bien
ancr&eacute; dans le nord du Vietnam. Je pense, quant &agrave;
moi, qu'il s'agit plut&ocirc;t d'un
ancien d&eacute;placement de population, comme il y en a eu
beaucoup &agrave; l'&eacute;poque de la domination
chinoise, dans le but d'installer des populations fid&egrave;les
pour mieux contr&ocirc;ler le pays. Par la
suite, cette langue d&eacute;plac&eacute;e aurait
&eacute;t&eacute; recouverte par la langue d'une partie des
Thai venus du Muong Theng en donnant
naissance &agrave; ce qui devait devenir le tay yo (et le tay
muong), ne laissant subsister qu'un
substrat r&eacute;duit. L'autre partie de cette expansion des Thai
aurait donn&eacute; les Tay
Daeng actuels." (Ferlus 2008: 308-309)</p>
<p>Ferlus,
Michel. 2008. The Thai
dialects of Nghệ An, Vietnam (Tay Daeng, Tay Yo, Tay Muong). In Anthony
Diller, Jerold A. Edmondson &amp; Luo Yongxian (eds.), <i>The
Tai-Kadai languages</i>,
298&ndash;316. London &amp; New York:
Routledge.</p>
