<br>
Certains des manuscrits lus proviennent de biblioth&egrave;ques
europ&eacute;ennes; d'autres ont &eacute;t&eacute;
&eacute;tudi&eacute;s sur place, en r&eacute;alisant des
photos des originaux et des enregistrements audio de lectures par des
informateurs (consultants) tai. Les manuscrits tai yo proviennent de la
biblioth&egrave;que de l'<a href="http://www.efeo.fr/">Ecole
Fran&ccedil;aise d'Extr&ecirc;me-Orient</a>
(cotes "Tha&iuml; 8", "Tha&iuml; 9" et "Tha&iuml; 10"
du catalogue de l'EFEO) et du fonds Maspero de la <a
 href="http://www.aibl.fr/societe-asiatique/" target="_blank">Soci&eacute;t&eacute;
Asiatique</a>. Un
d&eacute;veloppement informatique est en cours pour rendre les
enregistrements consultables avec un alignement temporel avec les
manuscrits et leur annotation. <br>
