           <div class="page-header"><h1>Corpus
hung (dialectes: toum,&nbsp;ly h&agrave;, t&agrave;y poọng, phong)</h1></div>
           <div class="text-left"> <p class="intertitre">&nbsp;</p>
            <h4 align="left">L'usage des ressources du corpus hung est
provisoirement r&eacute;serv&eacute; par leur auteur (Michel Ferlus).
Pour demander l'acc&egrave;s &agrave; ces donn&eacute;es, merci de bien
vouloir <a href="mailto:jrmferlus@orange.fr">prendre contact avec
l'auteur</a>.</h4>
Au sein du groupe
vietique, le
sous-groupe hung comporte deux zones dialectales: pong (poọng) / phong
/ tay pong d'une part ; toum, đan lai, ly h&agrave; de l'autre.
            <p style="text-align: justify;" class="intertitre">"Des
parlers de ce
sous-groupe ont
&eacute;t&eacute; connus d&egrave;s le d&eacute;but du
[XXe] si&egrave;cle gr&acirc;ce aux collectes
in&eacute;dites du P&egrave;re Th. Guignard (1907) sur le <span
 style="font-style: italic;">Hung</span>
et le <span style="font-style: italic;">Kh&ocirc;ng
Kh&ecirc;ng</span>. Ces deux
termes sont aujourd'hui sortis de l'usage et c'est la raison pour
laquelle nous avons retenu l'un des deux pour d&eacute;signer le
sous-groupe. Les deux vocabulaires, bien not&eacute;s
gr&acirc;ce aux qualit&eacute;s de lexicographe du
missionnaire, couvrent en fait deux variantes d'une m&ecirc;me
langue dont les locuteurs se reconnaissent aujourd'hui sous l'autonyme
de <span style="font-style: italic;">Pong</span>,
en vietnamien Poọng [<span style="font-weight: bold;">pɔːŋ⁴</span>]
et en lao <span style="font-style: italic;">Phong</span>.
Le vocabulaire <span style="font-style: italic;">K'katiam-Pong-Houk</span>
de Paul Macey (1907), h&eacute;las moins bien not&eacute;,
repr&eacute;sente &eacute;galement la m&ecirc;me langue.
Dans cette derni&egrave;re expression <span style="font-style: italic;">K'katiam</span>
doit pouvoir s'analyser en <span style="font-style: italic;">Kha Cham</span>,
c'est-&agrave;-dire "les Kha de Muong Cham", tandis que dans <span
 style="font-style: italic;">Pong-Houk</span>
seul le premier terme est identifiable. Le vocabulaire <span
 style="font-style: italic;">Toum Phong</span>
utilis&eacute; par A.G. Haudricourt (1966) est, malgr&eacute;
son nom, &eacute;galement du pong. Les parlers des <span
 style="font-style: italic;">Toum</span>
[<span style="font-weight: bold;">tuːm&sup1;</span>],
des <span style="font-style: italic;">Đan
Lai</span> et des <span style="font-style: italic;">Ly H&agrave;</span>
n'ont &eacute;t&eacute;
r&eacute;v&eacute;l&eacute;s que dans les ann&eacute;es
soixante (Mạc Đường 1964). Ils se diff&eacute;rencient nettement du
pong par plusieurs traits phon&eacute;tiques et une influence
r&eacute;cente du vietnamien. Les <span style="font-style: italic;">Pong</span>
sont repr&eacute;sent&eacute;s des deux
c&ocirc;t&eacute;s de la fronti&egrave;re, dans le district
de Tương Dương (Nghệ An, Vietnam), et dans la r&eacute;gion de
Muong Cham (Bolikhamxay, Laos). Les <span style="font-style: italic;">Toum</span>
ne sont repr&eacute;sent&eacute;s que du
c&ocirc;t&eacute; lao o&ugrave; ils vivent
dispers&eacute;s au milieu des populations Tay dans le massif qui
s&eacute;pare les deux pays, tandis que les&nbsp;<span
 style="font-style: italic;">Đan Lai</span>
et les <span style="font-style: italic;">Ly
H&agrave;</span>, de langue
identique, sont surtout repr&eacute;sent&eacute;s dans le
district de Con Cu&ocirc;ng. Le P&egrave;re Guignard rapporte
qu'au d&eacute;but du si&egrave;cle les <span
 style="font-style: italic;">Hung</span>
&eacute;taient tr&egrave;s nombreux dans la r&eacute;gion
et que le Chao Muong de Khamkeut &eacute;tait un des leurs.</p>
            <p style="text-align: justify;" class="intertitre">Dans
l'ensemble, ces
populations
pratiquent le m&ecirc;me type d'agriculture que leurs voisins Lao
ou Tay. Les <span style="font-style: italic;">Poọng</span>
de Tương Dương ont &eacute;t&eacute;
&eacute;valu&eacute;s &agrave; un peu plus de 700
personnes. On ne dispose malheureusement pas de chiffres pour le Laos
mais on peut penser que l'ensemble des locuteurs que nous avons
d&eacute;nomm&eacute;s <span style="font-style: italic;">Hung</span>
doit pouvoir s'&eacute;valuer entre 2.000 et 3.000
personnes."&nbsp;</p>
            <p style="text-align: right;" class="intertitre">(Pages
15-16 de: Ferlus, Michel.
1996. <a
