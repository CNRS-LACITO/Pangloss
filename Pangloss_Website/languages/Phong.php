           <div class="page-header"><h1>Corpus
hung (dialectes: toum,&nbsp;ly hà, tày poọng, phong)</h1></div>
           <div class="text-left"> <p class="intertitre">&nbsp;</p>
            <h4 style="font-style: italic;" align="left">Ce corpus a été ouvert au public en 2019, sous licence Creative Commons.</h4>
Au sein du groupe
vietique, le
sous-groupe hung comporte deux zones dialectales: pong (poọng) / phong
/ tay pong d'une part ; toum, đan lai, ly hà (liha) de l'autre.
            <p style="text-align: justify;" class="intertitre">"Des
parlers de ce
sous-groupe ont
été connus dès le début du
[XXe] siècle grâce aux collectes
inédites du Père Th. Guignard (1907) sur le <span style="font-style: italic;">Hung</span>
et le <span style="font-style: italic;">Không
Khêng</span>. Ces deux
termes sont aujourd'hui sortis de l'usage et c'est la raison pour
laquelle nous avons retenu l'un des deux pour désigner le
sous-groupe. Les deux vocabulaires, bien notés
grâce aux qualités de lexicographe du
missionnaire, couvrent en fait deux variantes d'une même
langue dont les locuteurs se reconnaissent aujourd'hui sous l'autonyme
de <span style="font-style: italic;">Pong</span>,
en vietnamien Poọng [<span style="font-weight: bold;">pɔːŋ⁴</span>]
et en lao <span style="font-style: italic;">Phong</span>.
Le vocabulaire <span style="font-style: italic;">K'katiam-Pong-Houk</span>
de Paul Macey (1907), hélas moins bien noté,
représente également la même langue.
Dans cette dernière expression <span style="font-style: italic;">K'katiam</span>
doit pouvoir s'analyser en <span style="font-style: italic;">Kha Cham</span>,
c'est-à-dire "les Kha de Muong Cham", tandis que dans <span style="font-style: italic;">Pong-Houk</span>
seul le premier terme est identifiable. Le vocabulaire <span style="font-style: italic;">Toum Phong</span>
utilisé par A.G. Haudricourt (1966) est, malgré
son nom, également du pong. Les parlers des <span style="font-style: italic;">Toum</span>
[<span style="font-weight: bold;">tuːm¹</span>],
des <span style="font-style: italic;">Đan
Lai</span> et des <span style="font-style: italic;">Ly Hà</span>
n'ont été
révélés que dans les années
soixante (Mạc Đường 1964). Ils se différencient nettement du
pong par plusieurs traits phonétiques et une influence
récente du vietnamien. Les <span style="font-style: italic;">Pong</span>
sont représentés des deux
côtés de la frontière, dans le district
de Tương Dương (Nghệ An, Vietnam), et dans la région de
Muong Cham (Bolikhamxay, Laos). Les <span style="font-style: italic;">Toum</span>
ne sont représentés que du
côté lao où ils vivent
dispersés au milieu des populations Tay dans le massif qui
sépare les deux pays, tandis que les&nbsp;<span style="font-style: italic;">Đan Lai</span>
et les <span style="font-style: italic;">Ly
Hà</span>, de langue
identique, sont surtout représentés dans le
district de Con Cuông. Le Père Guignard rapporte
qu'au début du siècle les <span style="font-style: italic;">Hung</span>
étaient très nombreux dans la région
et que le Chao Muong de Khamkeut était un des leurs.</p>
            <p style="text-align: justify;" class="intertitre">Dans
l'ensemble, ces
populations
pratiquent le même type d'agriculture que leurs voisins Lao
ou Tay. Les <span style="font-style: italic;">Poọng</span>
de Tương Dương ont été
évalués à un peu plus de 700
personnes. On ne dispose malheureusement pas de chiffres pour le Laos
mais on peut penser que l'ensemble des locuteurs que nous avons
dénommés <span style="font-style: italic;">Hung</span>
doit pouvoir s'évaluer entre 2.000 et 3.000
personnes."&nbsp;</p>
            <p style="text-align: right;" class="intertitre">(Pages
15-16 de: Ferlus, Michel.
1996. <a href="http://sealang.net/sala/archives/pdf8/ferlus1996langues.pdf" target="_blank">Langues et peuples viet-muong</a>. <span style="font-style: italic;">Mon-Khmer
Studies</span> 26. 7–28.)</p>
