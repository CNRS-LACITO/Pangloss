      <p>Les Hayu habitent une région du Népal située 50 à 100 km au sud-est de Kathmandu, dans la vallée de la rivière Sun Kosi et plus au sud à travers la chaîne du Mahabharat. On les appelle &quot;Hayu&quot; en népali et ils se nomment eux-mêmes &quot;Wayu&quot; (transcrit &quot;Vayu&quot; par Brian Hodgson au XIXe s.).</p>
      <p>Le hayu appartient à la famille tibéto-birmane. Il a été classé par Shafer (1955) dans la section &quot;West-Central Himalayish&quot; (proche du &quot;East Himalayish&quot; ou kiranti) du groupe Bodique dans le Sino-tibétain. Il est parlé couramment dans les villages de Murajor, Sukhajor VDC, dans le district de Ramechhap et Manedihi et Adhamara, qui dominent la vallée de la rivière Marin, dans le district de Sindhuli. Il y a peut-être 300 locuteurs, tous bilingues hayu-népali, le népali étant la langue nationale indo-aryenne.</p>
      <p>Les documents présentés ont été enregistrés et les premières transcriptions établies en 1972 par B. Michailovsky et Martine Mazaudon à Murajor.</p>
