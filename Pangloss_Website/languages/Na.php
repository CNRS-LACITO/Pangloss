<p style="text-align: justify;">Le lac de /ɭo˧ʂv̩˩/,
appel&eacute; &laquo; lac Lugu &raquo; en chinois, est
g&eacute;n&eacute;ralement d&eacute;sign&eacute; en na
comme &laquo; le Lac &raquo;, /hi˩nɑ˧mi˧/. Il tient une place
centrale dans la g&eacute;ographie du territoire na, et est l'un
des principaux lieux symboliques de la culture na/mosuo. Cette
l&eacute;gende raconte comment il serait n&eacute; d'un
d&eacute;luge caus&eacute; par l'avidit&eacute; des hommes.
Elle explique aussi l'origine des bateaux traditionnellement
employ&eacute;s sur le lac.
Trois versions de ce r&eacute;cit ont &eacute;t&eacute;
enregistr&eacute;es, en 2006, 2007 et 2008. La locutrice
consid&egrave;re la troisi&egrave;me comme la plus
satisfaisante ; c'est donc cette version qui a
&eacute;t&eacute; choisie pour le travail de transcription. </p>
<p>&nbsp;</p>
<h3><a name="tones"></a>Transcription
des tons</h3>
<div style="text-align: justify;">Les tons dans la forme phonologique
de surface sont indiqu&eacute;s
dans la transcription au niveau de la phrase. Le symbole ǀ est
employ&eacute; pour indiquer la division en groupes tonals,
&agrave; l'int&eacute;rieur desquels s'appliquent les
r&egrave;gles de &laquo; grammaire tonale &raquo;. Les tons
lexicaux (sous-jacents) sont indiqu&eacute;s dans les gloses mot
&agrave; mot. La mise en regard de ces deux niveaux permet
d'&eacute;tudier les nombreux processus tonals de cette langue.
</div>
<p>La complexit&eacute; du
syst&egrave;me rend n&eacute;cessaire l'emploi de deux symboles
non standard en plus des &laquo; lettres tonales &raquo; de
l'Alphabet Phon&eacute;tique International : <br>
</p>
<ul>
  <li>
    <div style="text-align: justify;"> le symbol #
repr&eacute;sente la fin du mot lexical. Ainsi, #H est un ton haut
qui ne peut se r&eacute;aliser qu'apr&egrave;s la
derni&egrave;re syllabe du mot auquel il est lexicalement
attach&eacute;. Ce ton ne se manifeste donc pas lorsque le mot est
dit &agrave; l'isol&eacute;e; le ton est dit&nbsp;<span
 style="font-style: italic;">flottant.</span></div>
  </li>
  <li style="text-align: justify;"> le dollar $
est employ&eacute;, dans l'expression H$, pour repr&eacute;senter une
cat&eacute;gorie tonale distincte aussi bien de H# (un ton haut final)
que de #H (un ton haut flottant). Le ton H$ a tendance &agrave;
s'associer &agrave; la derni&egrave;re syllabe du&nbsp;groupe
phonologique. En premi&egrave;re approximation, on pourrait dire
que&nbsp;H$ est un
ton haut qui se trouve r&eacute;alis&eacute; juste avant la fin
du groupe phonologique, donc sur sa derni&egrave;re syllabe; mais en
r&eacute;alit&eacute;, le comportement de ce ton est soumis &agrave;
des r&egrave;gles&nbsp;morphosyntaxiques (autrement dit, pas simplement
phonologiques) d&eacute;crites en d&eacute;tail dans <a
 href="http://langsci-press.org/catalog/book/109" target="_blank">une
monographie publi&eacute;e en 2017</a>.&nbsp; </li>
</ul>
