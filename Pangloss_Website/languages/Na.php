<p style="text-align: justify;">Ce mythe raconte l'origine du
rite pratiqu&eacute; lors du d&eacute;c&egrave;s d'une
femme qui s'est mari&eacute;e et a quitt&eacute; la maison
o&ugrave; elle est n&eacute;e. Ce rite, nomm&eacute;
&laquo;sɯ˧kʰɯ˩&raquo;, remonterait &agrave; un
si&egrave;cle environ d'apr&egrave;s la conteuse : il aurait
&eacute;t&eacute; &eacute;tabli &agrave; un moment
o&ugrave; l'institution du mariage commen&ccedil;ait
&agrave; gagner la plaine de Yongning, en contradiction avec la
coutume plus ancienne selon laquelle les enfants passent leur vie
enti&egrave;re dans la maisonn&eacute;e de leur m&egrave;re
et de ses fr&egrave;res et s&oelig;urs (tantes et oncles
maternelles), avec leurs fr&egrave;res et s&oelig;urs et leurs
cousins du c&ocirc;t&eacute; maternel, le r&ocirc;le social
du p&egrave;re/amant &eacute;tant discret voire
quasi-inexistant. D'apr&egrave;s la conteuse, l'institution de ce
rite refl&egrave;terait l'acceptation de l'existence de cas de
mariage, tout en les repr&eacute;sentant comme douloureux : le
mariage distend les liens entre la femme et sa famille d'origine.
Trois versions sont pr&eacute;sent&eacute;es ici,
enregistr&eacute;es sur trois ans. La premi&egrave;re et la
troisi&egrave;me comportent transcription et gloses. Le meilleur
enregistrement au plan technique/acoustique est celui de la
troisi&egrave;me version.
</p>
