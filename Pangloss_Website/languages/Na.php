<h1>Corpus na</h1>
</div>
<div class="text-left"><b>La
langue na (endonyme : /nɑ˩-ʐwɤ˧/, 'na'+'langue') </b>
est parl&eacute;e &agrave; la fronti&egrave;re des
provinces chinoises du Yunnan et du Sichuan, aux abords du lac Lugu
(lo˧ʂv̩˩-hi˩nɑ˧mi˧).
<p> Elle appartient au groupe
naish de la famille sino-tib&eacute;taine, qui comprend
&eacute;galement le naxi et le laz&eacute;. Elle est
&eacute;galement appel&eacute;e&nbsp;<span style="font-style: italic;">mos(u)o</span>.
</p>
<p style="text-align: justify;">En
2010, il lui a &eacute;t&eacute; accord&eacute; une
entr&eacute;e &agrave; part, sous le nom
translitt&eacute;r&eacute; de "Narua", dans l'inventaire des
langues du Summer Institute of Linguistics (code: nru).
Jusque-l&agrave;, le na &eacute;tait
consid&eacute;r&eacute; comme un dialecte de la langue naxi.</p>
<div style="text-align: justify;">La
grande majorit&eacute; des documents offerts ici &agrave; la
consultation et au t&eacute;l&eacute;chargement ont
&eacute;t&eacute; recueillis dans la plaine de Yongning (nom en
na&nbsp;: /ɬi˧di˩-di˩mi˩/), et proviennent du parler
d&eacute;crit dans une monographie parue en 2017 (<a
 href="http://www.langsci-press.org/catalog/book/109" target="_blank">disponible
en ligne</a>). Des "carnets de terrain" (blog) au sujet du na sont
consultables&nbsp;<a href="../fieldwork/Na_fw-Yongning.htm">ici</a><span
 style="text-decoration: underline;"></span>.
</div>
