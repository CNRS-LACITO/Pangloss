<p><br>
Des corrections mineures ont &eacute;t&eacute; apport&eacute;es au
syst&egrave;me entre une premi&egrave;re description publi&eacute;e en
2008 (Michaud 2008) et le livre de 2017 :<br>
</p>
<ul>
  <li> pour les disyllabes, il
existe une cat&eacute;gorie tonale LML, distincte de LM. Un exemple
&eacute;tait rapport&eacute; dans l'article de 2008 mais il y
&eacute;tait consid&eacute;r&eacute; comme une exception.
D'autres exemples ont &eacute;t&eacute; observ&eacute;s
depuis. </li>
  <li style="text-align: justify;"> lorsqu'un groupe tonal
ne comporte que des tons
bas, un ton post-lexical est ajout&eacute; &agrave; la fin de
la s&eacute;quence tonale. Du fait de l'absence d'oppositions
entre un sch&eacute;ma LM et un sch&eacute;ma LH dans ce contexte, il
est malais&eacute; de d&eacute;terminer si le ton
ajout&eacute; est un ton M(oyen) ou un ton H(aut). Ce ton
post-lexical &eacute;tait d&eacute;crit comme M dans l'article
de 2008 ; depuis, un argument phonologique a &eacute;t&eacute;
trouv&eacute; qui sugg&egrave;re qu'il s'agit d'un ton H, et
c'est ainsi qu'il est not&eacute; dans la transcription des textes
pr&eacute;sent&eacute;s ici.&nbsp; </li>
</ul>
Afin d'&eacute;viter un
d&eacute;calage trop important entre la transcription et la
prononciation r&eacute;elle, la <b>transcription
adopt&eacute;e ici n'est pas strictement phon&eacute;mique</b>.
En particulier : <br>
<ul>
  <li> les voyelles transcrites
comme [o] et [u] sont allophones d'un m&ecirc;me phon&egrave;me
; par souci de pr&eacute;cision phon&eacute;tique, [o] est
employ&eacute; apr&egrave;s les dentales et les uvulaires, et
[u] apr&egrave;s les autres consonnes (bilabiales,
alv&eacute;olo-palatales, r&eacute;troflexes, uvulaires et
glottales)
  </li>
  <li> la nasale palatale [ɲ] peut
&ecirc;tre analys&eacute;e comme allophone de /ŋ/
  </li>
  <li> les syllabes sans consonne
initiale en re&ccedil;oivent une au plan phon&eacute;tique: /i/
est r&eacute;alis&eacute; [ʝi], /u/ [wu], /ɯ/ [ɣɯ], etc. (La
situation est similaire en naxi.) Ces syllabes sont transcrites ici
avec leur initiale phon&eacute;tique : [ʝi], [ɣɯ], etc.
  </li>
</ul>
<p>En revanche, les voyelles
apicales [ʅ] et [ɿ] sont not&eacute;es selon l'analyse
phon&eacute;mique : comme /ɯ/. Au sujet de l'analyse
phon&eacute;mique, voir, &agrave; nouveau, <a
 href="http://langsci-press.org/catalog/book/109">l'ouvrage paru en 2017</a>.</p>
<p>Les <b>gloses
grammaticales </b> suivent pour
l'essentiel l'analyse propos&eacute;e par Liberty Lidz (Lidz 2010);
il va de soi que le transcripteur est seul responsible des
diff&eacute;rences introduites sur certains points. Les
abr&eacute;viations standard sont utilis&eacute;es pour les
concepts qui figurent dans la liste d'abr&eacute;viations des
Leipzig Glossing Rules (Comrie, Haspelmath, and Bickel), tandis que le
terme entier est employ&eacute; pour toutes les autres gloses. Les
gloses techniques sont pr&eacute;c&eacute;d&eacute;es d'un
symbole sp&eacute;cial &deg; qui les diff&eacute;rencie de
simples traductions.</p>
