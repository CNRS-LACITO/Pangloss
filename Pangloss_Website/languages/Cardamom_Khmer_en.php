            <h2><a name="language"></a>1.
About Khmer</h2>
            <p>Khmer,
also called Cambodian, is the official language of Cambodia and is a
member of the Mon-Khmer subgroup of languages, within the Austroasiatic
family (Sidwell 2009).</p>
            <h3>1.1. Khmer in the
landscape of Mon-Khmer languages</h3>
            <p style="text-align: justify;">Ferlus
(1979, 1992) distinguishes
among Mon-Khmer languages those that are
conservative&nbsp;(such as Khmu or Laven) and those that are
innovative
in terms of the evolution of initial stops. Khmer is among the
innovative languages, as it developed a complex vowel system from the
transfer of earlier oppositions on initial consonants -- the same
historical source from which Vietnamese, another Mon-Khmer language,
developed a two-way split of the tone system.</p>
            <p style="text-align: justify;">The
innovative developments all constitute compensatory answers
to the loss of the voicing feature of initial stop consonants. The
three attested scenarios are (i) the development of constrastive
voice-type
registers (<span style="font-style: italic;">registrogenesis</span>,
see Jones 1986 and Matisoff 2001), (ii) the two-way split of the vowel
system, or (iii) the two-way split of the tone system (Haudricourt
1965). </p>
            <p style="text-align: justify;">Khmer
played a major role in the discovery of this fundamental
phenomenon, which shaped the prosodic systems of most East and
Southeast Asian languages. In a groundbreaking study, Henderson (1952)
established the sequence of events from voicing oppositions on initials
to phonation-type oppositions and finally to vowel contrasts</p>
