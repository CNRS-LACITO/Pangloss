            <p style="text-align: justify;">A
more recent development, still undetectable in Henderson's time, is the
development of contrastive tone from the reduction of initial consonant
clusters: *kr and *k<sup>h</sup>
merge to k<sup>h</sup>,
and the earlier lexical
oppositions are preserved as a tonal contrast (Kirby 2014).</p>
            <p style="text-align: justify;">Khmer
bears traces of diverse influences (Higham 2002 : 14). Sanskrit
and Pali principally gave cultural, religious and royal lexicon. Among
colloquial registers, Thai, Lao, Vietnamese, Cham gave Khmer
several words. According to Higham, "It has been shown that Indian
religion, architecture,
scripts and the Sanskrit language were incorporated into the indigenous
cultures. This occurred among Mon speakers in the Chao Phraya Valley,
Khmer speakers of the middle and lower Mekong and the speakers of Cham
in central coastal Viet Nam." (Higham 2002 : 288; see also Lewitz 1967).</p>
            <h3>1.2. The special
interest
of the Cardamom dialect</h3>
            <p style="text-align: justify;">Khmer
is mainly spoken&nbsp;in
Cambodia and close border areas&nbsp;(Surin Khmer
dialect, Thailand
and Krom Khmer dialect, Mekong River Delta in South Vietnam). The
variety known as "Cardamom
Khmer" differs notably from Central Khmer. According to Ferlus
(1992), all Khmer dialects can be traced back to Middle Khmer: "For us,
from the point of view of historical phonetics and within the limits of
the present study, Middle Khmer begins at the
start of the 16th century." / "Pour nous, d'un point de vue de
phonetique historique et dans les limites de cette &eacute;tude, le
khmer
moyen commence avec le XVIe si&egrave;cle." (Ferlus 1992 : 58).</p>
            <div style="text-align: center;"><img
 style="width: 413px; height: 234px;" alt="middlekhmer"
 src="khmer_dialects.jpg"><br>
            </div>
            <p style="text-align: justify;">Cardamom
Khmer dialect is spoken on both sides of the border between Western
Cambodia and Eastern Central Thailand (Chanthaburi Province). <o:p></o:p>It
has a vowel system which remains highly similar with that of Middle
Khmer. According to Martin
(1992), following the conquest of the town of Longwek by
Siam&nbsp;in 1591,
the staff of the Court and inhabitants of Longwek took shelter in
Cardamom mountains area. Since
that time, Cardamom Khmer and Central Khmer are thought to have

diverged gradually.</p>
            <div style="text-align: justify;"></div>
            <p style="text-align: justify;">Cardamom
Khmer has a&nbsp;distinction between first register (high register:
slightly taut, modal voice, middle-range pitch, vowel opening) and
second
register (low register: slightly breathy, lower pitch, stable vowel
quality). Cardamom Khmer thus&nbsp;preserves clear traces of the
wide
range of phonetic correlates of the register opposition,
which was one of the main characteristics of Middle
Khmer, and has been lost in many Khmer dialects, where vowel
differences are now paramount (Ferlus, 1979, 1992; Henderson 1952;
Martin 1975; Pain 2014). The Mon language as
described by Shorto (1962) and Christian Bauer (PC) seems to be more
conservative than
Standard Khmer in this respect, preserving a register system with a
broad range of phonetic correlates (voice quality, vowel quality, and
some differences in pitch).</p>
