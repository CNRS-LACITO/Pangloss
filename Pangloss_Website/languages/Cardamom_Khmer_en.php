            <h2><a name="2_The_archived_documents:_Marie"></a><a
 name="documents"></a>2.
The archived documents:&nbsp;Marie Alexandrine Martin's audio
recordings</h2>
            <p style="text-align: justify;">Marie
Alexandrine Martin (1932 - 2013) was a botanist and an ethnologist. She
worked for the
CNRS Research Center on East Asia at Paris Sorbonne University (<a
 href="http://www.creops.paris-sorbonne.fr/chercheurs_fiche.php?id=176">CREOPS</a>).
Between
1965 and 1972, she conducted
ethnobotanical and ethnolinguistic
fieldwork in the Cardamom mountains (Khmer and Pear villages) and
also with Khmer and Pear people from Thailand. </p>
            <p style="text-align: justify;">A
selection of Marie Alexandrine Martin's bibliographic records can be
found on the website of the&nbsp;<a
 href="http://aefek.free.fr/travaux/news00010359.html" target="_blank">AEFEK</a>
(Association
d&rsquo;Echanges et de Formation pour les Etudes
Khm&egrave;res). In addition to her
scholarly publications, she wrote books for the general public based
her observations about Cambodia as she experienced it from
1965 to the late&nbsp;90's (Martin, 1989 and 1997).</p>
            <p>The
recordings made available here consist of a collection of audiotape
records, collected by herself from the
late 60&rsquo;s and the middle 70&rsquo;s. She entrusted a copy
of her
recordings to Michel Ferlus, who suggested that they should be
digitized along with his own collections.&nbsp;Marie Alexandrine
Martin&rsquo;s heirs enthusiastically agreed to the archiving and
online diffusion of her collection. Digitization was conducted by
Alexis Michaud, and cataloguing by Michel Ferlus and Julien
Heurdier.&nbsp;The
set consists of several
recordings of Khmer dialects and Pear languages (Pear, Chong, and
Samre)
spoken in or around the Cardamom mountains (West Cambodia and East
Thailand). It also includes a short recording of Brao and Tampuan
(Bahnaric languages of Ratanakiri province, North-East
Cambodia). </p>
