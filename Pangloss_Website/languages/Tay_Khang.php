<h2 align="center"><a name="Vietnamese"></a>Ngữ
liệu tiếng Tay Khang</h2>
Truyện kể Tay Khang được kể bởi một cộng t&aacute;c vi&ecirc;n cao tuổi
đến từ huyện Khamkeut, tỉnh Bolikhamsai, L&agrave;o. Truyện lịch sử
n&agrave;y được ghi &acirc;m tại Ban Pha Vang [Paksan, L&agrave;o].<br>
Số h&oacute;a c&aacute;c
tư liệu n&agrave;y l&agrave; một
phần của dự &aacute;n
k&eacute;o d&agrave;i từ
th&aacute;ng 9/2014 đến th&aacute;ng 2/2016, với ng&acirc;n
s&aacute;ch từ chương tr&igrave;nh Thư viện Khoa học Kỹ thuật
số <a href="http://www.bibliothequescientifiquenumerique.fr/"
 target="_blank">(Digital Scientific Library)</a><span
 style="font-style: italic;">, một chương
tr&igrave;nh của Bộ Gi&aacute;o dục đại học v&agrave;
Nghi&ecirc;n cứu Ph&aacute;p.&nbsp;</span>
